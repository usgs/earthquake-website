<p>
  The<a href="centennial.pdf"> Centennial Catalog (Engdahl and Villase&ntilde;or, 2002)</a> (2.75MB PDF) is a global catalog of locations and magnitudes of instrumentally recorded earthquakes from 1900 to 2008. It is being periodically updated as new arrival time data for recent years become available.
</p>
<p>
  This is a catalog of large earthquakes, created with the purpose of giving a realistic picture of the seismicity distribution in the Earth. It has been assembled by combining existing catalogs, reducing all available magnitudes for each earthquake to a common, corrected magnitude (same magnitudes as List of &quot;<a href="/data/sign_eqs.pdf">Preferred Magnitudes of Selected Significant Earthquakes</a>&quot;-Word file), and relocating the earthquakes with available arrival time data.
</p>
<p>
  For recent years (1964&ndash;present) a cut-off magnitude of 5.5 has been chosen for the catalog, and the catalog is complete down to that threshold. For the period prior to 1964 (also referred to as &quot;historical instrumental&quot; or simply &quot;historical&quot; period) the cut-off considered is magnitude 6.5. Between the 1930's and 1963 the catalog is complete to te magnitude 6.5 threshold, but prior to that, the catalog is only complete down to magnitude 7.0.
</p>
<p>
  Another important feature of the catalog is that most of the earthquakes (those with available arrival time data in digital form) have been relocated using the same locations method (EHB: Engdahl, van der Hilst and Buland, 1998) which features more accurate travel time tables and procedures for better determination of the focal depths.
</p>

