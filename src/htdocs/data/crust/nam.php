<p>Many well-known features are reproduced here, such as the thin
  crust of the Basin and Range and the thick crust of the US Great Plains and
  Midcontinent. The map also reveals a number of new features. The thickest
  crust is beneath Late Proterozoic regions, not the oldest crust (e.g., Superior
  Province, central and eastern Canada). The map shows 40+ km thick crust beneath
  the US Cascades, while thin crust is imaged to the north beneath the Canadian
  Cordillera.</p>
<p>These features were only hinted at in previously published
  contour maps (Soller et al., 1982, and Allenby and Schnetzler, 1983). In addition,
  the extent of the continental shelves along the Pacific and Atlantic coasts
  are more faithfully reproduced in terms of crustal thickness. The average thickness
  of the North American continental crust, including margins, is 36.5 km ranges
  from 14 km to 60 km.</p>
