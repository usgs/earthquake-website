<h3>Conclusions</h3>
<p>A close correspondence exists between high average crustal
  velocity and accretionary and magmatic orogenies (e.g., Trans-Hudsonian, Grenvillian,
  Acadian, Appalachian, the Cascades, and the Alaskan Range). In contrast, there
  are a number of interesting anomalously low average crustal velocities in the
  Basin and Range Province, the Snake River Region, and the Northwest Territories
  of Canada. Many of these are regions of active or recent extension or hotspot
  activity.</p>
