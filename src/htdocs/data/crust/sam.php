<p>These contour maps depict the seismic structure of the crust and uppermost mantle of South America and the surrounding oceanic basins. Notable geographic features include a region across northern Chile and northeast Argentina with anomalously low P- and S-wave velocities in the crust. Geographically, this corresponds to the shallowly-subducted portion of the Nazca plate (the Pampean flat slab first described by Isacks et al., 1968), which is also a region of crustal extension. The thick crust of the Brazilian craton appears to extend into Venezuela and Colombia. The crust in the Amazon basin and along the western edge of the Brazilian craton may be thinned by extension. The average crustal P-wave velocity under the eastern Pacific seafloor is higher than under the western Atlantic seafloor, most likely due to the thicker sediment layer on the older Atlantic seafloor.</p>

