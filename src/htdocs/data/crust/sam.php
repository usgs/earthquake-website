<h2>Conclusions</h2>
<ol>
	<li>The weighted average thickness of the crust under South America is 38.17 km (standard deviation, s.d. +/- 8.7 km), which is ~1 km thinner than the global average of 39.2 km (s.d. +/- 8.5 km) for continental crust.</li>
	<li>The average P-wave velocity of the crystalline crust (Pcc) is 6.47 km/s (s.d. +/- 0.25 km/s). This is essentially identical to the global average of 6.45 km/s.</li>
	<li>The average Pn velocity beneath South America is 8.00 km/s (s.d. +/- 0.23 km/s), slightly lower than the global average of 8.07 km/s.</li>
</ol>

