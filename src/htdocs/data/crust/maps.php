<h3>Age of Last Thermo-Tectonic Event</h3>
              <a href="age.html" title="Click to download"><img src="images/age2.jpg" alt="black and white contour map" width="300" height="159" /></a>
              <p>Thermo-tectonic age (or, age of the last thermo-tectonic event) refers to the
  age in which the last major event to influence the crustal structure took place.
  This event is generally consistent with the geologic province. It is assumed
  that, in the time since this event took place, the crustal structure was not
  significantly modified by more recent tectonic events, although cooling processes
  will have effected some crustal parameters through time. This study of
  the relationship between thermo-tectonic age and crustal structure is the first
  of its kind. You can see some of the results in the form of <a href="/data/crust/histoAge.html">histograms</a>
  of crustal thickness vs. thermo-tectonic age. Contrary to earlier
  thought, there is little change in mean crustal thickness before the Cenozoic
  and Mesozoic.</p>
