This region is named after the Aldan river, a tributary of the great Lena River.  In language here is of the Turkish family, and "Aldan" or "Altyn" means "gold":  gold mining was the first industry in this area after the Russians arrived there.  In early years, the local people were mostly hunters, so historical information about earthquakes is very poor and begins from only 1776.
</p>

<p>
  In defining this region, the western boundary between the Aldan and Baikal Regions was chosen at 122E.  This is the longitude, where the Yablonovy ridge in the Baikal Region, extending in a NE direction, is sharply crossed by Stanovoy ridge, running here in a SE direction.  Another significant ridge, called the Dzgugdzhur, curves slightly along the coast of the Sea of Okhotsk.  Together, these two ridges form an arc of about 140 degrees, extending from 122E to 132E, with a geometrical center at East Siberia.
</p>

