<p>
  The local seismic surveys in this region previously did not include depth among the parameters reported in their catalogs.  The Obninsk Bulletin mostly gives a depth of 3 or 33 km; this simply reflects which travel-time table was used.  The General catalog mostly gives depths of 15 km, assuming that this value is typical.  It is therefore assumed that only values which are different from 3, 15 or 33 km are not default values, but are actually the results of determinations.
</p>
