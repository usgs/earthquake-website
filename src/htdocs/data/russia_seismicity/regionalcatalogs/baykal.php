  The Baykal region (48-58N; 100-122E) can be divided on three parts with different kinds of seismicity:  Baykal Lake, itself, and branches of secondary rift zones are of high level of seismicity; Southeast of Lake Baykal, the seismicity appears scattered, with no clear structure; Northwest of Lake Baykal, there were very few earthquakes recorded by the network, although there are a number of events from industrial blasting in quarries.
</p>
<p>
  Careful paleoseismic study in this region has resulted in the identification of 30 earthquakes in the period from year 200 till the end of the nineteenth century [REF].  Historical information was found for more than 250 earthquakes, and their time, location and magnitudes were estimated by _ [REF].  The General Catalog includes 558 earthquakes for the time period before 1961.  Since 1955, the Obninsk Bulletin contains 17 earthquakes; 8 of these have identical parameters in the General Catalog.
</p>

<p>
  In the first few years of the operation of the local network, the lower limit of event size for inclusion in the catalog was about Energy Class K=7; nevertheless, about 100 earthquakes with 5 < K < 6 were reported.  The number of events reported during the five-year period from 1962-1966 is 5,394 (more than a thousand per year.  In 1967, the lower limit of Energy Class for publication was raised, to K=9.  During the 23 years from 1967 until 1990, the average number of earthquakes in catalog decreased by two-thirds, to an average of about 300 per year.
</p>
