<p>
  During 1985-1988, the observations were done in the delta of Lena River. During this period (9 months) 140 earthquakes were recorded, and 121 of them were located. Nevertheless, the time of observation was so short, that the data obtained becomes important for understanding this zone, where the mid-oceanic ridge transforms into scattered continental seismicity.
</p>
