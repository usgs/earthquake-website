<p>
  The Laptev Sea zone is where the Middle Arctic Ocean seismic belt abuts the continental shelf of Siberia. In the beginning of the XX century, several earthquakes as large as M=5.5-6.8 occurred here. The very poor seismic network here makes the seismicity badly known. In 1968, the SevMorGeo Institute (Geology of the North Sea) began seismological observations here, using a few temporary seismic stations. Observation was possible only in the summers. The instrumentation was by VEGIK with a flat frequency response 0.8-10 Hz and magnification about 30-40X.
</p>

