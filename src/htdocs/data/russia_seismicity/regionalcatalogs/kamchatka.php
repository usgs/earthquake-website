<p>(51-58N; 145-170E)</p>

<p>
  Note that this region includes the Okhotsk Sea, where the deep earthquake epicenters are located in the Kamchatka Benioff zone.
</p>

<p>
  Kamchatka is one of the most active seismic region in Russia. The Kamchatka Composite Regional Catalog includes 6,696 earthquakes from the General Catalog and 1939 events from the Obninsk Bulletin. Fifty events from the Obninsk Bulletin catalog are identically duplicated in the General Catalog. Many earthquakes in the General Catalog have origin times and coordinates different from Obninsk's. Catalog data from the ESSN annuals were not available yet in electronic format and, therefore, the small events are not yet well represented in the Composite Regional Catalog. Only the energy class values (KFS after S.Fedotov -- not identical to K after Rautian), were mentioned as having been used to calculate magnitude M in the General Catalog.
</p>

<p>
  Seismic events are known from 1737, from historical data. In the period from 1737 to 1900, many large events are known, and spread over a range of depths:
</p>

