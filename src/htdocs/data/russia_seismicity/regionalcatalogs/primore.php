<h2>Primore and Priamurie Regions Seismicity Map and Composite Regional Catalog</h2>
<a href="../download/primore.ZIP">EXCEL</a><br />
<p>(48-52N; 122-141.5E and 43-48N; 130-141.5E)</p>

<p>
  The name Priamore means "Near-Sea Land" and Priamurie, "Near Amur River Land". The local seismicity in the crust is not highly active, and deep earthquakes are also observed. More than a half of the large earthquakes here (M=6 and more) are deep &endash; some as deep as 600 km. Small events (M=3.5-4.5) are mostly shallow.
</p>
<p>
  A dozen earthquakes are known to have occurred before 1900, and about fifty were recorded between 1900 and 1961. The recent record has about a half thousand earthquakes. All of them are from the General Catalog and the Obninsk Bulletin. At present, no local data are available from the Annual (ESSN) Catalogs.
</p>
