<p>
  While many recent events have beene recorded, there are no data in the Annual useful for the discrimination earthquakes and explosions (other than origin time). The Apatity explosions begin in the early morning, about 5-6 local time, and continue until 1900h --very few are found at night. The apparent dependence of the number of events in earthquake catalog upon the time of day (most occur from 6 to 18 GMT; or 9-21 local time) is evidence that most events are explosions. This daytime distribution of events suggests that most of these events are explosions. But some, about 10 or more are probably natural earthquakes. Approximately 10-15 --from total about 150-- are not many, but they deserve to get attention.
</p>
<p>
  The list of announced explosions near Apatity in 1991-1992 was published with their location (mines) and the charge pointed. These data are published elsewhere, in a separate catalog.
</p>

<h3>Central Russia Region</h3>
<p>(52-58N; 30-52E)</p>

<p>
  Most seismological data for this region are historical, beginning from 1467. Half of 18 earthquakes occurred before 1900, the other half occurred from 1900 to 1956. Four events are duplicated in the different source catalogs, and their parameters, such as time, intensity (Io) and magnitude (M) are not the same. Four events listened in the General Catalog, and other ones (marked as Historical, "Hist‚Äù) were taken from collection by Nikonov and Anayin, scattered in different publications. Only one event was found in the Obninsk Bulletin catalog. No local seismic survey was here. Western surveys, such as ISC and NOA, recorded eight events at this area, between the years 1968-1989. They were apparently missed by Russian seismological stations. So this region is almost a "blank spot".
</p>
