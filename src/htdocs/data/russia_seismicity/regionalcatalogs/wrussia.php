<h3>North Russia Region</h3>
<p>(58-70N; 30-52E)</p>

<p>
  The local seismic survey is located in Apatity, near the center of the mining and quarring industry. That survey has monitored the natural seismicity around the White Sea and Spitsbergen, where the seismic station Piramida operates. The most attention is paid to the blasting at few quarries close to Apatity station. Their data does not regularly appear in the Annual (ESSN) catalog. The largest number of reported events was between 1987 and 1990, and the overall numbers of events does not represent the overall level of both natural and industrial seismic activity.
</p>
<p>
  In the early instrumental period, the local seismicity was studied by the Panasenko, working at the Apatity station. His catalog data are included in the General Catalog. Recent papers in the Annual described the natural seismicity around the White Sea, which in interpreted to result from the same general tectonic process as that of all Scandinavia.
</p>
<p>
  The regional catalog of events includes the epicenters at southern part of region: Karelia and the great Lakes, Ladoga and Onega. There are also historical data for some natural earthquakes there.
</p>
