<p>
  Ukraine is a region with low seismicity. The rare earthquakes there look like a marginal extension of Carpathian seismicity. Most of the earthquakes hypocenters are in the crust; a very few have a depth about 100 km. Stations recording these earthquakes are located in the western part of region and in Carpathia (Lvov, Uzhgorod, Rakhov, Kosov, Mezhgorie, Morshin, Nizhneye Selische, Trocnik, Gorodok), or to the south, in Crimea (Yalta, Feodosia, Simferopol, Sevastopol, Alushta, Kasantin, Sudak). There is, however, active blasting in the Ukraine. The seismic surveys have worked hard to eliminate the quarry blasts from their catalogs, but some of them probably still linked.
</p>
