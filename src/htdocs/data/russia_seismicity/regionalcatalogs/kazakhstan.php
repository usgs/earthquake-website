<h2>Kazakhstan Seismicity Map and Composite Regional Catalog</h2>

<a href="../download/kazakhstan.ZIP">EXCEL</a><br />
<p>(45-58 N; 65-80 E)</p>

<p>
  Kazakhstan is a marginal zone with low- and poorly-studied seismicity. The official northern boundary of the North-Tien Shan seismic survey is 45N. The territory northwest of 45N does not belong to any seismic survey. Its south-east corner is effected by the high activity of the Dgungaria zone.
</p>
<p>
  Along the Eastern boundary of Kazakhstan, there is a band of epicenters, which geographically and geologically belongs to the West side of the Altai seismic zone. Some of them are located in close proximity to Semipalatinsk Test Site (49.5 N; 77-79 E). The two earthquakes that have occurred in the North-West part of the region are a part of the seismicity of the Ural Region.
</p>
<p>
  Historically, the Zerenda seismic station recorded many of explosions within a few hundreds km of it, but these records never were processed. Some events are believed to be earthquakes because of following:
</p>
<ol>
  <li>They occurred at nighttime; </li>
  <li>They are present in Soviet-era catalogs (large explosion were usually excluded); </li>
  <li>The magnitudes are about 4 or more, considered too big for normal blasting. </li>
</ol>

<p>
  For example, the event of Aug.1, 1994 is located in the vicinity of several quarries or mines (48 N, 67 E). Nevertheless, it is believed to be an earthquake because of its magnitude, MLH=4.6 and MPVA=4.8, and early origin time.
</p>
