  Two peaks are seen, corresponding to GMT from 1 to 4 and about 21-23.  Local time here is about 10 hours later. So the first clear peak corresponds to 12-14 pm (lunch break), and the second one to 7-8 am (morning).  This suggests that about fifty events (of a total of 379 small events in the ESSN  catalog) are industrial blasts.   The total number of earthquakes in the Composite Regional Catalog for Chukotka during 1962-1999 is, therefore, as little as about 450.  Nevertheless, 38 earthquakes have occurred with at least one of the three (Obn) magnitudes (MLH, Msps, Mlpl) of 5.0 or more, and six with M Â³ 6.
