<p>
  Officially, the Sayan Region is a part of the "Altai-Sayan Zone", monitored by the seismic survey in Novosibirsk. But the character of the seismicity of the Sayans differs from that of the Altai, with the rate of seismicity much higher in Sayans. The industrial blasting is not significant here. Of the earthquakes of this region from 1776-1961, 176 are included in The General Catalog; only five from that period are found in the Obninsk Bulletin, two of these are identically replicated in the General Catalog. Six large earthquakes (M from 7 to 8.3) are known here at period 1761-1931, 22 are of M=6-6.9, 80 events of M=5-5.9 are in the General Catalog till 1962.
</p>
<p>
  Some of the other earthquakes in the General Catalog are the same as reported in the Obninsk Bulletin or the ESSN annuals, but differ in origin time or/and coordinates.
</p>
<p>
  The depth values in Regional Catalog are mostly listed as 3, 33 and 15 km. The first two came from the Obninsk Bulletin, indicating what travel time curve they use to process the data. The value of 15 is an assumed depth, which was believed by the compilers of the General Catalog to be typical for the region. Local surveys avoided any depth estimation because of big distance between the seismic station and very few cases of epicenter being under station. Unfortunately, until 1980 the energy class, K, was rounded to an integer; only from 1981 does the first decimal place of K appear. The General Catalog uses K to calculate M. However, they then rounded the calculated M, as in 3.5, 4, 4.4. It was found that the random scattering of K estimation on a single station is about 0.35 unit of K if there is no significant site effect.
</p>
