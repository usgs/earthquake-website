  </figure>
  Central Asia is one of the most high seismicity zone in Central Eurasia.  This Region includes the Hindu Kush zone, where a large number of deep earthquakes occur.  Earthquakes here are felt in Tadjikistan and Kirgyzstan.  Local networks in Tadjikistan, Kyrgyzia, and Uzbekistan consist of many closely-located stations, and the epicentral locations in this region are of high accuracy.  In the relatively small (100x150 km) area where the Garm Expedition worked from 1955 until 1992, more than 80,000 earthquakes were listed.
</p>
<p>
  The Central Asian Regional Catalog (35-45N; 65-82E) includes 74 historical earthquakes from the year 250 BCE to 1899, with magnitudes ranging from 5 to 8.3.  In the early instrumental period (1900-1961), the General Catalog contains 1068 earthquakes, compared to about 200 in the Obninsk Bulletin. About half of them have identical parameters in the two Catalogs.
</p>
<p>
  During period 1962-1999, 6549 Central Asian earthquakes were taken from the General Catalog, and 1964 from Obninsk catalog.  Only 123 of them have identical parameters in both sources; many more differ in origin time or/and coordinates.
</p>
<p>
  A significant fraction of the earthquakes are deep, with depths 50 to 300 km or more.  There are 329 deep earthquakes reported for the period before 1962 and about 3,000 from 1962-1999.
</p>
