<p>
  The seismic observations in Crimea begin in 1927. Crimean Peninsula looks like a small continent, with it's own tiny Benioff zone and epicenters reaching depths of 40-50 km. These are located mostly in the local Black Sea, an area of oceanic crust. There is even volcanism here (a mud one) in the Northern part of the region.
</p>

<p>
  The seismicity is low here, with no earthquakes with M&gt;5.5 occurring since the damaging event of 1927. However, during the last 2000 years, there are the historical data pointing to a large event in 63 BC, with M=6.8. Six earthquakes with M=&gt;6 are known before 1616. Up until 1961, twenty four events with M=&gt;5.0 are included in the General catalog for this region. The last such earthquake (M=5.5) occurs in 1957.
</p>
