<h2>Eastern Siberia Seismicity Map and Composite Regional Catalog</h2>

<a href="../download/siberia_east.ZIP">EXCEL</a><br />

<p>
  This broad region begins from the Laptev Sea and extends to the North-West. To the South, it contacts with the seismically-active Aldan region; to the South East it touches the neck of the Kamchatka peninsula. Some researchers believe that the Kamchatka seismic belt is the Western boundary of the North American plate, and even a rift zone, called “Mom Rift”. Some believe it to be the extension of the Middle Arctic Ocean rift.
</p>
<p>
  However, the character of the seismicity here (which is significantly scattered) and dominant fault plane solutions (many of them are of thrust-type) shows that the tectonic situation is more complicated. Only 13 earthquakes are known at period from 1735 to 1899; three of them have magnitudes from 5.7 to 6.5. In the early instrumental period, 1900-1961, thirty-three earthquakes are listed in the General Catalog.
</p>
<p>
  After local stations began operation, many earthquakes were recorded. In the ESSN bulletins, there are 3254 earthqakes with M from 1.5 to 6.5. Of these,134 are identically duplicated in the General Catalog; five event characterizations are identical in the Obninsk catalog. Many listings for events are nearly the same in the Obninsk and General catalogs, but do have not identical coordinates and origin times.
</p>
