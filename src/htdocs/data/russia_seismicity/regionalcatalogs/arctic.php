  <p>
    The locations of Arctic earthquakes in the New and General Catalogs were based on the data from all of these international seismic stations.  Of the 41 earthquakes that make up the Regional Arctic Catalog before 1962, eight of them were taken from the Obninsk Bulletin.  The magnitude range is 4.5-6.8.
  </p>

  <p>
    Later, with the installation of the seismic stations Kheis (on Franc-Joseph Land), Barenzburg and Piramida (Spitsbergen), along with stations in Siberia, arctic earthquakes could be located with Soviet stations alone, and they appeared in the Obninsk Bulletin and the ESSN Catalogs.
  </p>

  <p>
    In 1968, SevMorGeo (Inst. of Geology of the North Sea) began observations using temporary seismic stations (see Table, below).  The instrumentation was with the system "VEGIK", with a flat frequency response from 0.8-10 Hz and magnification about 30k-40k.
  </p>

