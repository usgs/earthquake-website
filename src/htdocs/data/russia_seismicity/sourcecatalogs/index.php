<h2>Principal Earthquake Catalog Sources of Russia and the Former Soviet Union</h2>
<p>
  <figure>
    <img src="../images/sourcecats.gif" alt="Catalogs of Earthquakes in Russia"/>
  </figure>
  By the end of the 20th century, four principal earthquake catalogs had been compiled and published (one electronically):
</p>
<ul>
  <li>
    The <a href="obninsk.php">Obninsk Bulletin</a>, issued every 10-days in the period from 1955 through 1999;
  </li>

  <li>
    The annual series, <a href="ESSN.php">Earthquakes in the USSR</a> (since 1991, "Earthquakes in Northern Eurasia‚Äù), consisting of the regional catalogs of the ESSN, or Unified System of Seismic Observation.  Published annually in Russian since 1962, the most recent issue, published in 2000, was for earthquakes in 1994.
  </li>

  <li>
    The <a href="strong.php">New Catalog of Strong Earthquakes in the USSR,</a> compiled by Shebalin and Kondorskaya (1974).
  </li>

  <li>
    The <a href="general.php">General Catalog of Earthquakes in North Eurasia</a>, compiled by the Institute of Physics of the Earth under the direction of W. Ulomov (and an inclusive extension of the "New Catalog").
  </li>
</ul>

<p>
  In compiling the composite regional catalogs, additional event data were taken from the catalog published by SevMorGeo, the book, "The Seismic Regionalization of USSR Territory", and various other publications.  Some non-instrumental data were taken from a collection of felt earthquakes by Nikonov and Ananin (still being compiled).  At present, not all of the available data are in electronic format.
</p>
