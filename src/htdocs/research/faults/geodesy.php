<h2>Near-Field Geodesy</h2>

<p>
  The USGS is at the cutting edge of measuring ongoing deformation of the Earth&rsquo;s surface, a field known as geodesy. In order to measure the rate at which the Earth&rsquo;s crust deforms between, during and after earthquakes, precise measurements need to be made along active faults zones. USGS scientists have long established alignment arrays, which are stable markers that cross faults zones and can be measured to determine the rate of slip on the fault zone. USGS scientists, along with collaborators from universities, have established a network of hundreds of alignment arrays across the major faults of northern California.
</p>
<p>
  USGS scientists are also at the leading edge of utilizing 3D laser scanning to map the Earth&rsquo;s surface and objects at and near the Earth&rsquo;s surface in order to quantify the rates and patterns of crustal deformation. This includes using 3D laser scanning technology from tripod mounts as well as mobile platforms (vehicles, backpacks, balloons, etc)  that enable measurement of landscapes at centimeter-level precision over large areas. These techniques allow USGS scientists to provide rapid scientific response to damaging earthquakes, and to advance our understanding of the physics of earthquakes and how earthquakes affect the Earth in three-dimensions and through time.
</p>

