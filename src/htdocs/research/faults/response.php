<p>
  USGS geologists respond to damaging earthquakes in active tectonic regions of the United States and around the world, rapidly providing critical information to stakeholders. Earthquake response activities include rapid assessment of landscape change (including mapping and measuring the locations and amount of offset caused by faults rupturing the Earth&rsquo;s surface), identifying ground failure and liquefaction, and mapping landslides. On-the-ground investigations are complemented by airborne surveys, rapid 3D laser scanning, and rapid acquisition and analysis of remote imagery to study inaccessible regions.
</p>
<p>
  Additional activities include quantifying and forecasting ongoing post-seismic deformation, identifying locations where critical infrastructure may be damaged, and providing assessment of ongoing hazards to critical infrastructure.
</p>
<p>
  USGS is able to provide information quickly to all levels of government, utilities, media and citizens about earthquake effects and ongoing hazards. Observations and data collected immediately following a damaging earthquakes helps scientists determine ongoing hazard and also allows them to better understand earthquake process and effects which then guides assessment of future hazard.
</p>

