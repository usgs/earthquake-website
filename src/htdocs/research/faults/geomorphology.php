<h2>Tectonic Geomorphology</h2>

<p>
  Repeated earthquakes shape the Earth over the millennia and fault zones often have unique and diagnostic landforms caused by the faulting process. These include steep scarps, folds, elongate ridges, sag ponds, offset terraces, and linear valleys, and deflected, offset and uplifted streams. By studying these landforms, USGS geologists uncover the location and pace at which faults deform the Earth&rsquo;s surface. Determining the rate at which a faults &ldquo;slips&rdquo; is a key piece of information for assessing the hazard that a fault presents to people and infrastructure. Scientists use  airborne laser mapping elevation data to create remarkable visualizations of the shape of the Earth&rsquo;s surface, even in areas covered by vegetation.
</p>
<p>
  The study of landscapes affected by tectonics, often referred to as &ldquo;tectonic geomorphology,&rdquo; also provides important clues about seismic hazard associated with areas beyond the well-defined fault traces. The mountain ranges along the California coast are testament to the combination of sliding and squeezing that occurs at the boundary between the Pacific and North American tectonic plates. Mountains grow as a result of many earthquakes that occur over time as one side of a fault moves up relative to the adjacent side, or a large area is bent and warped upward. Some earthquakes, such as the 1989 Loma Prieta earthquake in the Santa Cruz Mountains south of San Francisco, are associated with the growth of mountains. These types of hazards are better understood by studying uplifting landforms such as marine terraces and other sedimentary deposits and by analyzing the patterns of river channel topographic profiles.
</p>

