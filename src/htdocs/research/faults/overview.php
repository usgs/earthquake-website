<h2>Overview</h2>

<h3>Alaska</h3>
<p>
  <strong>Alaska has more large earthquakes than the rest of the United States combined.</strong> More than three-quarters of the state’s population live in an areas that can experience a magnitude 7 earthquake. Moreover, all the state's infrastructure centers are located in seismically vulnerable areas. The trans-Alaska pipeline transports about 17% of the Nation’s crude oil, and there are significant federal land holdings and military facilities in earthquake-prone regions. A trans-Alaska natural gas pipeline is likely to be constructed in the next 5-10 years, and all the proposed routes traverse or parallel active fault traces. The ability to prepare for and mitigate the effects of future earthquakes is critical to maintaining the economic health of the region and Nation. In  Alaska scientists are investigating the processes of earthquake generation on major fault systems throughout the state and along the southern margin, which will increase our general understanding of fault systems that can generate large earthquakes. This work is also examining relationships between the faults and active volcanoes.  The scientific work is helping to develop a chronology of ancient earthquakes on different parts of some of the major fault systems in Alaska.  A better understanding of earthquake hazards in Alaska is vital to the economic health and well being of Alaska.
</p>

<h3>Pacific Northwest</h3>

<figure class='left'>
  <img src="images/pnwfaults.gif" alt="Pacific Northwest faults">
  <figcaption>
    Major faults in the Pacific Northwest urban corridor.
  <figcaption>
</figure>

<p>
  <strong>Scientists are conducting investigations across the Pacific Northwest</strong> with a strong emphasis on understanding the earthquake hazards in the heavily populated urban corridor from Eugene, Oregon to Vancouver, British Columbia and across the Yakima Fold and Thrust belt, where significant infrastructure is located. In this area, where the oceanic tectonic plate is diving under the continental plate, hazards can come from:
</p>
<ul>
  <li>earthquakes occurring within the shallow continental crust</li>
  <li>earthquakes within the subducting oceanic slab</li>
  <li>earthquakes along the interface between the subducting oceanic slab and the overlying continental crust</li>
  <li>tsumanis from local and distant sources</li>
</ul>
<p>
  Scientists are characterizing the hazards posed by these three earthquake source zones, as well as the hazards posed by volcanoes, with the goal of helping the region develop effective mitigation strategies.
</p>
<p>
  In urban areas of the Pacific Northwest, research is helping to develop products, such as seismic hazard maps, that are used to design and implement effective earthquake mitigation strategies. These products are based on extensive new geologic, geophysical, and seismological investigations that document active crustal and interplate faults and prehistoric earthquake magnitudes and recurrence intervals, and estimate site response and amplification.
</p>
<p>
  Research objectives in the Pacific Northwest include:
</p>
  <ul>
    <li>contribute to the development of detailed seismic hazard maps for the Seattle and Portland areas that incorporate site response, basin effects, and rupture directivity</li>
    <li>document the location, geometry, and slip rates of active crustal faults in the Puget Lowland</li>
    <li>develop a database of the chronology and magnitude of large prehistoric crustal and interplate earthquakes and tsunamis in the Pacific Northwest</li>
    <li>formulate models for the Pacific Northwest, especially the Seattle and Portland areas, that show wave propagation and ground motion for large scenario earthquakes from diverse sources</li>
    <li>incorporate new geological, geophysical, and seismological data in regional and national seismic hazard maps. The paleoseismic results of this project will be incorporated into the Quaternary fault database. The geophysical results will be incorporated into representative community velocity models.</li>
  </ul>
</p>

<h3>San Francisco Bay Area</h3>

<figure class='right'>
  <img src="images/sfbayfaults.jpg" alt="San Francisco Bay Area faults">
  <figcaption>
    From "Earthquakes and Faults in the San Francisco Bay Area (1970-2003)", Scientific Investigations Map 2848.
  </figcaption>
</figure>

<p>
  <strong>The San Francisco Bay Area has the highest density of active faults of any urban area in the Nation.</strong> The probability of one or more large (M6.7) urban earthquakes in the next 30 years is high, estimated at 62%. The ability to prepare for and mitigate the effects of these future earthquakes is critical to maintaining the economic and social fabric of the region. This can be accomplished through an increased understanding and characterization of the timing, size, and location of past earthquakes, which are based on paleoseismic information in addition to LiDAR.  LiDAR is a remote sensing technology that measures distance by illuminating a target with a laser and analyzing the reflected light.  This method is able to “see through” heavily vegetated and forested areas to the ground surface and provide topographic data in an area that would otherwise be difficult to access and collect this information.  Paleoseismic and earthquake geologic studies of historical surface ruptures in a range of tectonic environments also provide critical data for evaluating general aspects of fault behavior and input into decisions on the national seismic hazard map.
</p>
<p>
  In the San Francisco Bay Area, they are doing studies to improve the knowledge of the various earthquake sources.  They are trying to understand the three-dimensional nature of the fault system and the variation in size and timing of past earthquakes.  Scientists are also investigating the faults outside of the Bay Area region that will  increase the general understanding of the behavior of large earthquake-generating fault systems like the San Andreas Fault.
</p>

<h3>Southern California</h3>

<figure class='left w300'>
  <img src="images/scafaults.jpg" alt="Southern California faults">
  <figcaption>
    Faults from Jennings, 1994; Landsat image from Jet Propulsion Laboratory of the California Institute of Technology.
  </figcaption>
</figure>

<p>
  <strong>Southern California has the highest level of earthquake risk in the United States</strong>, with half of the expected financial losses from earthquakes in the Nation expected to occur in southern California. Sitting astride the Pacific - North American plate boundary at the Big Bend of the San Andreas Fault, Southern California has over 300 faults capable of producing magnitude 6 earthquakes. Affecting the more than 20 million inhabitants of the Los Angeles and San Diego metropolitan areas, this complex set of faults presents the greatest urban risk in the United States.  All aspects of the earthquake problem can be addressed in Southern California, using the modern earthquake networks that have been developed over the past decade. The high level of earthquake activity and the complexity of the fault systems in the area provides a unique natural laboratory for the study of the physics of earthquakes. Scientists are studying fault interaction by comparing the seismic behavior in southern California to analogous areas in the world with large strike-slip faults, to provide insight into possible past and future earthquakes in the region.
</p>

<h3 style="clear:left;">Intermountain West</h3>

<figure class='right'>
  <img src="images/trench.jpg" alt="Wasatch fault zone colluvium">
  <figcaption>
    Shear zone and fault-scarp-derived colluvium exposed along the Wasatch fault zone near Draper, Utah. Photo by Rich Briggs, USGS.
  </figcaption>
</div>

<p>
  <strong>Motion between the North American plate, the Pacific plate, and the remnant of the Juan de Fuca plate off the coast of the Pacific Northwest, is causing deformation throughout western North America.</strong> The majority of this deformation is occurring close to the plate boundaries in California and offshore Oregon and Washington, but significant ongoing deformation extends eastward across the Basin and Range Province and throughout the Intermountain West to the eastern front of the Rocky Mountains. Even though the rates of seismicity are commonly modest throughout the Intermountain West and the adjacent Rocky Mountain region, historical earthquakes (such as the 1983 Borah Peak, and the 1954 Dixie Valley), coupled with evidence of older surface faulting, demonstrates the existence of a significant, but poorly quantified seismic hazard. Also unknown are what effects strong shaking from an earthquake would have in most of the region's urban centers. In order to better quantify the seismic hazard of this InterMountain region, scientists are conducting geological and geophysical studies that contribute to a better understanding of the levels of hazard and risk, particularly in the more populous areas.
</p>
<p>
  Scientists are determining the shapes of the basins and the types and depths of the basin deposits in order to estimate the expected shaking under the urban areas that sit in these basins.  They are attacking problems at a variety of scales ranging from detailed studies of individual faults to regional studies that clarify critical details of the tectonic processes operating in the region.
</p>

<h3>Central and Eastern US</h3>

<figure class='left w300'>
  <img src="images/newmadrid.jpg" alt="New Madrid Seismic Zone">
  <figcaption>
    Locations of earthquakes &ge; magnitude 2.5 (yellow circles) and locations where subsurface faulting has been detected (red stars).
  </figcaption>
</figure>

<p>
  <strong>Earthquakes in the Central and Eastern US are low probability, high impact events on enigmatic sources.</strong> They can cause widespread damage because of low attenuation rates and an aging building stock not designed to withstand strong earthquake shaking. When coupled with the large population centers in the CEUS (e.g., Memphis, St. Louis, Boston, Charleston, Washington D.C.), these low probability events could possibly result in substantial losses – often as high or higher than in portions of the seismically active West.  Scientists are trying to improve our understanding of earthquakes in these areas, such as when have they happened in the past, where and when are they likely to happen in the future, and what will be the effects?
</p>

