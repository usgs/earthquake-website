<p>
  The Imperial and Coachella Valleys, located in the Salton Trough, are forming by active plate tectonic processes. From the Imperial Valley southward into the Gulf of California, plate motions are rifting the continent apart. In the Coachella Valley, the plates are sliding past one another along the San Andreas and related faults. These processes build the stunning landscapes of the region, but also produce damaging earthquakes.
</p>
<p>
  Rupture of the southern section of the San Andreas Fault, from the Coachella Valley to the Mojave Desert, is believed to be the greatest natural hazard that California will face in the near future. With an estimated magnitude between 7.2 and 8.1, such an earthquake would result in violent shaking, loss of life, and disruption of lifelines (freeways, aqueducts, power, petroleum, and communication lines) that will bring much of southern California to a standstill.
</p>
<p>
  As part of the nation&rsquo;s effort to avert a catastrophe of this magnitude, a number of projects are underway to more fully understand and mitigate the effects of such an event. One project, funded jointly by the National Science Foundation (NSF) and the U.S. Geological Survey (USGS), is to understand through &ldquo;seismic imaging&rdquo; the structure of the Earth surrounding the San Andreas Fault, including the sedimentary basins on which cities are built.</p>
<p>This project, the Salton Seismic Imaging Project (SSIP), will create images of underground structure and sediments in the Imperial and Coachella Valleys and adjacent mountain ranges to investigate the earthquake hazards they pose to cities in this area.  Importantly, the images will determine the underground geometry of the San Andreas Fault, how deep the sediments are, and how fast earthquake energy can travel through the sediments.  All of these factors determine how hard the Earth will shake during a major earthquake.  If we can better understand how and where earthquakes and strong shaking will occur, then buildings can be better designed or retrofitted to resist damage and collapse, and emergency plans can be prepared.
</p>

