<p>
Not only was the Patton Bay splay fault system far away from the main subduction zone boundary, but also the tsunami it caused arrived quickly and caused extensive, if localized, damage. Scientists wondered if this splay fault scenario was common in other subduction zone areas, and if so, how could it be identified?  If they could learn more about what happened during the 1964 event, then they may be able to spot other areas that could give rise to tsunamis that were generated closer to the shoreline than the main subduction boundary, and residents could be warned immediately to evacuate if there were a large earthquake.
</p>
<p>
With more work, scientists found that megathrust splay faults appear to be common to many subduction zones.  Splay faults don’t always rupture during a large subduction zone earthquake, so scientists wondered why did they rupture in some places sometimes, in other places often, and in yet other locations, rarely. These <a href="/learn/glossary/?term=thrust%20fault">thrust faults</a> seemed to extend from the sea floor all the way down to the subduction zone boundary.  The &ldquo;thrust&rdquo; means that some part of the earth’s crust is thrust upward.  Splay faults were thought to be the cause for localized large tsunamis in subduction zone earthquakes, such as the 1932 Mexico, 1944 Japan, 1945 Persian Gulf, and the 2010 Ecuador events. However, there was no direct evidence, and the mechanism was not well known. Because splay faults are typically offshore, they are not easily accessible.
</p>

<h2>New Images of Splay Faults</h2>

<p>
New techniques to image the subsurface have recently been developed, and scientists recognized that they could be used to study the megathrust splay faults in Prince William Sound.  The results showed that there are many active and inactive splay faults along parts of the Aleutian subduction zone, extending far beyond Montague Island.  The imaging also showed more detail about the splay faults than scientists were able to see previously, and so they were able to figure out how they work and how they cause tsunamis, at least in the Prince William Sound.
</p>
<p>
They found uplifted blocks consist of <a href="/learn/glossary/?term=accretionary%20wedge">accretionary wedge</a> material that is squeezed up by contraction in this area when there is slip on the main subduction zone boundary. An accretionary wedge is a collection of material that is scraped off the top of the oceanic plate as it dives under the continental plate.  The wedge grows each time there is slip on the subduction boundary. The oldest material in the wedge nearer the shore becomes strong and more rock-like from compression, while the younger, newest, material further from shore is still weak.
</p>

