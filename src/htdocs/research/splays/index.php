<p>
&quot;Tsunami&quot; became a more common household word after the devastating M9.1 Sumatra earthquake on December 26, 2004. The world now has a greater awareness of what a tsunami is and what it can do.
</p>
<p>A tsunami is generated when there is sudden up or down movement of the seafloor, typically from slip on a fault or a landslide. A landslide will create a depression on the ocean's surface above. Slip on a fault can create a depression if the ocean floor drops, or a &quot;hill of water&quot; on the ocean's surface if the ocean floor has uplift. The sea surface above the shifted seafloor is suddenly either higher or lower than the otherwise flat ocean, and in either case, the water will move in order to make the ocean surface flat again. The movement of the water creates a wave that moves outward in all directions, much like when a rock is thrown into a pond.  As the volume of water moves across the ocean, it may die out before it reaches a land mass. Or it can rise up as a large swell as it approaches land due to shallower water near the coast. Certain coastal geometries, like harbors, for example, can make the swell even larger as it is squeezed into a smaller horizontal space.
</p>
<p>
What causes displacement of the seafloor during an earthquake?  We know that both large-scale and small-scale sudden uplift of the crust can occur in large earthquakes along <a href="/learn/glossary/?term=subduction%20zone">subduction zone</a> boundaries where an oceanic <a href="/learn/glossary/?term=tectonic%20plates">tectonic plate</a> is subducting, or diving, under a continental plate.  But if we want to understand the hazards in a particular location, and have a better idea of what to expect when there is a large earthquake in these places, we need to learn more.
</p>

