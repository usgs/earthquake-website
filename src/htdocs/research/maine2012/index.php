<p>
  On October 16, 2012 at 7:12pm EDT an M4.0 earthquake occurred in southern Maine that was widely felt.  Since moderate-size earthquakes don’t happen that often in New England, a small group of scientists spent some time afterwards in the general vicinity to collect information on the earthquake effects.  The data they collected provided a “ground-truth” check on the <a href="/data/dyfi/">Did You Feel It? (DYFI)</a> data that were collected through the USGS's online questionnaire following the earthquake.
</p>

<p>
  The New England area is not even remotely close to a modern plate tectonic boundary, so it doesn’t experience earthquakes very often. An earthquake does happen somewhere in New England every once in a while, but it’s usually too small to associate with a specific fault.  Scientists think these infrequent earthquakes are caused by modern plate stresses on old faults, but the exact mechanism is not known.
</p>

<p>
  There are two named seismic zones in the region.  The Charlevoix seismic zone is the most active in the northeast U.S. and is centered in the Saint Lawrence Seaway.  The October 2012 M4.0 earthquake was in the northeast Massachusetts seismic zone, further to the south. The statistics for these two seismic zones indicates that on average there will be an M4.6 about every 20 years, an M5.0 about every 50 years, and an M7.0 about every 2,500 years.
</p>

<p>
  The largest earthquake to occur in Maine was on March 21, 1904, before there were any accurate instruments in the area.  It is estimated to have been about a magnitude 6, based on the shaking intensity data (maximum <a href="/learn/topics/mercalli.php">Modified Mercalli Intensity (MMI)</a> of approximately VII).  The largest instrumentally-recorded earthquake was an M4.8 on June 21, 1973 in western Maine.
</p>

