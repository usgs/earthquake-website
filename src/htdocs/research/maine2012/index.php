<p>
  The epicenter of the October 2012 earthquake was 35 km west of Portland, Maine, near Waterboro and Hollis Center.  The fault plane it occurred on was on a north-south line dipping steeply to the east.  The rupture propagated from the bottom part of the fault toward the surface, and the motion on the fault was a <a href="/learn/glossary/?term=thrust%20fault">reverse-thrust</a>. (See also the <a href="/learn/animations/animation.php?flash_title=Thrust+Fault+and+Reverse+Fault&flash_file=thrustfault&flash_width=220&flash_height=320">thrust fault animation</a>.) The only damage was a broken chimney and cracks in the plaster of two buildings in Waterboro.  Interviews with 27 citizens indicated a maximum MMI of V-VI, compared with the DYFI maximum MMI of V.  At 15 km from the epicenter, the MMI was about III to IV.  Seismic waves travel more efficiently through the older, harder Northeastern U.S. than in the Western U.S.  This M4.0 was felt as far as 400km away, but if it had occurred in California, it would have been felt only out to 75km.
</p>

<p>
  The ground-truth interviews provided by residents in the Waterboro and Hollis Center areas showed that for moderate-size earthquakes with relatively few DYFI data, the DYFI method of averaging the reported shaking within a Zip Code may average out the strongest shaking. Fortunately, the DYFI data are now all geocoded and averaged at a more detailed, 1-km resolution than allowed by typical Zip Code areas. The geocoded data are an improvement especially for moderate-size events like the M4.0 earthquake in southern Maine.
</p>

