<h3>Fact 1: Fracking is NOT causing most of the induced earthquakes. Wastewater disposal is the primary cause of the recent increase in earthquakes in the central United States.</h3>

<p>Wastewater disposal wells typically operate for longer durations and inject much more fluid than hydraulic fracturing, making them more likely to induce earthquakes. Enhanced oil recovery injects fluid into rock layers where oil and gas have already been extracted, while wastewater injection often occurs in never-before-touched rocks. Therefore, wastewater injection can raise pressure levels more than enhanced oil recovery, and thus increases the likelihood of induced earthquakes.</p>

<h3>Fact 2: Not all wastewater injection wells induce earthquakes.</h3>

<p>Most injection wells are not associated with felt earthquakes. A combination of many factors is necessary for injection to induce felt earthquakes. These include: the injection rate and total volume injected; the presence of faults that are large enough to produce felt earthquakes; stresses that are large enough to produce earthquakes; and the presence of pathways for the fluid pressure to travel from the injection point to faults.</p>

<h3>Fact 3: Wastewater is produced at all oil wells, not just hydraulic fracturing sites.</h3>

<p>Most wastewater currently disposed of across the nation is generated and produced in the process of oil and gas extraction. As discussed above, saltwater is produced as a byproduct during the extraction process. This wastewater is found at nearly every oil and gas extraction well.</p>

<p>The other main constituent of wastewater is leftover hydraulic fracturing fluid. Once hydraulic fracturing is completed, drilling engineers extract the fluids that are remaining in the well. Some of this recovered hydraulic fracturing fluid is used in subsequent fracking operations, while some of it is disposed of in deep wells.</p>

<h3>Fact 4: The content of the wastewater injected in disposal wells is highly variable.</h3>

<p>In many locations, wastewater has little or nothing to do with hydraulic fracturing. In Oklahoma, less than 10% of the water injected into wastewater disposal wells is used hydraulic fracturing fluid. Most of the wastewater in Oklahoma is saltwater that comes up along with oil during the extraction process.</p>
<p>In contrast, the fluid disposed of near earthquake sequences that occurred in Youngstown, Ohio, and Guy, Arkansas, consisted largely of spent hydraulic fracturing fluid.</p>

<h3>Fact 5: Induced seismicity can occur at significant distances from injection wells and at different depths.</h3>

<p>Seismicity can be induced at distances of 10 miles or more away from the injection point and at significantly greater depths than the injection point.</p>

<h3>Fact 6: Wells not requiring surface pressure to inject wastewater can still induce earthquakes.</h3>

<p>Wells where you can pour fluid down the well without added pressure at the wellhead still increase the fluid pressure within the formation and thus can induce earthquakes.</p>

