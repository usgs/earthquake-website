<p>
  Seismologists have observed that both topographic highs and basins have complex
and varying effects on seismic waves. By deploying arrays of seismic recorders
our understanding is improved of what specific features have what specific
effects on the seismic waves.  These studies use basins, ridges, and fault zones
in the San Francisco Bay area as a laboratory to explore in greater detail what
is happening as seismic waves propagate through them.  The results can be used
in similar urban areas throughout the world to estimate the expected shaking
from both local and more distant earthquakes.
</p>

<p>Seismic waves traveling through  basins and topography are affected by:
<ul>
	<li>The edges of the basin</li>
	<li>The age of the basin, which is related to the density of the material in the basin</li>
	<li>Fault zones in these areas</li>
	<li>The direction of approach of the seismic waves and the shape of the feature</li>
	<li>The distance between the location of the earthquake and the feature</li>
	<li>The frequency content of the seismic waves</li>
</ul>
</p>

<p>
    The basic approach employed in these studies is to record seismic waves
    produced from both earthquakes and man-made sources such as explosions, and
    then to use several different mathematical methods and models to try to
    explain the observed levels of shaking. The portable seismographs are set up
    in different arrays in different locations in order to record various
    effects. Our studies are facilitated by using a combination of different
    analysis methods to reveal the wave type, direction, and speed of the
    important components of the ground motion. Both basins and topographic highs
    can trap seismic waves and cause them to be amplified. An understanding of
    these amplification effects is important for proper evaluation of seismic
    hazards in urban areas.
</p>

