<h2>History of the Rock Physics Laboratories</h2>

<p>
  On March 27, 1964 the second largest earthquake in recorded history, with a magnitude of 9.2, occurred under Prince William Sound near the coast of Alaska, as the Pacific crustal plate rapidly slid under the North American plate. The violent shaking and tsunami that ensued destroyed much of the city of Anchorage and many nearby villages. The severity of the earthquake prompted the United States Geologic Survey to establish the National Center for Earthquake Research (NCER) in Menlo Park, California in 1965, building on its substantial capabilities in earthquake geology and crustal studies. At the same time the National Oceanographic and Atmospheric Administration&rsquo;s (NOAA) predecessor organization established the Earthquake Mechanism Laboratory in San Francisco to augment its role in seismic observatory operations.  In 1973 the NOAA earthquake programs were moved to the USGS and the new Office of Earthquake Studies was established to bring together seismologists and geologists. This center has evolved since its early conception into the current Earthquake Science Center in Menlo Park, with field offices in Pasadena and Seattle.   Other centers with earthquake programs within the USGS are located in Golden, Colorado and Reston, Virginia.
</p>

<p>
  Less than a decade after the Alaska earthquake, California&rsquo;s 1971 San Fernando earthquake also caused significant damage and loss of life.  This destructive earthquake provided additional impetus for the United States Congress to establish the National Earthquake Hazards Reduction Program (NEHRP) in 1977  to coordinate funding and emergency response across a variety of federal agencies. Many of these agencies have evolved or merged over the years, and today NEHRP oversees earthquake programs within the USGS, the National Science Foundation (NSF), the National Institute of Standards and Technology (NIST), and the Federal Emergency Management Agency (FEMA).
</p>

