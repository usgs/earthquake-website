<p>
  James Dieterich addressed the problem of earthquake nucleation and propagating by developing a large-scale biaxial press containing a 1.5 meter square rock with a 2 meter fault running diagonally from corner to corner.  The fault is instrumented with an array of slip sensors and strain gages that measure local slip and stress changes at many locations along its length. This unique earthquake-generating machine is large enough to allow both nucleation and rupture propagation to occur on the fault surface. In this way, the slow earthquake initiation process can be studied in detail, as well as the way that the rupture propagates along the fault and ultimately stops. This apparatus, together with a smaller double-direct shearing machine, were used to establish the important rate- and state-dependent friction equations that are used to describe time-dependent processes of earthquake nucleation and occurrence.
</p>

