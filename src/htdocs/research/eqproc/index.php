	<p>
  	The overarching theme of this research is for scientists to discover as much as they can about earthquakes and faulting from field and laboratory observations, and to combine this with geophysical, geological, geochemical, and mathematical (including computational) modeling of earthquake sources and fault zones so as to best improve USGS earthquake hazard assessments.
	</p>
	<p>
  	The high level of earthquake activity and the complexity of the fault systems throughout California area provides a unique natural laboratory for the study of the physics of earthquakes. Scientists are studying fault interaction by comparing the seismic behavior in California to analogous areas in the world with large strike-slip faults, to provide insight into possible past and future earthquakes in the region.
	</p>
	<p>
    In addition, earthquakes are generated in the laboratory under controlled conditions to learn how they start and what indications there might be that they are about to happen. Also, fault zone materials are carefully tested to learn about the physical and chemical processes that control earthquakes.
  </p>
	<p>
  	This research strives to increase the quality and impact of these products, and to reduce their uncertainties, through multidisciplinary research aimed at better understanding the earthquake process. The project works in close collaboration with regionally focused projects and with the <a href="/hazards/">National Seismic Hazard Mapping (NSHM)</a> project in order to support those efforts at issuing earthquake hazard products.
	</p>

	<h3>Some of the key scientific questions scientists seek to answer:</h3>
	<ul>
		<li>How is stress loaded onto faults as a function of space and time by both plate motions and other geological processes?</li>
		<li>How do the stresses redistributed by one earthquake affect the probability of future events?</li>
		<li>Do identifiable earthquakes recur with some average repeat time and definable variation or is each earthquake unique?</li>
		<li>How does the structure of faults control the nucleation of small earthquakes and their growth into larger ones and what does this predict about the distribution of sizes of earthquakes we can expect in a region or along a fault?</li>
	</ul>

<h2>Tracking Stress Buildup</h2>

<figure class='left w300'>
	<img src="images/stressrate.jpg" alt=""/>
	<figcaption>
    Stressing rate of the crust around California derived from two decades of geodetic measurements.
  </pfigcaption>
</div>

<p>The constant plate tectonic motions between the Pacific and North American plates guarantees that the crust in the western US is continually building up stress. The image of crustal velocities provided by extensive GPS coverage reveals where these velocities change rapidly over short distances, demanding that the intervening crustal rock stretch and build up stress over time. Such a map of the stress reveals two main lines where stress is concentrated: The San Andreas fault zone and the Eastern California Shear Zone. These zones have experienced numerous earthquakes over the century and a half that earthquakes have been historically observed.</p>

<p>The mechanism of stress buildup within these fault zones is uncertain. One hypothesis is that the hot rocks below the upper 15-km-thick layer (the upper crust that has the vast majority of continental earthquakes) flows continually in response to periodic earthquakes, forcing the upper crust to bend with this flow. Another hypothesis is that slip of the deeper continuation of faults, steady slip that doesn&rsquo;t produce earthquakes but still involves motions across the fault, forces the upper crust around the faults to bend and thus concentrate stress. Both hypotheses are the subject of active research. But the fact remains that high stressing rates observed on the surface likely translate to high stressing rates at the depths (~10 km) where earthquakes typically nucleate, so these stressing rates are a guide to the seismic hazard.</p>

<h2>Crustal Deformation</h2>

<figure class='left w300'>
	<img src="images/instruments.jpg" alt="">
	<figcaption>
    Map depicting crustal deformation instruments deployed in the San Francisco Bay Area.
  </figcaption>
</figure>

<p>
  Crustal deformation refers to the changing earth&rsquo;s surface caused by tectonic forces that are accumulated in the crust and then cause earthquakes. So understanding the details of deformation and its effects on faults is important for figuring out which faults are most likely to produce the next earthquake. There are several hypotheses about how this works, but more data is needed to determine which one is the best.
</p>

<p>
  Crustal deformation is a heavily data driven field. To measure the motions of earth&rsquo;s surface, the USGS employs a variety of methods, including LIDAR, the Global Positioning System (GPS), Interferometric Synthetic Aperture Radar (InSAR), creepmeters, and alinement arrays.  In parts of the U.S. with few or no historically-recorded major earthquakes or where background seismicity is sparse, geodetic data may provide the only insight into present-day seismic hazard. The motions captured by these diverse measurement techniques provide vital information on:
</p>

<ul>
	<li>The slow &lsquo;background&rsquo; tectonic motions between the earth&rsquo;s plates, thereby constraining the buildup of stress on faults.</li>
	<li>The offsets across creeping faults such as the Hayward fault, which results in steady motions (typically several millimeters per year) of blocks of crust moving past each other along a common fault boundary.</li>
	<li>The offsets across a fault during a large earthquake (the coseismic displacements).</li>
	<li>Rapidly decaying motions that persist for weeks to years after a large earthquake, arising from a combination of continued slip on the fault (&lsquo;afterslip&rsquo;) and possibly its extension into the lower crust and flow of rock in the deeper lower crust and mantle, where the temperature is high enough to permit ductile flow.</li>
</ul>

<h3>Specific problems of interest include:</h3>

<ul>
	<li>Quantifying fault slip rates, creep rates, and off-fault strain rates as well as the spatial extent of locked and creeping zones.</li>
	<li>Deformation rates between earthquakes with different recurrence intervals.</li>
	<li>Comparing models with observed data to assess the accuracy of each.</li>
	<li>Mapping the distribution of the locked (non-slipping) and freely sliding parts of faults to determine where slip in future earthquakes will occur.</li>
	<li>Improving earthquake early warning and rapid response using real time high rate GPS data.</li>
	<li>Investigating the effects of seismic waves travelling across a fault from an earthquake on another fault.</li>
</ul>

