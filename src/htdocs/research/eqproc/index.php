<h3>Surface Motions</h3>

<figure>
  <img src="images/creeping.png" alt="diagram showing fault creep"/>
</figure>

<figure class='left w300'>
  <img src="images/velocity-profile.jpg" alt="velocity profile map on the San Francisco Bay Area"/>
</figure>

<p>
  Faults are thought to be creeping at depth in the lower crust, where the lack of frictional resistance at the prevailing high temperatures allows steady fault slip and no buildup of stress.  Faults may also be creeping at shallower depth in the upper crust (left figure), leading to block-like motions and sharp changes in surface velocity across the fault.  Faults are usually locked in the upper crust (right figure), leading to a gradual change in surface velocity across the fault and bending of the upper crust.  This bending produces stress buildup that eventually leads to earthquakes.
</p>
<p>
  The horizontal velocity field in the San Francisco Bay area is constrained by <a href="/monitoring/gps/">continuous GPS (CGPS) and survey-mode GPS (SGPS) measurements</a>. These velocities are almost parallel to the regional faults and decrease from west to east as one approaches the interior of the North American plate.  While a gradual transition across the San Andreas Fault indicates that it is locked, the abrupt transition across the southern Hayward fault indicates that it is creeping.  This is confirmed by <a href="/monitoring/deformation/data/plots/?region=SF">creepmeter measurements across the Hayward fault</a>.
</p>

<h2>Ground Shaking</h2>

<figure class='left w300'>
  <img src="images/effects.jpg" alt="image of expected shaking amplification in LA Basin">
  <figcaption>
    Two important local geologic factors that affect the level of shaking experienced in earthquakes are (1) the softness of the surface rocks and (2) the thickness of surface sediments. This image of the Los Angeles region combines this information to predict the total amplification expected in future earthquakes from local geologic conditions or site effects.
  </figcaption>
</figure>

<p>
  The overall objective of this research is to improve the understanding of the damaging ground motions produced in earthquakes in order to develop better methods for seismic hazard assessment and mitigation in urban areas. Past earthquakes have shown that the amplification of motions due to surface-to-bedrock geology, 3D crustal structure, and topography have a major influence on seismic damage and loss in urban areas. Also of significant importance are the details of the rupture process on the fault, and the way a built structure is engineered.
</p>
<p>
	As the waves propagate they are affected by the earth structure, such as changes in elastic properties resulting in effects such as constructive and destructive interference and basin amplification. Near the ground surface, strong shaking can result in nonlinear soil behavior or raise pore fluid pressure causing liquefaction. Likewise, the geometry of a man-made structure, the construction materials, the type of ground, and its anchorage in the ground affect its vulnerability to damage during the shaking. This research aims to understand each of these processes and to work with the seismic engineering community to bring the best estimates of strong ground shaking to engineering practice.
</p>

<h3>Research includes:</h3>
<ul>
	<li>investigation of the ground beneath high-impact areas to understand the shaking that would occur in a large earthquake.</li>
	<li>investigation of the effect on seismic waves of traveling long distances through the earth’s crust, sedimentary basins, mountains, and other crustal features.</li>
	<li>development of better imaging methods for identifying faults and crustal structure.</li>
</ul>

<h2>Ground Failure</h2>

<figure class='left w300'>
	<img src="images/grd-failure.jpg" alt="">
	<figcaption>
    Damaged house from October 17, 1989 M6.9 Loma Prieta, CA earthquake.
  </figcaption>
</figure>

<p>
	This research focuses on the ability to determine what areas are more prone to experiencing effects such as liquefaction and landslides when there is shaking from an earthquake. Cone penetration testing (CPT) provides much of the data.
</p>

<h3>Cone Penetration Testing (CPT)</h3>

<p>
  Cone Penetration Testing (CPT) is used to identify subsurface conditions in the upper 100 ft of the subsurface. The USGS CPT uses a 23-ton truck to push a “cone” into the ground. The weight of the truck is partially supported by both the tip of the cone and the sleeve of the cone. The “tip resistance” is determined by the force required to push the tip of the cone and the “sleeve friction” is determined by the force required to push the sleeve through the soil. The “friction ratio” is the ratio between sleeve friction and tip resistance, measured as a percentage. Soil type and thereby resistance to liquefaction can be inferred from these measurements.
</p>

<figure class='right'>
  <img src="images/shaking-truck.jpg" alt="photo of truck with sketch of instrument below surface"/>
</figure>

<figure class='right'>
  <img src="images/truckseis.jpg" alt="photo of shaking truck and person"/>
</figure>

<p>
  <a href="/research/cpt/data/">Data from the USGS cone,</a> which includes a seismometer, can also be used to predict how local shallow soil conditions can modify shaking. The capacity of local soil conditions to modify shaking is inversely proportional to the shear-wave velocity near the surface, which can be computed with data recorded with the seismometer. Seismic energy is created manually with a sledgehammer or automatically by a compressed air driven hammer. We measure the time it takes for the seismic energy to travel from the land surface, through the ground, to the seismometer mounted in the cone. The distance to the seismometer divided by the travel time of the shear-wave is approximately the average shear-wave velocity.
</p>
<p>
  Output from the cone as it penetrates the soil is digitally recorded by a computer and is collectively known as a sounding.
</p>

