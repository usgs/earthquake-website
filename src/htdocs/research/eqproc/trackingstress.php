<p>
  The constant plate tectonic motions between the Pacific and North American plates guarantees that the crust in the western US is continually building up stress. The image of crustal velocities provided by extensive GPS coverage reveals where these velocities change rapidly over short distances, demanding that the intervening crustal rock stretch and build up stress over time. Such a map of the stress reveals two main lines where stress is concentrated: The San Andreas fault zone and the Eastern California Shear Zone. These zones have experienced numerous earthquakes over the century and a half that earthquakes have been historically observed.
</p>

<p>
  The mechanism of stress buildup within these fault zones is uncertain. One hypothesis is that the hot rocks below the upper 15-km-thick layer (the upper crust that has the vast majority of continental earthquakes) flows continually in response to periodic earthquakes, forcing the upper crust to bend with this flow. Another hypothesis is that slip of the deeper continuation of faults, steady slip that doesn&rsquo;t produce earthquakes but still involves motions across the fault, forces the upper crust around the faults to bend and thus concentrate stress. Both hypotheses are the subject of active research. But the fact remains that high stressing rates observed on the surface likely translate to high stressing rates at the depths (~10 km) where earthquakes typically nucleate, so these stressing rates are a guide to the seismic hazard.
</p>
