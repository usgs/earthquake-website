<p>
  There are currently two main Experimental Rock Physics Laboratories in the Earthquake Science Center in Menlo Park, California. These laboratories specialize in generating earthquakes under controlled conditions to measure the processes that determine how they start and how large they will become.
</p>
<p>
  Since most damaging earthquakes originate many miles below the earth&rsquo;s surface, it is almost impossible to study them directly. Instead, much of what we know about natural earthquakes comes from analyzing the seismic waves that they produce. In the laboratory, however, we are able to recreate the conditions of high pressure, high temperature and slow stress buildup that faults undergo in the months to years before earthquakes. We also have specialized testing machines that slide rocks against each other at the high speeds (meters per second) that occur during earthquakes.
</p>
  Because testing is carried out in a controlled environment, relevant properties are measured before, during and after the occurrence of laboratory earthquakes. These include strength and frictional behavior of rocks and fault zone materials, the velocity of seismic waves through rock, electrical resistivity measurements, as well as the role of fluids and fluid flow in fault zones.
</p>

<h2>How is this data used?</h2>

<p>Information on rock properties is combined with other geophysical observations to improve our models of the earthquake process, such as the timing and magnitude of earthquakes, earthquake triggering, recurrence, rupture propagation, and ground motion. This in turn is necessary to understand earthquake hazards and risk in earthquake-prone areas.</p>

