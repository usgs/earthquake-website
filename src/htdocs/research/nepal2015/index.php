<h2>Collecting Data and Carrots</h2>
<p>
  The global seismographic network is sparse in Nepal, and data from some of the closest sources are not publically available.  Acquiring the quantity and quality of data required to accurately locate the epicenter and determine the shaking throughout the region, much less answer the many scientific questions posed by this earthquake, took more time and effort than usual.  The seismic station closest to the epicenter with real-time data was about 650 km (404 mi) away in Lhasa, Tibet.  The next closest stations were a NetQuakes instrument at the U.S. Embassy and three Quake-Catcher instruments in the Kathmandu area.
</p>
<p>
  The USGS Did You Feel It? (DYFI) website often helps scientists fill in data gaps where seismic instrumentation is sparse in some areas of the U.S.  Responses to the online DYFI questionnaire from citizens is converted into shaking intensity values that supplement the instrumental ShakeMap data in order to provide a map of the distribution of shaking from an earthquake.  However, much of Nepal is remote and not connected to the Internet, so real-time shaking intensity data were sparse, and virtually no reports were collected from the mountainous areas hardest hit by the shaking.  Instead, scientists gathered data from the news, social media, and converted European Macroseismic Scale (EMS) intensities, totaling 3831 data points at locations throughout the Kathmandu Valley.
</p>
<p>
  Scientists used InSAR imagery to look for surface rupture and landslides, and it was also used to direct field teams to areas that appeared to be most affected by the shaking. A multi-organizational reconnaissance team called GEER, Geotechnical Extreme Event Reconnaissance, sent two teams out after the earthquake to look for surface fault ruptures, landslides, liquefaction, and other surface disturbances, as well as damage to structures and infrastructure, such as bridges and pipelines. Several USGS scientists worked with these teams to collect data.
</p>
<p>
  On this mission, scientists discovered a most unusual surface disturbance near the village of Mulpani. The GEER team observed many of the villagers, young and old eating carrots, and they saw a lot of carrots lying on the ground in seemingly random locations.  The villagers informed scientists that when the earthquake occurred, many of the carrots that were growing in the fields were brought out of the ground as water flowed up and flooded the fields (this is called liquefaction, where the ground behaves like a liquid).  Since the crops cannot be replanted once they are pulled up (or pushed up in this case), the villagers were eating as many carrots as they could before they started rotting.  Thus, the first known earthquake-related incident of “root vegetables ejected out of the ground” was recorded.
</p>

