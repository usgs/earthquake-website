<h2>The Nature of Kathmandu Valley</h2>

<p>
  Some scientists think that the shaking and resulting damage was less than expected for an earthquake of M7.8 in this area, and they are seeking an explanation. Analysis of the seismograms and the geology of the area may have provided clues, although confirmation will take further research.
</p>
<p>
  The recording of shaking on instruments in the Kathmandu Valley suggested that something about the valley had dampened the shaking. The Kathmandu Valley is a bowl of ancient lakebed sediments as thick as 500 m (1540 ft). In most cases, soft sedimentary valleys increase the shaking frequency that is damaging to most buildings, but this time the opposite may have happened. Scientists are describing this as a non-linear effect, meaning that the sediments in the valley lost their cohesiveness during the shaking and didn’t transmit the energy as efficiently as they would with their normal cohesiveness. In other words, the normally soft-rock-like valley may have become even softer temporarily, like a viscous fluid, and fluids cannot transmit certain types of seismic waves. Further research will be conducted to see if this theory holds up.
</p>
<p>
  If there was indeed non-linear behavior in the valley, there are implications for the analysis of historical earthquakes in this area. Namely, using intensity data to determine the magnitudes of earthquakes for which we have no instrumental data may have underestimated the magnitudes. A second look at the macroseismic data we have for older events in the Himalayan area may be in order.
</p>

<h2 class="clear">Better Construction Can Make a Difference</h2>
<p>
  In Nepal, the fatalities and injuries were mostly caused by poor construction. Homes are made of rock, brick, or concrete that is either unreinforced or poorly reinforced, and although there are building codes, there are not enough resources to follow them, or to enforce them. Newer construction in Kathmandu fared better with less damage. This reminds us again that construction practices and enforcement of building codes can make a huge difference between a structure that is still standing after an earthquake, and one that has collapsed.
</p>
