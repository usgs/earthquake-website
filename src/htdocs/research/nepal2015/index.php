<p>
  Of course, everyone wanted to know if Mt. Everest, the tallest mountain on Earth, had become even taller.  Data from a satellite-mapping receiver that Chinese scientists had installed in 2005 revealed that Mt. Everest was not taller… or shorter.  Instead, the motion on the fault had shifted the entire mountain 3 cm (1.2 in) to the southwest, leaving the height of the mountain still at 8848m (29,029ft).  The background motion of the mountain is about 4 cm (1.6 in) toward the northeast and 0.3cm (0.1 in) up (taller) each year.
</p>

