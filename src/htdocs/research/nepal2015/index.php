<h2>In the Shadow of Mountains</h2>
<p>
  The people of Nepal and the surrounding areas have been living with earthquakes for many generations.  The Himalayan mountain range was formed by millions of years of earthquakes as the India and Eurasia tectonic plates have continued to push against one another at a relative rate of 40-50 mm/yr (1.6-2 in/yr).  A large amount of that motion (18 mm/yr) is driving the uplift of the Himalayan Mountains.  The boundary region between these two plates has a history of large and great earthquakes, making it one of the most seismically hazardous regions in the world.
</p>
<p>
  Three M6+ earthquakes have occurred within 250 km (155 mi) of the 2015 earthquake within the last 100 years. Most recently, an M6.9 in 1988 that caused around 1500 fatalities. In 1934 an M7.5-8.0 severely damaged Kathmandu and caused around 10,600 fatalities, and a previous earthquake of approximately M7.5 occurred in 1833.  Despite this long history of earthquakes and an elevated seismic hazard, the lack of resources in the rural areas has prevented many Nepalese from using modern construction methods and materials that are designed to withstand earthquake shaking.  More than 98% of the buildings in Nepal are built with low-quality materials by people not trained in proper construction practices.
</p>

