<p>
  The debris avalanche that destroyed the village of Langtang was the largest and most destructive landslide.  During the shaking, an avalanche of snow and ice initiated high on the slopes of a 7,200-m-high peak and subsequently gathered debris as it moved downhill.  It then launched off a 500-m-tall cliff (1640 ft).  What wasn’t buried by the rapidly-moving debris was flattened by the accompanying air blast.  The air blast destroyed many structures and flattened trees within about 1 km (0.6 mi) in each direction up and down the valley.
</p>
<p>
  Some of the landslides did not fail immediately with shaking but rather were progressive in nature, such as the May 24 rockslide that buried the village of Baisari on the Kali Gandaki River.  Cracks were first observed in the cliff after the mainshock, and they widened during the largest aftershock on May 12. Basairi was evacuated 10 days later when rocks began falling from the cliff.  Two days after the evacuation the slope beneath the cliff slid, damming the river and burying the entire village under about 30m (98 ft) of debris.  A 2.7-km-long (1.7-mi-long) lake formed behind the dammed river, and villages downstream were evacuated just before the water overtopped the dam and flooded the downstream area.
</p>

