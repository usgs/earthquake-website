<h2>Meet the Scientist</h2>

<p>
Scott Bennett is a USGS Mendenhall Postdoctoral Fellow at the Geologic Hazards Science Center in Golden, Colorado.  Scott thinks that faults and fault scarps (vertical offsets of the ground along a fault from an earthquake) are really interesting because you can see the amazing effects of a dramatic event, an earthquake that moved huge blocks of Earth's surface several meters in a matter of seconds.  He enjoys his work because learning about paleo-earthquakes informs our communities about what can happen during future earthquakes, so it has a direct impact on society.  It's hard work though, and it requires time away from home and family during field work expeditions.
</p>
