<p>A team of scientists from the USGS Geological Hazards Science Center, led by Mendenhall Postdoctoral Fellow Scott Bennett and Research Geologists Ryan Gold, Richard Briggs, Christopher DuRoss, and Stephen Personius are collaborating with scientists at the Utah Geological Survey to gather data from new paleoseismic trenches along the Wasatch fault zone.  These new datasets will help researchers to understand if past surface-rupturing earthquakes have spanned fault segment boundaries. They are also analyzing new high-resolution airborne LiDAR topographic data to characterize previously unmapped fault traces and to measure how vertical displacements (vertical offset of the ground surface from faulting) vary, both in space (from north to south) and time (the last 20,000 years).
</p>

<h3>Down in the Trenches - Paleoseismology</h3>
<p>
The USGS and collaborators are excavating trenches near the Salt Lake City-Provo and Provo-Nephi fault segment boundaries (yellow arrows on map below). These trenches near fault segment boundaries can provide critical information about the length and displacement of past earthquakes. These data will also allow the researchers to figure out if past earthquakes repeatedly stop at a fault segment boundary or sometimes break through.
</p>
<p>
Preliminary observations include evidence for four to seven Holocene earthquakes at each trenching site. Ongoing geochronologic analysis will help determine the timing of these earthquakes. The timing of these earthquakes will be compared to earthquakes observed in trenches on adjacent fault segments to help identify through-going ruptures across segment boundaries.
</p>

<h3>Up in the Air - LiDAR Analysis</h3>
<p>
<strong>LiDAR</strong>, which stands for <strong>Li</strong>ght <strong>D</strong>etection <strong>A</strong>nd <strong>R</strong>anging, is a remote sensing method that uses laser pulses, usually from a specially-equipped airplane, to create detailed topographic maps of Earth's surface. LiDAR is able to "see through" the vegetation to record ground elevations and is a much faster and more precise way to gather topographical data than collecting data with a ground survey.
</p>
