</div>
<p>
The USGS, in collaboration with the Utah Geological Survey, Federal Emergency Management Agency (FEMA), Utah Division of Emergency Management, Salt Lake County Surveyor's office and partner cities, has acquired 3,683 sq. km (appox. 1,400 sq. miles) of new high-resolution (8 points per sq. meter) airborne LiDAR topographic data along the entire WFZ. <a href="http://gis.utah.gov/data/elevation-terrain-data/2013-2014-lidar/">These data are available to the public</a>. Detailed bare-earth digital elevation models (DEMs) and hillshade models of these LiDAR data allow the scientists to identify previously unmapped fault traces, providing a more complete story of Holocene earthquake ruptures on the WFZ.
</p>
<p>
Analysis of the LiDAR data helps quantify fault displacements and slip rates, allows scientists to estimate the extent of fault ruptures associated with most recent earthquakes, and documents spatial and temporal slip patterns near fault segment boundaries.
</p>
<p>
Preliminary observations identify numerous previously undocumented fault scarps, including recent scarps north of Ogden, UT that may correspond with a late Holocene rupture that spanned the Brigham City-Weber fault segment boundary, as previously suggested from nearby paleoseismic trench studies. 
</p>
