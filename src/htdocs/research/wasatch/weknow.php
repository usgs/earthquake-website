<h2>What do we already know?</h2>

<p>
  For over a century, geologists have documented tectonic fault scarps along the Wasatch fault zone. In the 1880s, renowned geologist G.K. Gilbert first reported fault scarps at the mouth of Little Cottonwood Canyon, approximately 20 km (12 miles) south of downtown Salt Lake City, and suggested the scarps were evidence of prehistoric ground-rupturing earthquakes.  Today there is a <a href="http://geology.utah.gov/surveynotes/geosights/gilbertpark.htm">park at this location</a> commemorating Gilbert's work and showcasing the geological features.
</p>
<p>
  Since 1979, paleoseismologists, scientists who study past earthquakes, have excavated numerous paleoseismic trenches along the WFZ (white stars on location map, at right), with the goal of documenting the timing and frequency of past earthquakes (see <a href="http://geology.utah.gov/ghp/consultants/paleoseismic_series.htm">Paleoseismology of Utah Series</a>, Utah Geological Survey). Paleoseismologists used this information about the recent past to provide insight into the frequency of future earthquakes and ground shaking, for example, as with the <a href="http://earthquake.usgs.gov/hazards/products/conterminous/2014/2014_pga2pct50yrs.pdf">2014 USGS National Seismic Hazard Maps</a>. In connection with the trenching work, geologists produced detailed maps of the Wasatch fault trace and the rock units and young <a href="/learn/glossary/?term=alluvium">alluvial</a> deposits that it cuts.
</p>
<p>
  Together, these contributions led to the subdivision of the WFZ into 10 structural fault segments, each approximately 30-60 km (19-37 miles) long. The central five fault segments have been active during Holocene time (the last 10,000 years) and produce large (magnitude 7.0) earthquakes about every 900-1300 years. The most recent earthquake on fault segments near Weber, Provo, and Nephi occurred 200-700 years ago. The most recent earthquake on the Salt Lake City segment occurred 1200-1600 years ago. It has been 2200-2800 years since the most recent earthquake on the Brigham City fault segment.
</p>

