<h2 class="clear">What is still unknown?</h2>

<p>
  Paleoseismologic studies have provided a detailed record of <a href="/learn/glossary/?term=Holocene">Holocene</a> earthquakes on the WFZ. This record generally suggests that individual magnitude 7.0 earthquake ruptures have been limited to one of the approximately 30-60 km-long fault segments. However, in a few cases the data could also be interpreted as multi-segment ruptures on the WFZ, where rupture spanned a segment boundary.
</p>
<p>
  Multi-segment earthquake ruptures on the WFZ may have produced longer surface ruptures and generated larger (magnitude 7.4) earthquakes; however, they may also result in less frequent and fewer total surface-rupturing earthquakes. The extent and frequency of ruptures that span segment boundaries remains poorly known, adding uncertainty to seismic hazard models for this populated region of Utah.
</p>

