<p>
  Thus, when new high-resolution <a href="https://earthquake.usgs.gov/research/everyone/2015/wasatch/collect.php">lidar</a> datasets (<a href="https://opentopography.org/">Open Topography</a> and <a href="http://tngis.org/lidar">Tennessee GIS</a>) became available in 2010-2012 in northwestern Tennessee, USGS scientists looked to this new source of data for clues of past earthquakes. They were intrigued to discover indications of geologic cracks and scarps, called sackungen, on the tops of bluffs near the Reelfoot fault in the New Madrid seismic zone (One is a sackung, many are sackungen.) Sackungen have been observed in many places around the world and have been attributed to deglaciation (the melting of glaciers), slumping due to gravity, or strong shaking from earthquakes. These processes usually occur in previously glaciated or steep and mountainous terrain, in contrast to the glacier-free low-elevation bluffs in western Tennessee. Since the sackungen observed on the lidar only occurred on the ridges near the Reelfoot fault, even though similar bluffs exist throughout Tennessee, the scientists suspected these features were caused by earthquake shaking.
</p>
<p>
  Careful mapping of 1762 sackung features showed them to be generally &lt;1.5 m (&lt;5 ft) high, 5 to 335 m (16 to 1100 ft) long, and as wide as ~5 m (16 ft). They are present only within 15 km (9 mi) of the Reelfoot reverse fault, and 85% are within 5 km (3 mi) of the fault. They appear on both sides of the fault, but are dominantly on the hanging wall (the side that "hangs over" the fault), which typically shakes more during an earthquake on a reverse fault. The sackungen are visible in the topmost sediments which have been dated to between 11,000 and 30,000 years old, which puts a maximum limit on the sackungen ages. From 11,000 years ago to present, there has been no deposition, just changes to those sediments due to deformation or erosion.
</p>
<p>
  The amount of shaking required to create the sackungen in this location was estimated to be about 0.33g (1/3 the force of gravity). Since the shaking from the February 23, 1812 event on the Reelfoot fault has been estimated to be 1.4-1.5g, that earthquake, and presumably others before it, are believed to be large enough to have caused the sackungen.
</p>

