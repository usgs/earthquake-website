<p>
  The central U.S. is not the usual location for large earthquakes in the U.S., but if you were living near the Mississippi River between Memphis and St Louis in 1811-1812, you may have begged to differ. Three large earthquakes in the <a href="https://earthquake.usgs.gov/learn/topics/nmsz/">New Madrid seismic zone</a> between December 1811 and February of 1812 shook a huge area from the mid-west to the east coast. Historical accounts, as well as many scientific studies, have established their approximate locations (see map) and magnitudes (~M7+).
</p>
<p>
  Details of past earthquakes are difficult to come by in this area where evidence of shaking has long since been eroded or covered by sediments from the Mississippi River, or modified by farming. Older earthquakes are even harder to pin down, but <a href="https://earthquake.usgs.gov/learn/topics/topics.php?topicID=32">trenching and liquefaction</a> studies have documented evidence for three earthquakes in the past 1100 years, occurring roughly 550 years apart, with a possible fourth earthquake 4400 years ago. Extending this earthquake chronology would help scientists understand the long-term pattern of earthquakes in this region, as constraints on the timing of past earthquakes are essential for estimating the future earthquake hazard.
</p>

