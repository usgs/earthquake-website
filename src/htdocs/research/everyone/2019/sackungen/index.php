<p>
  The central U.S. is not the usual location for large earthquakes in the U.S., but if you were living near the Mississippi River between Memphis and St Louis in 1811-1812, you may have begged to differ. Three large earthquakes in the New Madrid seismic zone between December 1811 and February of 1812 shook a huge area from the mid-West to the East Coast. Historical accounts, as well as many scientific studies, have established their approximate locations (see map) and magnitudes (~M7+).
</p>
<p>
  Details of past earthquakes are difficult to come by in this area where evidence of shaking has long since been eroded or covered by sediments from the Mississippi River, or modified by farming. Older earthquakes are even harder to pin down, but trenching and liquefaction studies have documented evidence for three earthquakes in the past 1100 years, occurring roughly 550 years apart, with a possible fourth earthquake 4400 years ago. More information extending even further into the distant past is needed to understand the long-term pattern of earthquakes in this region. Placing constraints on the timing of past earthquakes is essential for estimating the future earthquake hazard in this region. However, clues regarding past earthquakes are few and far between in this setting.
</p>
<p>
  Thus, when new high-resolution lidar datasets (<a href="https://opentopography.org/">Open Topography</a> and <a href="http://tngis.org/lidar">Tennessee GIS</a>) became available in 2010-2012 in northwestern Tennessee, USGS scientists began searching for clues of past earthquakes. They were intrigued to find indications of geologic cracks and scarps, called sackungen, on the tops of bluffs near the Reelfoot fault in the New Madrid seismic zone (One is a sackung, many are sackungen.) Sackungen have been observed in many places around the world and have been attributed to deglaciation (the melting of glaciers), slumping due to gravity, or strong shaking from earthquakes. These processes usually occur in previously glaciated or steep and mountainous terrain, in contrast to the glacier-free low-elevation bluffs in western Tennessee. Since the sackungen observed on the lidar were only near the ridges near the Reelfoot fault, even though the bluffs exist throughout Tennessee, it looked suspiciously like they were caused by earthquake shaking.
</p>

