<p>
  To further investigate the possibility of earthquake-produced sackungen, USGS scientists dug a trench across an especially well-preserved sackung to date the sediments and geologic features. The trench exposed the bluff sediments that were deposited between 4400 and 11,000 years ago. Importantly, this investigation represents the longest earthquake record for Reelfoot fault. They found a sedimentary record of four episodes of sackung movement, with the oldest event about ~4400 years ago and the most recent in 1811-1812. If the trenched sackungen have recorded all the earthquakes since the bluff sediment was deposited, it means there was a long time period with no earthquakes from 11,000 years ago to 4,400 years ago, followed by four major earthquakes between around 4400 years ago and ~200 years ago (1811-1812 AD).
</p>
<p>
  These data agree with several previous studies conducted by researchers using different methods. All indications are that indeed that these sackung in the New Madrid area were created by earthquakes on the Reelfoot fault, and perhaps sackungen can be used to gather data about past earthquakes in other areas where the clues have been buried. In this particular area, these unusual features have contributed to our understanding of the earthquake history in the New Madrid seismic zone and provide a new opportunity to study them. Seismic hazard models may be improved by considering the recent earthquake recurrence intervals as well as the longer-term quiescence. Every new bit of data helps scientists to more accurately estimate the future earthquake hazard in this area of the country.
</p>

