<p>
  Careful mapping of 1762 sackung features showed them to be generally &lt;1.5 m (&lt;5 ft) high, 5 to 335 m (16 to 1100 ft) long, and as wide as ~5 m (16 ft). They are present only within 15 km (9 mi) of the Reelfoot reverse fault, and 85% are within 5 km (3 mi) of the fault. They appear on both sides of the fault, but are dominantly on the hanging wall, which typically shakes more during an earthquake on a reverse fault. The sackungen are visible in the topmost sediments which have been dated to between 11,000 and 30,000 years old, which puts a maximum limit on the sackungen ages.
</p>
<p>
  The amount of shaking required to create the sackungen in this location were estimated to be about 0.33g (1/3 the force of gravity). Since the shaking from the February 23, 1812 event on the Reelfoot fault has been estimated to be 1.4-1.5g, that earthquake, and others before it, was large enough to have caused the sackungen. The shaking analysis and the clustering around the Reelfoot fault led the USGS scientists to conclude that the sackungen were a result of earthquakes on the Reelfoot fault. A question left for future research is why the sackungen only occurred very near the fault, but previously-studied large landslides from the same earthquake occurred at much greater distances from the Reelfoot fault. It’s possible that the creation of sackungen requires other conditions in addition to the shaking that only exists near the fault.
</p>

