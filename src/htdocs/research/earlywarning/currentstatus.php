<h2>Current Status</h2>

<p>
  Since 2008, the USGS Earthquake Hazards Program has supported research and development on earthquake early warning in partnership with Caltech, University of California Berkeley, and others, with goals to develop methods that would allow rapid detection of earthquakes in the western United States, to test and improve those methods using the <a href="http://www.cisn.org/">California Integrated Seismic Network</a> (CISN), to define the network improvements that would be needed to support a fully operational system, and to build a prototype system capable of providing early warnings to certain test users. Continued funding of and by the USGS complements funding from a private foundation recently awarded to the academic institutions (partners in the <a href="https://earthquake.usgs.gov/monitoring/anss/">Advanced National Seismic System</a> (ANSS) for research on and development of EEW components. This increase is part of an $8.6 million initiative to improve USGS disaster response capabilities through preparedness and robust monitoring.
</p>

<p>
  The earthquake early warning system under development leverages federal and state investments already made in the ANSS that monitors earthquake activity in the US. By utilizing an existing, active seismic network, the early warning system can be tested and monitored daily through existing operations.  Any infrastructure improvements required for EEW will also result in improved information for emergency response and aftershock forecasting. For instance, in 2011, upgrades to stations across California have been completed to reduce latencies in packaging and sending ground motion observations useful for EEW.
</p>

