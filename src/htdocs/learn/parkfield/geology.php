<p>The San Andreas fault defines an approximately 1300 km (800 mi) portion of
  the boundary between the Pacific and North American plates (see
  <a href="/learn/parkfield/wallace.html">tectonic map</a>). Along its
  length, the fault undergoes horizontal strike-slip motion that accommodates
  most of the relative motion between the plates. To the north, a complex of
  transform faults and spreading centers accommodates the motion of the Gorda
  and Juan de Fuca plates. To the south, a similar complex of spreading centers
  and transform faults accommodate the displacement in the Gulf of
  California.</p>

<p>The <a href="/learn/parkfield/town.html">Town of Parkfield</a> is
  situated on a relatively straight section of the San Andreas fault in
  central California, where fault movement occurs as right lateral slip
  both in earthquakes and as aseismic slip, or &quot;creep&quot;. South
  of Parkfield, large earthquakes (including the <a href="/learn/parkfield/1857.php">M
  7.9 Fort Tejon earthquake of 1857</a>) have occurred.
  From both geodetic and seismic data, currently, the southern section appears
  to be locked producing no movement or small to moderate sized earthquakes.
  Similarly, the section of the fault north of San Juan Bautista also has
  produced large earthquakes, including the <a href="/earthquakes/events/1906calif/18april/">M
  7.9 San Francisco earthquake in 1906</a> and the <a href="/earthquakes/events/1989lomaprieta/">M
  6.9 Loma Prieta earthquake in 1989</a>. Most of the northern section
  of the fault is also currently locked, with no detectable movement and few
  earthquakes since 1906. Between these locked sections, the San Andreas fault
  creeps (slips aseismically). From San Juan Bautista to Parkfield, the creeping
  section produces numerous small (mostly M=5 and smaller) earthquakes but no
  large ones. The stretch of the fault between Parkfield and Gold Hill defines
  a transition zone between the creeping and locked behavior of the fault.</p>

