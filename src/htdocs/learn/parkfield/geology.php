<p>The <a href="/learn/parkfield/hist.php">seismicity at Parkfield</a> is
  noteworthy for several reasons. First, since 1857, six similar, M~6
  earthquakes have occurred on the San Andreas fault near Parkfield with
  apparent regularity&mdash;one approximately every 22 years. Secondly,
  <a href="/learn/parkfield/hist.php">waveforms</a> recorded on regional
  seismographs are strikingly similar for the 1922, 1934 and 1966 earthquakes,
  suggesting that these earthquakes involved repeated rupture of the same area
  on the fault. These observations suggest that there may be some
  predictability in the occurrence of earthquakes, at least at Parkfield. In
  addition, much is being learned about the physics of earthquakes from advances
  at Parkfield, including the discovery of repeating microearthquakes (see
  <a href="/learn/parkfield/scientific.php#Ells95">Ellsworth, 1995</a>) and
  earthquake &quot;streaks&quot;
  (see <a href="/learn/parkfield/scientific.php#W">Waldhauser et al 1999</a>,
  and <a href="/learn/parkfield/scientific.php#Rubin99">Rubin, 1999</a>).</p>
