<h2>Introduction &amp; Basics </h2>
<p>
  Earthquake &quot;booms&quot; have been reported for a long time, and they tend to occur more in the Northeastern US and along the East Coast. Of course, most &quot;booms&quot; that people hear or experience are actually some type of cultural noise, such as some type of explosion, a large vehicle going by, or sometimes a sonic boom, but there have been many reports of &quot;booms&quot; that cannot be explained by man-made sources. No one knows for sure, but scientists speculate that these &quot;booms&quot; are probably small shallow earthquakes that are too small to be recorded, but large enough to be felt by people nearby.
</p>
<p>
  As it turns out....there are many factors that
  contribute to the &quot;sound' that an earthquake
  makes. To begin to understand these factors we
  have to understand the <a href="http://www.usgs.gov/faq/?q=categories/9828/3289">different types of waves</a>,
  the speed they travel through the earth, and the
  speed that sound travels through the air.
</p>
<p>
  Perhaps the best way to understand earthquake
  sounds are from an actual experiment that took
  place back in the 80's in California by David
  Hill. Dr. Hill's team recorded sounds that came
  out of the earth (from nearby small earthquakes
  between magnitude 2.0 and 3.0) and simultaneously
  measured the arrival of the P wave on a
  seismograph. Researchers also reported hearing a
  sound before the S waves were recorded; this turned out to be the arrival of the P wave. See this Alaska Science Forum article entitled <a href="http://www.gi.alaska.edu/ScienceForum/ASF7/761.html">&quot;Earthquake Waves Outrace Sound&quot;</a> for a description of that experiment.
</p>
<h2>Observations of Earthquake Sounds</h2>
<h3>Spokane, Washington</h3>
<p>
  The most recent documented earthquake sounds were from a swarm of small earthquakes that unnerved the city of Spokane, WA in 2001. Many of the Spokane quakes were definitely accompanied by &quot;booming sounds&quot;. The quakes in Spokane were shallow, sometimes only a mile or two deep. This probably contributed to all the noise they made. Higher-frequency vibrations make the booming sound, and when quakes are deeper, those vibrations are gone by the time they reach the surface. Sometimes the quakes boom even when no vibration is felt.
</p>

<h3>New Madrid, Missouri</h3>
<p>
  There are <a href="http://www.ceri.memphis.edu/compendium/">accounts of &quot;artillery&quot;-like sounds</a> that were said to have occurred before or during the New Madrid earthquakes of 1811-1812.
</p>
<h3>Charleston, S. Carolina</h3>
<p>
  As with most historical earthquakes, details
  about sounds and the actual level of ground
  motion related to the Charleston, SC Earthquake
  (1886) are somewhat sketchy and hard to
  authenticate. &quot;Dutton, Clarence E., 1889: &quot;The
  Charleston Earthquake of August 31, 1886,&quot; Ninth
  Annual Report, 1887-88, U.S. Geological Survey,
  Washington D.C., p. 203-528&quot; is perhaps the most
  comprehensive scientific document that compiles
  seemingly credible accounts from numerous
  sources. Dutton objectively sums these
  interviews/observations with the following:
</p>
<blockquote>
  <p>
    &quot;According to the testimony of some, the first intimation of the
    disturbance was a strange sound or murmur. Others say that with the
    sound they felt the trembling, and that both increased, at first
    steadily, but by perceptible stages, and then suddenly or by swift
    degrees, to the full roar and energy of the climax.
    Dr. Manigault resides in a very quiet street near the Battery,
    and but a few hundred yards from the estuary of the Ashley River. He
    was engaged in a game of chess, and a member of his family was sitting
    by an open window. The latter, surprised or perhaps alarmed
    by the prolonged sound, arose, crossed the room, entered
    the hall, and passed out into the open air before the doctor became
    aware of anything unusual. The sound appeared to come across the water
    of Ashley River from the west-southwest. Another observer of
    intelligence was seated in the park at the Battery, near the statue of
    Jasper. He suddenly became conscious of a deep murmur, which swelled
    in volume, and which appeared to come from the open bay, lying
    southeastward. Very soon there was a sound of agitation in the leaves
    of the trees overhead, and at the same instant, he thinks, he became
    aware of a tremor in the ground. Springing to his fee, there suddenly
    broke upon his ear a rapid swell in the sound, which became a mighty
    roar, and with the roar came a shock.&quot;
  </p>
</blockquote>
<p>
  It is apparent from these observations that, at
  least for the main shock, people heard the actual
  low frequency motions of the damaging earthquake
  waves as they rolled across the region and right
  down their street.....the &quot;roar' came with the
  &quot;shock&quot;.
</p>
<p>
  As for scientific evidence for loud sounds that
  preceded the 1886 Charleston, SC Earthquake,
  there is none. To our knowledge, there were no
  seismographs or barographs that recorded the
  earthquake. The only data that was collected
  that could have scientific significance (aside
  from tide gauge and other water surface
  measurements, was from large clocks that stopped
  due to ground motions. Unfortunately none of
  these clocks were precisely synchronized so those
  data are of poor quality.
</p>
<p>
  For several weeks after the Charleston Earthquake
  (8/31/1886) there were many aftershocks that were
  reportedly accompanied by &quot;loud detonations&quot;.
  But there was little mention of sounds occurring
  before an event. The earth was in a fairly
  continuous state of agitation and it would be
  difficult to relate a specific sound to a
  specific earthquake. The following account from
  the Dutton report is particularly informative:
</p>
<blockquote>
  <p>
    &quot;For several weeks following the principal disturbance minor
    shocks continued to be felt at frequent intervals. Many of them would
    have been considered very forcible and alarming and they not been
    greatly disparaged by the convulsion of August 31. Almost all of them
    were accompanied by loud detonations. Mr. McGee thus describes several
    which he experienced.
  </p>
  <p>
    I reached Summerville about 5 o'clock p.m. Detonations were
    heard at intervals averaging perhaps half
    an hour. From that time until
    9:30 p.m. occasional and very slight spasmodic tremors of an instant's
    duration accompanied the detonation. I endeavored to determine the
    direction from which the sounds came, but no two individuals agreed.
    They seemed to me to come from the northwest. They were much like, but
    somewhat more muffled than peals of thunder at distance of half a mile
    or more, or perhaps more like the discharge of a blast in a mine or
    quarry at a little distance.&quot;
  </p>
</blockquote>
<h2>Seneca Guns</h2>
<p>
  The term &ldquo;Seneca guns&rdquo; is just a name, not an explanation. It does not tell
  us anything about what causes these noises and shakings. The name
  originated in a short story that James Fennimore Cooper wrote during the
  1800&rsquo;s. The name refers to booms that have been heard on the shores of Lake
  Seneca and Lake Cayuga in New York State. The name has been applied to
  similar noises along the coasts of North Carolina, South Carolina, and
  Virginia. Similar booms are called Barisol guns in coastal India. These phenomena have also occurred in three widely separated places around
  the world. That&rsquo;s about all we know about the Seneca guns.
</p>
<h3>What might be the cause</h3>
<p>
  The thing that comes closest to matching all of the observations is sonic booms
  from military aircraft. Articles in the media have  summarized reports of Seneca
  guns from coastal South Carolina, and another article reports on one loud boom
  that was heard in Myrtle Beach, SC on Dec. 14 of an unspecified year. The sound
  was so loud that it shook a window and the sofa that the person was sitting on,
  and she felt the shock from the sound. Thus, a loud enough boom can be felt.
</p>
<p>
  In the Myrtle Beach case, the Air Force claimed responsibility and said
  that they had been conducting training exercises at the time. The
  seismologist whom I called in Virginia reported that, during the 1970&rsquo;s,
  Seneca guns were heard on the coast of Virginia. Reporters and
  seismologists investigated for several weeks but were unable to determine
  the cause. Finally the Navy admitted that one of their planes had caused a
  sonic boom. The problem
  with sonic booms is that they cannot explain Seneca guns that occurred
  before supersonic jets.
</p>
<p>
  Naval ships firing their guns offshore might have produced some of the
  booms. Under certain atmospheric conditions, sounds
  can travel farther than usual so that they might be heard onshore as loud
  booms. Naval firing might explain some of the Seneca guns that occurred
  before jet planes were in wide use. In particular, naval gunfire might have
  caused some of the booms that were heard during the 1800&rsquo;s and early
  1900&rsquo;s, when it might have been more common for ships to fire within a few
  miles of shore. However, naval gunfire cannot explain the Seneca guns
  inland, around Seneca Lake and Cayuga Lake. These lakes are much too small
  to have any military vessels on them, and no navigable rivers connect them
  to the ocean or to the Great Lakes.
</p>
<p>
  Earthquakes are also a possible cause. In southeastern North Carolina,
  earthquake lists show seven events between 1871 and 1968. Each event was
  reported by people who felt it or heard it. The problem with the earthquake explanation is that something that is felt
  or heard that strongly should have been recorded on nearby seismographs
  (these are the instruments that record ground shaking for seismologists to
  analyze). A seismologist in Virginia who has tried, has never been able to match any of the
  reported Seneca guns with his seismograph records, and he has tried lots of
  times over the years.
</p>
<h3>What is probably not the cause </h3>
<p>
  Tidal wave: any wave large enough to produce shaking and booms on land
  would have been reported. I have not found any reports of tidal waves.
</p>
<p>
  Landslides off the continental shelf: these have happened in the geologic
  past, but I don&rsquo;t know of any reports that they happened during recorded
  history. The main effect of large landslides under the ocean would be tidal
  waves, which we&rsquo;ve already considered.
</p>
<p>
  Industrial disasters, like the Chernobyl explosion: anything that large
  would be reported. There would be no way to hide the news because too many
  people nearby would have seen, heard, and felt the blast, and there might
  be injuries too.
</p>
<p>
  Global warming: the articles that suggest this don&rsquo;t offer any explanation
  of how a gradual warming of the atmosphere would produce something as
  sudden as a loud boom. Also, global warming would affect the whole globe,
  whereas Seneca guns have only been reported from a few areas.
</p>
<p>
  A hole in the ozone layer: holes in the ozone layer form over the north and
  south poles, not over the U.S. or India. The reasons why they form are well
  understood, although I don&rsquo;t understand them myself because I&rsquo;m a
  geologist, and the reasons apply only to polar areas.</p>
<p>
  Shifts of tectonic plates: there are no tectonic plate boundaries near the
  east coast. The nearest plate boundaries are in the middle of the Atlantic
  Ocean and in the Caribbean Sea.
</p>
<p>
  Pockets of air being released: air can fill small pores between particles
  of soil, and cracks in rock, but it&rsquo;s not held in pockets that are tight
  enough to contain it under pressure. If the air is not under pressure, its
  release would not make booms or shake the ground.
</p>
<p>
  Methane released from the ocean floor or lake bottoms and exploding when it
  rises to the surface and contacts the air: there is methane buried under
  the ocean floor and bottoms of lakes. It forms from the gradual decay of
  buried plants and microscopic animals. Sometimes the methane can seep
  upward to ground level. However, it does not come up suddenly enough or in
  large enough amounts to cause explosions. Also, burning methane makes
  flames, and a couple of the articles that Google listed point out that
  flames have not been reported at the times and locations of Seneca guns.
</p>
<p>
  New faults forming: fracturing rock to produce a new fault, as well as
  moving an existing fault, should produce an earthquake. We&rsquo;ve already
  examined earthquakes.
</p>
<p>
  Cold air meeting warm Gulf Stream air: the articles that I&rsquo;ve read which
  suggest this cause do not explain how it would operate. Two results might
  be thunder and lightning, but Seneca guns have been reported in clear
  weather as well as stormy.
</p>
<p>
  Meteor exploding in the atmosphere: this suggestion would have to be
  evaluated by an expert in meteorites. It&rsquo;s a possible cause, but my guess
  is that any meteorite large enough to cause something as strong as a Seneca
  gun would be much rarer than Seneca guns. If my guess is correct, then
  exploding meteors might explain a few Seneca guns but not most of them.
</p>
<p>
  Top secret military activity: the problem with this explanation is that
  it&rsquo;s too easy. Centuries ago people called on magic the same way. There&rsquo;s
  no way to disprove this idea as a cause of Seneca guns, because if it&rsquo;s top
  secret we won&rsquo;t know about it, but I&rsquo;ve never seen any reason to take the
  explanation seriously. Sure, the military has lots of secrets, but
  something big enough to cause Seneca guns in so many regions, including
  India, would be really hard to keep secret. One of the great things about
  America is that it&rsquo;s hard to keep big secrets here. Most of us, including
  me, would be eager to share a really big secret with friends and family,
  and if we weren&rsquo;t lots of reporters would be glad to do it for us.
</p>
<p>
  Indian ghosts firing guns to disturb descendants of settlers who took their
  land: you made the best assessment of this suggestion: &ldquo;we want an
  explanation, not a ghost story&rdquo;.
</p>
<p>
  Lightning: Some of the Seneca guns have been reported in clear weather.
</p>
<h3>Conclusion</h3>
<p>
  There does not appear to be any agreement on what causes the Seneca guns.
  They have been occurring in several places around the eastern U.S. and in
  India for at least a century or two. As far as I can tell, they have
  worried people but they have never caused damage or injury. The Earth is a complex place and there is a
  lot about it that we don&rsquo;t understand. Perhaps someday we will understand what causes Seneca guns, but right now
  we don&rsquo;t understand what makes them. However, they do not seem to pose a
  threat to anyone.
</p>
