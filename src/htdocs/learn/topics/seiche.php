<p>
	Seismic seiches are standing waves
	set up on rivers, reservoirs, ponds,
	and lakes when seismic waves from an
	earthquake pass through the area. They
	are in direct contrast to tsunamis which
	are giant sea waves created by the sudden
	uplift of the sea floor.
</p>
<p>
	The term seismic seiche was first
	coined by Anders Kvale in 1955 to
	describe oscillation of lake levels in
	Norway and England caused by the
	<a href="http://earthquake.usgs.gov/earthquakes/world/events/1950_08_15.php">Assam earthquake of August, 1950</a>. But this
	was not the first time that seismic
	seiches had been observed. The first
	published mention was after the great
	<a href="http://earthquake.usgs.gov/earthquakes/world/events/1755_11_01.php">earthquake of November 1755 at Lisbon</a>,
	Portugal. An article in Scot's Magazine
	in 1755 described seiches in Scotland
	in Loch Lomond, Loch Long, Loch
	Katrine and Loch Ness. They were also
	seen in English harbors and ponds and
	were originally described in the
	Proceedings of the Royal Society in 1755.
</p>
<p>
	Earthquake effects recorded by
	surface-water gages were first noticed by
	A.M. Piper of the U.S. Geological Survey
	(USGS). He reported that two of
	six gauges on the Mokelumne River in
	California showed a slight fluctuation
	caused by the December 20, 1932 earthquake
	at Lodi, California. Since then
	many seiches resulting from earthquakes
	have been recorded. Kvale made a detailed
	study of 29 seiches recorded in
	fiords and lakes in Norway and four
	seiches on reservoirs in England, all
	caused by the 1950 Assam earthquake.
	Frank Stermitz, a USGS scientist,
	reported readings from 54 stream gages
	that recorded seiches caused by the
	<a href="http://earthquake.usgs.gov/earthquakes/states/events/1959_08_18.php">Hebgen Lake, Montana, earthquake of
	17 August 1959</a>. These were in Montana,
	Wyoming, Idaho, and Alberta, Canada -
	the most distant seiche being 545 kilometers
	from the epicenter.
</p>
<p>
	Seismic waves from the
	<a href="http://earthquake.usgs.gov/earthquakes/states/events/1964_03_28.php">Alaska earthquake of 28 March, 1964</a>,
	were so powerful
	that they caused water bodies to
	oscillate at many places in North America.
	Seiches were recorded at hundreds
	of surface-water gaging stations -
	although they had rarely been reported
	following previous earthquakes. Indeed,
	four seiches were observed in Australia.
</p>
<p>
	Some of the 1964 seiches were very
	large. Waves as high as 1.8 meters were
	reported on the Gulf Coast -
	probably because they were generated in
	resonance with the seismic surface waves.
</p>
<p>
	Arthur McGarr and Robert C. Vorhis
	studied the continental distribution
	of seiches produced by the Alaska earthquake.
	They divided the seiches into
	two groups - those that occurred in
	Alaska itself and those that occurred
	outside the State.
</p>
<p>
	The Alaska seiches were not wholly
	seismic, but were caused by landslides,
	submarine slides, tsunamis, and tilting -
	as well as by seismic surface waves.
	It was therefore difficult to isolate a
	particular mechanism for seiches produced
	within the epicentral region. At
	teleseismic distances (greater than 1000
	kilometers) from the epicenter, inelastic
	effects are unimportant and seiches are
	generated solely by seismic surface waves.
</p>
<p>
	After the 1964 Alaska earthquake,
	the southeastern part of the United
	States had by far the greatest density of
	seiches. Other high density areas
	included north and central New Mexico,
	eastern Kansas, and the region at the
	southern tip of Lake Michigan. The
	areas west of the Rockies, the Middle
	Atlantic States and New England
	experienced few or no seiches.
</p>
<p>
	The 1964 distribution does not have
	any obvious dependence on distance or
	azimuth from the epicenter. But it does
	seem to have definite regional patterns,
	which reflect the influence of major
	geologic features:
	<ul>
		<li>
			The density of seiches is roughly
			proportional to the thickness of surface
			sediments, for example, in the
			Mississippi Delta region.
		</li>
		<li>
			Thrust faults apparently provide a
			favorable environment for seiche
			generation. The relationship is especially
			clear in Georgia, near the Brevard
			thrust zone, in the Ouachita Mountains,
			and also in the Valley and Ridge
			province of Tennessee and Alabama.
		</li>
		<li>
			Seiche locations were also controlled
			by structural uplifts and basins -
			such as the Williston and Michigan basins.
		</li>
	</ul>
</p>
