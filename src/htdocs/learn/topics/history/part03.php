<h2>by<br>James Dewey and Perry Byerly</h2>

<blockquote>
	<h3>The Earliest Seismoscope</h3>
	<p>
		The Chinese philosopher Chang H&#234;ng
		<font size=-1>
		[Chang H&#234;ng is also referred to as Choko and
		Tyoko, modifications
		of the Japanese form of his name (Needham, 1959).]
		</font>
		invented the earliest known seismoscope in
		132 A.D. A fascinating and complete account
		of the seismoscope is given by Needham
		(1959). The instrument was said to resemble a wine
		jar of diameter six feet
		(Figure 1).
		On the outside of the vessel there were eight dragon-heads,
		facing the eight principal
		directions of the compass.
		Below each of the dragon-heads was a toad, with its mouth
		opened toward
		the dragon. The mouth of each dragon held a ball.
		At the occurrence of
		an earthquake, one of the eight dragon-mouths would
		release a ball into the open
		mouth of the toad situated below.
		The direction of the shaking determined which of the
		dragons released its ball. The instrument
		is reported to have detected a four-hundred-mile
		distant earthquake which was not felt at the
		location of the seismoscope.
	</p>

	<figure class="left">
		<img src="figures/fig_01.gif" alt=""/>
		<figcaption>
			Figure 1. Chang Heng's seismoscope,
			as visualized by Wang Chen-To (1936).
		</figcaption>
	</figure>

	<p>
		The inside of the Chinese seismoscope is unknown. Seismologists of the
		nineteenth and twentieth centuries have speculated
		on mechanisms which would
		duplicate the behavior of Chang H&#234;ng's seismoscope, but would not be
		beyond the Chinese technology of Chang H&#234;ng's time. All assume the use
		of some kind of pendulum as the
		primary sensing element, the motion of which would activate one of
		the dragons. In his
		translation of the original Chinese
		description of Chang H&#234;ng's seismoscope,
		Milne (1886b, p. 13-15) implied that the pendulum was a suspended
		mass, or, as we shall call it, a common pendulum. Imamura (1939)
		thought an inverted pendulum more probable. Hagiwara constructed
		an inverted-pendulum seismoscope which behaved nearly
		as Chang H&#234;ng's was reported to have behaved (Imamura, 1939). The
		model designed by Hagiwara, however, responded most frequently to
		transverse motion, and indicated a direction normal
		to the azimuth between observer and epicenter, whereas the
		Chinese seismoscope was reported to have indicated the azimuth
		of the earthquake. Needham (1959, p. 630) has suggested that
		Chang H&#234;ng's "earthquake weathercock" was calibrated
		empirically for its direction-determining properties.
	</p>

	<p>
	Needham reports that knowledge of Chang H&#234;ng's instrument remained
	for over four centuries. Books describing the working of
	"earthquake weathercocks" were written as late as the end
	of the sixth century. In later years, however, the seismoscope
	seemed to disappear from Chinese science; later Chinese writers
	questioned that such a machine was possible.
	</p>

	<p>
		<small>
  		From the <i>Bulletin of the Seismological Society of America</i>.
  		Vol. 59, No. 1, pp. 183-227. February, 1969.
		</small>
	</p>
</blockquote>

