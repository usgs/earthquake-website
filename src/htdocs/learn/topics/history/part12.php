			Figure 27. Vertical-motion record obtained with
			Schl&#252;ter's klinograph, on September 30, 1901
			(reproduced from <i>Beitrage zur Geophysik</i>, <b>5</b>. 1903,
			plate 2). To obtain this record, the klinograph was modified
			from the form shown in Figure 25 to make it sensitive to
			vertical displacements (see text).
		</figcaption>
	</figure>
	<p>
		Schl&#252;ter's klinograph seems to have been used only in G&#246;ttingen and
		only for a relatively short time. The high cost of photographic
		registration may have limited the
		use of the klinograph (Wiechert, 1907, p. 629). Because of the
		difficulties inherent in the design of a mechanically-recording
		vertical seismometer of medium period, Wiechert postponed the
		construction of such an instrument until after he had perfected
		his horizontal seismograph (Wiechert, 1904, p. 436).
		A mechanically-recording vertical seismograph with a period of
		five seconds was installed in G&#246;ttingen in 1905 (Wiechert,
		1907, p. 629).
	</p>
	<p>
