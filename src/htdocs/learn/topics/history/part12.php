<h2>by<br>James Dewey and Perry Byerly</h2>

<blockquote>
	<h3>The G&#246;ttingen Seismographs</h3>

	<p>
		We return to the history of the seismographs themselves.
		In 1898, E. Wiechert of G&#246;ttingen introduced a seismograph
		with a viscously-damped pendulum as a sensor (Wiechert, 1899).
		The damping was added to lessen the effects of the pendulum
		eigen-oscillations. Wiechert's first seismograph was a
		horizontal-pendulum instrument, which recorded photographically.
		After a trip to Italy to study seismometers used in that
		country, he decided to build a mechanically-recording seismograph.
		For a sensor, he used an inverted pendulum stabilized by springs
		and free to oscillate in any direction horizontally (Wiechert, 1904).
		The seismograph was completed in 1900.
	</p>
	<figure class="left" style="max-width:429px">
		<img src="figures/fig_26.gif"/>
		<figcaption>
			Figure 26. The 1000 kg Wiechert inverted pendulum seismograph
			(after Wiechert, 1904). The plate <i>P</i> is attached to the frame
			of the instrument. <i>N</i> is attached to the pendulum mass.
			The motion of the mass relative to the frame is resolved at <i>A</i>
			into perpendicular components. Restoring force is applied
			to the mass <i>M</i> from springs at <i>C, C',</i> by means of
			the rods <i>B, B'</i>. <i>H, H'</i> are the damping cylinders.
			The whole inverted pendulum is pivoted at <i>K</i>. In the actual
			seismometer, the rotation of the pendulum about <i>K</i> takes place
			in flat springs, which are arranged in a Cardan hinge
			to permit the pendulum to move in any horizontal direction.
		</figcaption>
	</figure>
	<p>
		In principle, Wiechert's inverted-pendulum seismometer resembled
		the inverted-pendulum seismometer of James Forbes, built sixty
		years earlier. The restoring springs, which kept the pendulum in
		stable equilibrium for small oscillations, were applied to the top
		of the inertial mass. At the base of the pendulum was a joint
		which permitted motion in any horizontal direction. The first
		description of the seismograph which we have found was published
		in 1904, after the instrument had undergone alterations in details
		of its construction (Wiechert, 1904). The 1904 pendulum (Figure 26)
		carried a mass of 1000 kilograms.
		<font size=-1>
		[In later years, Wiechert inverted-pendulum seismometers were built
		with masses as light as 80 kilograms (Berlage, 1930, p. 434-435).]
		</font>
		The relative motion of the mass with respect to the ground was
		resolved into perpendicular components, magnified 200 times by a
		mechanical lever system, and written on smoked paper. Time marks
		were put on the paper every minute by lifting the writing index
		off of the paper. For damping, Wiechert applied the motion of
		the pendulum mass to pistons which fit closely inside cylinders
		attached to the seismometer stand. Resistance of the air to the
		piston motion provided damping
		for the pendulum; this resistance was controlled by a valve
		which had the effect of regulating the amount of
		air space between the piston and the cylinder.
	</p>
	<p>
		We have mentioned the work of W. Schl&#252;ter, a colleague of
		Wiechert's, who attempted to measure tilt with his klinograph.
		After he failed to record tilt, Schl&#252;ter converted his klinograph
		into a standard seismograph. By adding mass to one side of the
		klinograph and thus moving the instrument's center of mass away
		from the axis of rotation toward that side, he could make the
		klinograph sensitive to linear motion perpendicular to the line
		containing the center of mass and the axis of rotation.
		In particular, by moving the center of mass horizontally away
		from the axis of rotation,
		and maintaining it in that position with a vertical spiral spring,
		he could render the klinograph sensitive to vertical displacement.
		Schl&#252;ter obtained in this way the first long-period
		vertical-motion seismometer. As he finally developed the instrument,
		it was used with a period of sixteen seconds and a static
		magnification of 160. The klinograph was damped; it recorded
		photographically. Schl&#252;ter used compensation devices to remove
		drift due to temperature changes and changes in the stiffness of
		the spiral
		spring: most of the records reproduced by him show remarkably
		little drift (Figure 27).
	</p>
	<p>
		Schl&#252;ter's klinograph obtained the first satisfactory records
		of longer-period vertical motion. Schl&#252;ter noted that, in general
		appearance, vertical-motion seismograms resembled horizontal-motion
		seismograms. There were differences, however, particularly in the
		onsets of earthquakes, which were much sharper on the
		vertical-motion seismograms than on horizontal-motion seismograms
		of the same earthquakes. For very distant earthquakes, further than
		90 degrees away, the onset on the vertical-motion records occurred
		several minutes earlier than the onset on the horizontal-motion
		seismogram. Schl&#252;ter accordingly suggested that travel times might
		better be obtained from vertical-motion records than from
		horizontal-motion records.
	</p>
	<figure class="right">
		<img src="figures/fig_27.gif"/>
