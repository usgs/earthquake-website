<h2>by<BR>James Dewey and Perry Byerly</h2>

<blockquote>
	<h3>Abstract</h3>

	<p>
		The earliest seismoscope was invented in 132 A.D.,
		by Chang H&#234;ng. Seismoscopes
		of limited effectiveness were used by Bina and others
		in the eighteenth century. The
		middle nineteenth century saw the invention
		by Palmieri of a seismoscope to record
		the times of small earthquakes.
	</p>
	<p>
		A successful seismograph of low sensitivity was invented
		by Cecchi in 1875.
		British scientists at the College of Engineering, Tokyo,
		independently built
		seismographs in the 1880's. The British in Japan made
		many observations with their
		instruments and must be credited with first demonstrating
		the value to seismology of
		seismographic devices.
	</p>
	<p>
		Von Rebeur-Paschwitz obtained the first recording of a teleseism in 1889.
		In the next decade, investigators in Italy,
		Germany, and
		England studied the waves
		from distant earthquakes and constructed the first teleseismic travel-time charts.
		Wiechert introduced a seismometer with viscous damping in 1898.
	</p>
	<p>
		Theory seems to have been neglected in the early development of the
		seismograph. Theoretical studies of forced damped harmonic-oscillator
		seismographs were presented by Perry and Ayrton, and Lippmann, but these had little
		effect on the construction of seismographs. In the 1890's, the importance of tilt was
		much debated. By 1900,
		many seismologists had become convinced that the effect
		of tilting on seismograph response could usually be neglected.
	</p>
  <p>
  	<small>
    	From the Bulletin of the Seismological Society of America.
    	Vol. 59, No. 1, pp. 183-227. February, 1969.
  	</small>
  </p>
</blockquote>

