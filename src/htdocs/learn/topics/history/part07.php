	<p>
		Cecchi's seismograph had relatively little impact upon
		Italian seismology. The insensitivity of the instrument
		must have discouraged others from building seismographic devices
		(De Rossi, 1887). In addition, Cecchi's apparatus would have
		been costly and unsuitable for an observatory of modest means.
		Finally, many Italian seismologists seemed to believe that
		an earthquake could be satisfactorily described by seismoscopic data
		(De Rossi, 1877, p. 9). We won't discuss in detail any more of
		the Italian seismoscopes. Descriptions of many are given by
		Agamennone (1906) and Ehlert (1897a). A review in English
		of the Italian instruments was given in <i>The Electrical World </i>
		(anon., 1887). Some of these seismoscopes depended on the fall
		of a delicately-balanced object to trip a time-recording
		device and sound an alarm. Many were modifications
		of Palmieri's seismoscopes. The microphone was used by some
		Italian seismologists to listen to "earth noises" (De Rossi, 1883).
	</p>
	<p>
		An example of the use of seismoscopes in Europe is the experiment
		of von Lasaulx (Hoernes, 1893). He constructed an apparatus
		in which the fall of a poised weight caused a pendulum clock
		to be stopped. About one hundred and fifty of these devices
		were installed at telegraph stations in Germany. The sensitivity
		of the installed seismoscopes was deliberately kept low,
		so that human disturbances would not cause the clocks to be
		stopped frequently. Nevertheless, two earthquakes were large
		enough to be detected by many of the seismoscopes. Time and
		"direction of motion" were determined from each instrument.
		The data were contradictory and seemed only to indicate that
		most of the clocks at the telegraph stations were not
		sufficiently accurate for seismological work.
	</p>
