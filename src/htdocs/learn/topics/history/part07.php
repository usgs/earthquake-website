	<h3>The Invention of a Seismograph in Italy</h3>

	<p>
		The research begun by Cavalleri and Palmieri was vigorously
		continued by Italian seismologists in the 1870's. 1874 saw
		the publication of the first journal devoted to solid-earth
		geophysics, the <i>Bulletino del Vulcanismo Italiano</i>,
		founded and edited by
		M. S. De Rossi. In spite of the high seismological activity,
		however, relatively few instrumental advances emerged from
		this period, and the instrument which to us is most
		significant seems to have been little noticed at the time
		it was invented. This was Cecchi's seismograph, which we will
		describe in the next paragraphs. The great majority of the
		other instruments designed by Italian seismologists at this time
		were seismoscopes of the same general type as had been
		invented earlier by Palmieri.
	</p>
	<p>
		Cecchi's instrument has been described by later Italian
		seismologists as the first true seismograph (Agamennone, 1906, p. 91).
		The machine was apparently built in 1875 (Cecchi, 1876).
		Unlike any of the instruments we have discussed so far, the
		Cecchi seismograph was expected to record the relative motion
		of a pendulum and the Earth as a function of time. For horizontal
		vibrations, two common pendulums were used, one vibrating in a
		north-south plane and the other vibrating in an east-west plane.
		The pendulums "beat seconds"; their motion was magnified three
		times by a thread-and-pulley apparatus. For vertical motions,
		a mass on a spiral spring was used. Finally, a machine which
		was expected to record rotary motions, was incorporated into
		the seismograph. This consisted of a cross bar with weights at
		both ends, much like a dumbbell, which was pivoted at its
		center of mass so as to rotate in a horizontal plane.
		Restoring force was applied to the dumbbell by springs,
		so that it oscillated with a period of one second.
	</p>
	<p>
		Cecchi arranged a seismoscope to start a clock and to start
		into motion the recording surface at the time of an earthquake.
		The recording surface would translate under the indicating needles
		at a speed of one centimeter-per-second for twenty seconds.
		From the time on the clock, an observer arriving at the
		seismograph would determine how long before his arrival
		the earthquake had occurred.
	</p>
	<p>
		Unfortunately, the subsequent history of Cecchi's earliest
		seismograph is unclear. A modified form of the instrument
		was reported to have been installed in Manila (Du Bois, 1885).
		This instrument and the later Cecchi seismographs reported
		by Agamennone (1906) are not so interesting to us because,
		by the time these instruments were built, better seismographs
		were being used by British scientists in Japan. The early
		form of the Cecchi instrument was apparently installed in
		several observatories. One might think that one of these
		seismographs would have recorded an earthquake in the five years
		between 1875 and 1880, the latter being the year of the earliest
		seismogram obtained by the British in Japan, who also claimed
		the first real seismograph. But the earliest date we have found
		for a seismogram obtained with the early Cecchi instrument
		is February 23, 1887, when one of these seismographs recorded
		a large earthquake which occurred in the French-Italian border region
		(Denza, 1887). In that earthquake, only the east-west component
		was recorded, although the seismograph was located in a zone
		of strong shaking. The instrument must have been most insensitive.
		The seismogram is reproduced in Figure 6. The record appears to us
		to be as accurate a representation of earth movement as was obtained
		by the early seismographs in Japan. Assuming that the recording
		instrument had not been significantly altered between 1875 and 1887,
		it would seem that Cecchi indeed deserves credit for the
		construction of the earliest seismograph, insensitive
		though it might have been.
	</p>
