<h2>by<br>James Dewey and Perry Byerly</h2>

<blockquote>
	<h3>Introduction</h3>

	<p>
	Instruments have been used in the study of
	earthquakes for over eighteen hundred
	years. The earliest known seismoscope was
	intended to record both the occurrence of
	earthquakes and the azimuths of their origins from the observer.
	In the eighteenth
	century, we find proposals to record the
	times of earthquakes and the character of the
	ground motion occurring in earthquakes.
	A most important advance was made late in the
	nineteenth century, with the invention of instruments which
	gave records
	representing earthquake ground motion
	as a continuous function of time. We will call such
	instruments seismographs, regardless of the accuracy
	of their representations of earth
	motion. Our history will end at the beginning of the twentieth century.
	</p>

	<P>
		<small>
  		From the <i>Bulletin of the Seismological Society of America</i>.
  		Vol. 59, No. 1, pp. 183-227. February, 1969.
		</small>
	</p>
