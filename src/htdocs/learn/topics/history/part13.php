<h2>by<br>James Dewey and Perry Byerly</h2>

<blockquote>
	<h3>Conclusion</h3>

	<p>
		We end our history of early seismometers at the beginning of the
		twentieth century. The Wiechert inverted-pendulum seismometer
		is probably the earliest seismograph which is still used,
		in essentially its original form, in some modern seismological
		observatories. With the introduction of damped seismographs,
		seismologists had instruments with which it would be possible
		to calculate fairly accurate ground displacements for all
		recorded waves, not just short-period waves. Mathematical theory
		for calculating displacements from recorded waveforms would be
		forthcoming in the next decade (for example, Wiechert, 1903;
		Galitzin, 1904; Reid, 1910).
	</p>
	<p>
		We have seen that a large number of instrumental needs
		were recognized by the turn of the century, including the need for
		a strain seismometer. The type of seismometer most obviously
		missing from our history of early seismometers is the electromagnetic
		seismograph, introduced by Galitzin (1903). The
		galvanometer was widely used in the nineteenth century; important
		improvements were made by some of the
		very men we have encountered in seismology, including
		Thomas Gray, W. E. Ayrton, and J. Perry
		(see references given by A. Gray, 1921).  We
		find Thomas Gray simultaneously publishing papers on
		galvanometers and seismometers (Gray, 1887a, 1887b).
		Milne (1882a) even used a galvanometer in a
		seismoscope in 1879; the galvanometer
		indicated the output of a microphone with which Milne hoped to
		detect possible "creaking" of a fault preceding an earthquake.
		But no one in the nineteenth century seems to have tried to
		obtain an electrical analogue of earthquake motion and record it
		with a galvanometer.
	</p>
	<p>
		We have described the early development of seismological instruments,
		from seismoscopes capable of giving only a few data to seismographs
		suitable for detailed studies of earth motion. We have seen that
		the first important step in seismometry
		was the use of an inertial system so arranged that it would be
		caused by an earthquake to move relative to the ground.
		The value of various types of pendulums for earthquake sensing
		instruments was realized early, probably as early as 132 A. D.
		Andrea Bina, in 1751, was the first to build an instrument for
		recording the relative displacement of the ground and a pendulum
		bob in an earthquake.
	</p>
	<p>
		In these early years of seismology, emphasis was placed on measuring
		a few specific parameters of earthquake motion. Besides detecting
		the occurrence of an earthquake, almost all the instruments we
		have discussed gave an indication of the "direction" of the shock.
		Many were designed to record the relative "size" of an earthquake.
		Devices to record the time of an earthquake were proposed in the
		eighteenth century. Palmieri's apparatus, installed in 1856,
		seems to have been the first to give the "times"
		of shocks.
	</p>
	<p>
		Cecchi may be regarded as the inventor, in 1875, of the earliest
		seismograph. Nevertheless, British scientists working in Japan
		in the 1880's must be given credit for developing the seismograph
		as a practical research instrument. At this time, most seismologists
		tried to obtain the longest possible periods for their instruments.
		Long common pendulums were used. The horizontal pendulum was
		introduced into seismology in Japan. The inverted pendulum,
		combined with a restoring force so that it would be in stable
		equilibrium, had been used for a seismometer by Forbes in 1840, and
		was tried again in Japan in the 1880's. A short-period instrument,
		to have a period less than the periods occurring in earthquake
		motion, was suggested by Perry and
		Ayrton in 1877.
	</p>
	<p>
		The last decade of the nineteenth century saw seismographs
		designed to record
		teleseisms as well as local earthquakes. The first record of a
		distant earthquake had been obtained by von Rebeur-Paschwitz in
		1889. Von Rebeur's seismograph recorded optically on a
		continuously moving photographic surface. Photographic recording
		made possible small seismographs of high sensitivity. The records
		given by mechanically-recording seismographs, however, were
		generally sharper and easier to read than those from
		photographically-recording instruments. Artificial viscous damping
		was first used in seismographs by Wiechert in 1898. In 1900,
		the first Wiechert inverted-pendulum seismograph was built.
	</p>
	<p>
		The earliest mathematical theory of a seismograph was given by
		Forbes in 184l. Forbes considered only simple non-oscillatory
		ground displacements. A theory of the response of a seismograph
		to arbitrary, periodic, ground motion was written in Japan, by
		Perry and Ayrton in 1877, but it appears to have had little
		influence on the development of seismographs in that country.
		Later theoretical notes by Poincar&#233; and Lippmann on integrating
		seismograms to obtain displacements seem also to have
		passed unnoticed by most seismologists in the nineteenth century.
		The importance of tilting in seismograms were debated; by the end
		of the century, the once widely-accepted view that teleseismic records
		represented tilting of the ground was being abandoned by many
		seismologists.
	</p>
	<p>
		<small>
			From the <i>Bulletin of the Seismological Society of America</i>.
			Vol. 59, No. 1, pp. 183-227. February, 1969.
		</small>
	</p>
</blockquote>

