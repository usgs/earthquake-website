<h2>C - Creep evidence</h2>
<figure class="right" ><img src="../images/creep_img.gif" alt="offset curb photo" /></figure>
<p> Creep is steady fault movement, varying from continuous
                      to episodic with creep events lasting minutes to days. Generally creep occurs
                      without any associated earthquake activity (i.e., aseismic). Creep has been
                      monitored on the Hayward fault for forty years (<a href=  "http://quake.usgs.gov/research/geology/docs/HFcrpGRL01a.pdf">Lienkaemper et al., 2001</a>) and is also observed along some sections of other faults in the San Francisco Bay region, including the San Andreas, Calaveras, Concord-Green Valley and Maacama (<a href= "http://quake.usgs.gov/research/geology/docs/Creep_BSSA03.PDF">Galehouse and Lienkaemper, 2003</a>). Locations on the map showing creep evidence are indicated by green circles.</p>
<p>The depth of creep varies, from as little 2 miles deep in northern Oakland to as deep as 7 miles near the northern and southern ends of the fault. Unfortunately, this means the lower, brittle fault zone remains largely locked (i.e., it's not creeping), so it is building strain which can only be relieved by major earthquakes, which occur about every 100 to 200 years. The average recurrence time for earthquakes is determined by paleoseismologists, geologists who work in trenches across the faults. They document evidence of paleoearthquakes recorded in sedimentary layers, using radiocarbon analysis to date them.</p>
