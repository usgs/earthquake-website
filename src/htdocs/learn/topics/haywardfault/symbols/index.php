<h2>T - Trenching evidence</h2>
          <p>Locations of trenches are shown on the maps as thin blue lines and each is labeled using codes (T) and abbreviated descriptions of the evidence found in the trench.</p>
          <p>Most trenches across the Hayward fault were excavated to meet the requirements of the <a href="http://www.consrv.ca.gov/CGS/rghm/ap/index.htm">Alquist-Priolo Earthquake Fault Zoning Act</a>, to insure that structures are not built over active fault traces. Other trenches have been used by scientists to learn about the fault's capability as a source of earthquakes. Earthquake geologists probe in to the fault's past behavior using the tools of paleoseismology.</p>
