<p>To better understand the distribution of shaking and damage that accompanied the great 1906 earthquake, seismologists have constructed new computer models to recreate the ground motions. The simulations show how ground moved on the two sides of the San Andreas fault and how seismic waves radiated away from the fault to produce the shaking. The earthquake, which began 2 miles offshore from the City of San Francisco, ultimately grew to cause shaking and damage along more than 300 miles of the San Andreas Fault. </p>
<p>The movies and snapshots available here portray the shaking over a 155 mile by 70 mile area covering the San Francisco Bay region. They are offered in .mp4 format, which can be viewed using most browsers. These movies are typically 10 MB or more in size, and are best downloaded using a high speed internet connection. Snapshots from one movie are also offered as an alternative for those with slower modem connections. </p>
<p>Explanation of <a href="../colors.php">colors</a> and <a href="features.php">earthquake features</a> in the movies.</p>

<h2>Map Views of Ground Shaking</h2>
<p>
Map-view snapshots and movies show the seismic waves
propagating outward from the fault as the rupture
expands north and south from the epicenter, which lies offshore
from San Francisco.  The colors and shading
indicate the maximum shaking intensity at each location and the current
shaking at the time noted in seconds on each movie frame.
</p>

