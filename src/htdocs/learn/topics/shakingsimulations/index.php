<p>
  To better understand the distribution of shaking from past and anticipated future earthquakes, seismologists have constructed computer models of earthquakes and the resulting ground shaking. These computer models capture the shaking at length scales larger than about 300 ft (100 m), so they do not include the effects of very thin, soft sediments, such as Bay Mud around the perimeter of San Francisco Bay. As a result, the simulations provide a lower bound on the level of shaking in areas with these thin, soft sediments. The movies available on these web pages were constructed from these computer simulations. The movies include both map views and detailed, perspective views. The colors indicate the peak intensity of the shaking so far at each location. See <a href="./colors.php">Explanation of Colors</a> for a more detailed description of the color scheme. In the perspective views, the deformation of the ground has been exaggerated by a factor of 1000 to illustrate how the ground moves when the seismic waves propagate through the earth.
</p>
