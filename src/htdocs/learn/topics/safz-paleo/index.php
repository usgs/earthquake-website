<p>
  Maybe you’ve heard that the “Big One is overdue” on the San Andreas Fault.  No one can predict earthquakes, so what does the science really say?  Where does the information come from?  And what does it mean?
</p>
<p>
  Earth scientists have been gathering data at key <a href="/education/paleo-intro/">paleoseismic sites</a> along sections of the San Andreas Fault to figure out the past timeline of earthquakes at each spot. The data show that at many places along the San Andreas Fault, we have gone past the average time between large earthquakes.  Since we have exceeded the average, many people use the term &ldquo;overdue,&CloseCurlyDoubleQuote; but it’s more complicated than that. First, let's zoom out and look at the big picture.
</p>

<h2>San Andreas Fault Zone – The Big Picture</h2>

<figure class='left safz'>
  <img src='images/plates.gif' alt='plate tectonic cartoon'/>
  <figcaption>
    Cartoon sketch of the Pacific Plate-North American Plate boundary showing the San Andreas Fault. (from <a href="https://pubs.usgs.gov/gip/dynamic/dynamic.html"><i>This Dynamic Earth: The Story of Plate Tectonics, USGS</i></a>)
  </figcaption>
</figure>

<p>
  Scientists have a good big picture understanding of the San Andreas Fault Zone (SAFZ).  The SAFZ started moving about 28-30 million years ago, and has horizontally slipped (transform motion) a total of about <strong>300-350 km (186-220 mi)</strong> since it began moving.  The SAFZ is the main part of the boundary between the Pacific tectonic plate on the west side and the North American plate on the east side. The “zone” part of the name means it’s a system with a main fault and many sub-parallel faults that all together take up the motion between the two plates. In northern California, the zone includes the Hayward, Calaveras, as well as the Northern San Andreas and other faults, and in southern California, the zone is even wider, encompassing the Southern San Andreas, the San Jacinto, and other faults in the Los Angeles area.
</p>
<p>
  The <strong>relative motion between these two tectonic plates is 50 mm/yr (about 2 inches/yr)</strong>, but that rate is distributed across all the faults that are part of the SAFZ.  The faults are boundaries between blocks, and each block is constantly moving, which we can see by analyzing GPS (Global Positioning System) data.  However, the edges of the blocks, the faults themselves, are stuck and only move where there is a large earthquake (some faults creep a little bit, but most are locked).  An earthquake occurs when the stress from the force of the moving plate overcomes the friction causing the plate boundary edges, the fault, to stick.  The stuck section slips, and the edge of each block catches up to the rest of the plate.  The plate is moving slowly all the time, but the edges move in fits and starts.
</p>
<p>
  Many of the sites paleoseismologists have been studying are along key sections of the SAFZ where there is a large population or major infrastructure that would be affected by a large earthquake in the future. Let's start in southern California and work our way north.
</p>

<h2>Southern California</h2>

<figure>
  <img src='images/sca.gif' alt='Southern CA map'/>
  <figcaption>
    Map of faults in southern California. Bold numbers show the average time between big earthquakes, determined at paleoseismic sites (triangles). Thick red lines show the extent of historic ruptures. (Kate Scharer, USGS)
  </figcaption>
</figure>

<p>
  There are only two large known historic earthquakes on the San Andreas Fault in southern CA, the most recent in 1857, and before that one in 1812.  With about 45 years between the historic earthquakes but about 160 years since the last one, it is clear that the fault does not behave like a clock with a regular beat.  Historic information doesn’t provide enough data to establish whether or not there is a pattern in the timing of earthquakes, but paleoseismology has provided an abundance of data.
</p>
<p>
  Along the southernmost San Andreas, from <strong>Palm Springs to the Salton Sea</strong>, earthquakes happen infrequently, about every 200-300 years.  The most recent earthquake occurred during the time of Spanish exploration, about 300 years ago, but there is no historic record of the event. Instead, radiocarbon dating provides the age of the most recent earthquake and six more that occurred since about 800 A.D.
</p>
<p>
  A paleoseismology site in <strong>Wrightwood, CA</strong> has been studied by several scientists, and recently (in 2010) the detailed data from multiple studies were joined together to create a single timeline.  The resulting 3000-year record includes 29 surface-rupturing earthquakes.  Careful analysis of the age of the earthquakes, including the uncertainties in radiocarbon dating (see Determining the Age of a Paleoearthquake in <a href="/education/paleo-intro/">Introduction to Paleoseismology</a>), showed that the average time between earthquakes is about 100 years.
</p>
<p>
  The recurrence intervals (times between earthquakes) at Wrightwood are more regular than clustered (determined by a mathematical analysis), and only four times in the past has the interval between two major earthquakes been longer than the current interval (since 1857).  The results of this study indicate that this section of the San Andreas Fault is likely to have a large earthquake in the not-too-distant future.
</p>
<p>
  About 100 km to the northwest along the fault another site at <strong>Frazier Mountain</strong> has been investigated.  At that location, the record is about 1000 years long, and in that time period there are about 9 large earthquakes recorded in the sediments, including the 1857 rupture.
</p>
<p>
  Comparing the data from sites like Wrightwood and Frazier Mountain, earthquake scientists are working to understand the pattern of large earthquakes – asking questions such as how typical was the large (M7.9) earthquake in 1857?  Or is the size of the 1812 earthquake (~M7.1) more common? Note that because the magnitude scale is a log scale, there is about a 25-fold difference in the energy released by these different earthquakes.
</p>

<h2>Northern California</h2>

<figure>
  <img src='images/nca.gif' alt='Northern CA map'/>
  <figcaption>
    Map of faults in northern California. Bold numbers show the average time between big earthquakes, determined at paleoseismic sites (triangles). Thick red lines show the extent of historic ruptures. (Kate Scharer, USGS)
  </figcaption>
</figure>

<p>
  The <strong>Hayward fault</strong> in the San Francisco Bay area runs through a densely-populated area, so it has been studied quite a bit.  The most recent major earthquake on this fault was approximately M6.9 and occurred in 1868.  The fault has been creeping about 4.6 mm/yr (0.2 inches/yr) for the last several decades, but that is only half of the long-term slip rate, so stress is building up on this fault.  A paleoseismic study in 2007 at <strong>Tyson’s Lagoon</strong> (now a BART station) found evidence for 12 paleoearthquakes (including the historical 1868 earthquake) with an average time between earthquakes of about 160 years. The average time interval between the 5 most recent earthquakes is a little shorter, about 140 years. The study concluded that there is 33% likelihood of a surface-rupturing earthquake within the next 30 years. (See <a href="https://pubs.usgs.gov/fs/2016/3020/fs20163020.pdf">Earthquake Outlook for the San Francisco Bay Region 2014&mdash;2043</a>).
</p>
<p>
  The <strong>Maacama fault</strong> is the northward continuation of the Hayward-Rodgers Creek fault system in northern California. In 2014, a paleoseismology site at <strong>Hael Creek</strong> on the Maacama fault reiterated the results found on the Hayward fault to the south – creeping with infrequent large earthquakes, and a large one expected in the not-too-distant future.
</p>
<p>
  The <strong>Hazel Dell</strong> site near Corralitos, CA was trenched in 2013 to characterize the Santa Cruz Mountains section of the San Andreas Fault.  The Santa Cruz section stretches 62 km (39 mi) from Los Gatos (near San Jose) to San Juan Bautista, CA and was last ruptured in the famous 1906 San Francisco earthquake.  Observations in the trenches along with radiocarbon dating of charcoal, wood chips, and small plant remains, combined with a reevaluation of three previously-studied nearby paleoseismic sites revealed a variation in seismic activity in the past. Three earthquakes occurred within a 70-year period between 1838 and 1906, but there were no earthquakes during the 500 years before that, and there have been no earthquakes in the 110 years since 1906.
</p>
<p>
   This shows that the average time between earthquakes includes some intervals that are short and some intervals that are long. New studies farther to the northwest along the <strong>Peninsula section of the San Andreas Fault</strong> also show a long interval between the 1906 earthquake and the previous earthquake, which occurred  around 1300. Prior to 1300 the intervals are shorter, about 200 years. The North Coast section of the San Andreas Fault is north of San Francisco. Studies of this section of the fault suggest an average recurrence interval of 200-300 years.
</p>

<h2>Now What?</h2>

<p>
  The paleoseismic data on different parts of the San Andreas Fault Zone are all telling us that some sections appear to be past the average, or "overdue" for a significant earthquake.  But the data can’t be used to make predictions: we do not understand earthquakes well enough to know exactly where the next earthquake will occur, what the magnitude will be, or exactly when it will happen.
</p>
<figure class='right'>
  <img src='images/Drop_Cover_Hold_On_ENG_Blue_Orange.png' alt='Drop, Cover, Hold On icon'/>
</figure>
<p>
  Let’s imagine for a minute that we know where, how large, and when an earthquake will be.  You might think that would be good because then you could leave the area beforehand and then return after the earthquake.  But focusing only on avoiding an earthquake doesn’t address most of the effects from the shaking.  As you returned to your home, you would probably see damaged and collapsed buildings and bridges, broken pipes and snapped power lines, and scorched remains of fires.  Upon entering your house, you would stumble over toppled bookcases, broken glass from mirrors no longer on the walls, and the contents of kitchen cabinets in piles on the floor.
</p>
<p>
   <strong>What we do know is that California is "earthquake country" and we need to be prepared.</strong> In particular, we need to design buildings and infrastructure to be able to withstand the earthquake shaking or be easily repaired. Scientists are working to improve forecasts that estimate how often future earthquakes will occur and how much the ground will shake so engineers and planners will know where to focus efforts to mitigate the effects of damaging earthquakes. Using the forecasts, we can properly engineer structures, plan for earthquake response, and be prepared at home to make a big difference in the impact of a significant earthquake.
</p>

