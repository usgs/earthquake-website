<p>
  Maybe you’ve heard that the “Big One is overdue” on the San Andreas Fault.  No one can predict earthquakes, so what does the science really say?  Where does the information come from?  And what does it mean? Earth scientists have been gathering data at key sites along sections of the San Andreas Fault to figure out the past timeline of earthquakes at each spot.  This type of research is called paleoseismology (&ldquo;paleo&CloseCurlyDoubleQuote; means old, and &ldquo;seismology&CloseCurlyDoubleQuote; is the study of old large earthquakes, so paleoseismology is the study old large earthquakes, called &ldquo;paleoearthquakes&CloseCurlyDoubleQuote;).  The data show that in many places along the San Andreas Fault, we have gone past the average time between large earthquakes.  Since we have exceeded the average, many people use the term &ldquo;overdue,&CloseCurlyDoubleQuote; but it’s more complicated than that.
</p>

<h2>Paleoseismology - The Past Informs the Future</h2>
<p>
  The basic assumption that paleoseismologists use is that what happened in the past will most likely happen again in the future. Therefore, the more information that can be gathered about the past, the better idea we’ll have about what is likely to happen in the future.  This is important because we need to be able to design bridges and big buildings to withstand future large earthquakes, and to plan so we can be resilient in the days and years after the big earthquakes occur. This means we need paleoseismologists to collect information about when past earthquakes occurred on faults and how large the earthquakes were.
</p>
<p>
  When it comes to sleuthing for evidence of past earthquakes, there is primary evidence and secondary evidence. Primary evidence includes clues that are created by the movement on the fault during the earthquake such as fault scarps, offset or folded layers of sediment and soil, and parts of the landscape that have been tilted, uplifted, down-dropped, or torn apart.  The picture above shows an example of a fault scarp from the relatively small 2014 South Napa earthquake (M6.0). In larger earthquakes, the movement may be bigger.  For example, a historic account from the 1857 M7.9 earthquake on the San Andreas Fault in southern California, stated that “This rent was in some places five to 10 yards wide, the earth at times filling it up like ploughed furrows; at others the ground stood apart, leaving a deep fissure.” Secondary evidence includes clues produced by the shaking during the earthquake such as sand blows, rockfalls, landslides, water level changes, and damaged trees.
</p>

