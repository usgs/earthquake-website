  In addition to trenching, there are some other rather creative techniques for identifying paleoearthquakes. Using dendrochronology, or tree rings, is an interesting way to look for paleoseismic evidence.  Trees that were severely shaken, permanently tilted from shaking, or were inundated by salt water from subsidence, will suffer in health and not grow as quickly as usual for a while after an earthquake.  The subsequent tree ring for that time period will be thinner than usual. In coastal areas like where this photo was taken, dendrochronology can be used to determine when large paleoearthquakes altered the level of the land: during subduction zone earthquakes some parts of the land surface can lower by several feet. Thus forests with trees going along a coast will suddenly die as the roots of the trees are drowned in salt water.
</sidebar>

