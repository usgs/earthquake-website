<h2>Finding Paleoearthquakes</h2>
<p>
  Finding these paleoearthquakes requires going to the right place.  First, scientists study the landscape to find places where the faults are active.  Second, they must go to places where the shape of the land can preserve the evidence – places where the soil and sediment will cover the fault scarp and preserve it for hundreds to thousands of years.  For the most part, paleoseismologists only see the larger earthquakes, above about a M6, as below that magnitude they are too small to leave a mark on the landscape.  Consequently, paleoseismology mostly provides data on the biggest earthquakes with the potential to cause the most damage.
</p>
<p>
  To find a paleoearthquake, first you need to find a fault.  The standard use of lidar (See Down in the Trenches and Up in the Air - earthquake.usgs.gov/learn/topics/wasatch/collect.php) in geological investigations has made it much easier to find evidence of faults. Lidar can cover a lot more ground than a few geologists hiking around looking for evidence of past earthquakes, and its ability to “see through” the vegetation also allows coverage in areas where it may be difficult or impossible for scientists to access.
</p>

