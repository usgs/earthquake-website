<h2>Southern California</h2>
<p>
  There have been only 2 historic earthquakes on the SAFZ in southern CA, the most recent in 1857, and before that one in 1812.  With about 45 years between the historic earthquakes but about 160 years since the last one, it is clear that the fault does not behave like a clock.  That information doesn’t provide enough data to establish whether or not there is a pattern in the timing of earthquakes, but paleoseismology has provided an abundance of data.
</p>
<p>
  A paleoseismology site in Wrightwood, CA has been studied by several scientists, so recently (in 2010) the detailed data from multiple studies was joined together to create a single timeline.  The resulting 3000-year record includes 29 surface-rupturing earthquakes.  Careful analysis of the age of the earthquakes, including the uncertainties in radiocarbon dating (see Determining the Age of a Paleoearthquake above), showed that the average time between earthquakes here is about 100 years.  The recurrence intervals (time between earthquakes) is more regular than clustered (determined by a mathematical analysis), and only four times in the past has the interval between two major earthquakes been longer than the current interval (since 1857).  The results of this study indicate that this section of the SAFZ is likely to have a large earthquake in the not-too-distant future.
</p>
<p>
  About 100 km to the northwest along the fault another site at Frazier Mountain has been investigated.  At that location, the record is about 1000 years long, and in that time period there are about nine large earthquakes recorded in the sediments, including the 1857 rupture.  Comparing the data from Wrightwood and Frazier Moutain, earthquake scientists are working to understand the pattern of large earthquakes – asking questions such as how typical was the large (M7.9) earthquake in 1857?  Or is the size of the 1812 earthquake (~M7.1) more common? Remember that because the magnitude scale is a log scale, there is about a 25-fold difference in the intensity of these different earthquakes.
</p>
<p>
  Along the southernmost San Andreas, from Palm Springs to the Salton Sea, the earthquakes happen less frequently, about 150-200 years apart.  The last earthquake to occur here happened when the Spanish were exploring what is now thw Southwest of America, about 300 years ago, but there is no historic record of the event. Instead, radiocarbon dating provides the age of the most recent earthquake and six more that occurred since about 800 A.D.
</p>

