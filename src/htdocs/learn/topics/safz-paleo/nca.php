<h2>Northern California</h2>
<p>
  The Hayward fault in the San Francisco Bay area runs through a densely-populated area, so it has been studied quite a bit.  That last major earthquake on this fault was an approximately M6.9 in 1868.  The fault has been creeping about 4.6mm/yr for the last several decades, but that is only half of the long-term slip rate, so stress is building up on this fault.  A paleoseismology study in 2007 at Tyson’s Lagoon (now a BART station) included 11 paleoearthquakes with an average time between earthquakes of about 170 years.  AMS radiocarbon dating was used to generate the best dates possible.  The study concluded that there is a 33% likelihood of a surface-rupturing earthquake within the next 30 years http://pubs.usgs.gov/fs/2016/3020/fs20163020.pdf
</p>
<p>
  The Maacama fault is part of the northward continuation of the Hayward-Rodgers Creek fault system in northern California. In 2014, a paleoseismology site at Hael Creek on the Maacama fault reiterated the results found on the Hayward fault to the south – creeping with infrequent earthquakes, and a large one expected in the not-too-distant future.
</p>
<p>
  The Hazel Dell site near Corralitos, CA was trenched in 2013 to characterize the Santa Cruz Mountains section of the SAFZ.  The Santa Cruz section stretches 62 km (?? mi) from Los Gatos (near San Jose) to San Juan Bautista, CA and was last ruptured in the famous 1906 earthquake.  Observations in the trench along with radiocarbon dating of charcoal, wood chips, and small plant remains, combined with a reevaluation of three previously-existing nearby paleoseismic sites revealed a variation in seismic activity in the past. Three earthquakes occurred within a 70-year period between 1838 and 1906, but there were no earthquakes during the 500 years before that, and there have been no earthquakes in the 110 years since 1906. This shows that the average time between earthquakes includes some intervals that are short and some intervals that are long. We need to build major infrastructure that can withstand these earthquakes.
</p>

