<h2>San Andreas Fault Zone – The Big Picture</h2>
<p>
  Scientists have a good Big Picture understanding of the San Andreas Fault Zone (SAFZ).  The San Andreas Fault started moving about 28-30 million years ago, and has horizontally slipped (transform motion) a total of about 500 km since its birth.  The SAFZ is the main part of the boundary between the Pacific tectonic plate on the west side and the North American plate on the east side. The “zone” part of the name means it’s a system with a main fault and many sub-parallel faults that all together take up the motion between the two plates. In northern California, the zone includes the Hayward, Calaveras, as well as the main San Andreas and other faults, and in southern California, the zone is even wider, encompassing the main San Andreas, the San Jacinto, and other faults in the Los Angeles area.
</p>
<p>
  The relative motion between these two tectonic plates is 50 mm/yr, but that rate is taken up by the series of fault in the SAFZ, bound by blocks.  Each block is constantly moving, which we can see by looking at GPS data.  However, the edges of the blocks, the faults themselves, are stuck and only move where there is a large earthquake (some faults creep a little bit, but most are locked).  An earthquake occurs when the stress from the force of the moving plate overcomes the friction causing the plate boundary edges, the fault, to stick.  The stuck section slips, and the edge of each block catches up to the rest of the plate.  The plate is moving slowly all the time, but the edges move in fits and starts.  The sites paleoseismologists have been studying are along key sections of the SAFZ where there is a large population or major infrastructure that would be affected by a large earthquake in the future.
</p>

