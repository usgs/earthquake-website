<h2>Faults East of the U.S. Rocky Mountains</h2>

<p>
  Faults are different from fault lines. A fault is a three-dimensional surface within the planet. Rocks have been broken at the fault surface  due to rocks on one side of the fault moving past the rocks on the other side. Faults come in all sizes, from small ones whose short fault lines can be seen in a single road cut, to large faults whose long fault lines can be seen best in pictures taken from orbiting satellites. On continents, faults are everywhere, of all sizes, and they formed at many different times during the Earth's long history. In contrast, a fault line is the location where the fault cuts the Earth's surface.
</p>

<p>
  The most prominent faults in each state are usually shown on a state's geologic map as black lines. However, many faults are entirely beneath the surface and do not reach ground level. Therefore, these buried faults have no fault lines, and they are usually not shown on geologic maps. If a buried fault is known at all, information about it is usually published in technical articles in geological journals.
</p>

<p>
  East of the Rockies, faults are usually not visible to a person standing on the ground. This is because faulting breaks rocks and tends to grind them into small bits that decompose faster than the nearby, unfaulted rocks. The result is that most fault lines are obscured by thick soils. That's especially common in more humid eastern states, because moisture accelerates  decomposition. In the northern states, fault lines and the adjoining rocks are also likely to be covered by sand, clay, or gravel deposited by melting glaciers. As a result, the presence of a fault east of the Rockies is often inferred by a geologist.
</p>


<h2>Earthquakes East of the U.S. Rocky Mountains</h2>

<p>
  Earthquakes occur when the two sides of a fault slip suddenly past each other and send out shock waves or vibrations. East of the Rockies, most earthquakes probably occur by new slip on existing faults. The vibrations travel through the Earth and, if strong enough, reach ground level, and can shake and cause damage to structures.
</p>

<p>
  Most earthquakes that occur east of the Rockies are often too small to be felt or cause damage. Of those that do damage buildings or other man-made structures, most cause only slight, localized damage with few injuries. However, a damaging earthquake east of the Rockies can occur periodically.
</p>


<h2>Faults and Earthquakes East of the U.S. Rocky Mountains</h2>

<p>
  In general, individual known faults and fault lines are poor indicators  to the likelihood of earthquakes. In California, a large earthquake can generally be associated with a particular fault because earthquakes often result in surface expressions such as ground surface offset. East of the Rockies things are less straightforward, because it is rare for earthquakes to break the ground surface. In particular, modern earthquakes east of the Rockies do not appear to correlate with most known faults and fault lines. The reason for this is not well understood and most earthquake locations cannot be measured very accurately east of the Rockies.
</p>

<p>
  Earthquakes typically occur several miles deep within the Earth. Their locations, including their depths, are usually uncertain by a mile or more. Although the larger faults extend from their fault lines downward deep into the Earth, their locations at earthquake depths are usually wholly unknown. The uncertain underground locations of earthquakes and faults make it difficult to determine whether a particular earthquake occurred on a particular known fault. There are many faults hidden underground that are large enough to generate damaging earthquakes, but which are also too small to extend from earthquake depths all the way up to ground level where we have the best chance of seeing the faults. Hidden faults are likely to be at least as numerous as known faults.
</p>

<p>
  Accordingly, an earthquake is as likely to occur on an unknown fault as on a known fault, if not more so making fault lines east of the Rockies unreliable guides to earthquake occurrence. The best guide to earthquake hazard east of the Rockies is probably the earthquakes themselves. Most earthquakes tend to occur in the same general regions that are already known to have earthquakes. In general, however, there is no reliable way to know where or when the next damaging earthquake will occur.
</p>


