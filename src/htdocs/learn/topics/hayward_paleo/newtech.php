<p>
	Recently, two new techniques have been introduced that supplement the basic trenching method. First,<em> peeling</em> helps preserve the information revealed in the trench by actualy removing the trench face, much the way the paint is sometimes pulled off a wall with old tape. When a peel is removed, the reading of the geologic strata in the trench walls can continue long after the trench is closed.
</p>

