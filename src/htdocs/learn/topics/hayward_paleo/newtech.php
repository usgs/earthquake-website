<p>
	Next, to extend the depth of a trench study (which extends the historical period available for study), geologists utilize a new device called Geoslicer, invented by Takashi Nakat of Hiroshima University. This device can reveal faulting in the deeper, water-saturated sediments found below the water table, where geologists cannot safely trench.
</p>

<p>
	On Tuesday October 2, 2001 Japanese scientists from the Geological Survey of Japan and Hiroshima University and American scientists from the U. S. Geological Survey removed a peel from one of the walls of a trench excavated the previous week. Using a rubberized epoxy compound, scientists eeled and removed the south face of the trench at Tule Pond. A series of photos take you through this <a href="slideshow/index.php#peel">peel process</a> at the Tule Pond site.
</p>

<p>
	Finally, on Wednesday, October 3, at the same location, the Japanese and American scientists demonstrated the Geoslicer, a large, crane-operated device designed and built in Japan. The Geoslicer can cut an additional section 12 feet deep (relative to the bottom of the10-foot-deep trench) into the geologic deposits, and bring it, intact, to the surface for study. The Geoslicer allows the geologists to sample below the water table. By extending the depth, the Geoslicer extends the length of the earthquake history available for study. A series of photos takes you through the work done with the
	<a href="slideshow/index.php#slicer">Geoslicer</a> at the Tule Pond site.
</p>

<h3>The Geoslicer travels across the country</h3>

<p>
	The Japanese team next brought the Geoslicer to the New Madrid seismic zone. In conjunction with the USGS, seven samples from a liquefaction site near Blytheville, Arkansas were pulled out on October 22nd.
</p>

