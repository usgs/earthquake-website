<p>
  <strong>A large, widely damaging earthquake will occur on the Hayward fault in the future. That much we know.<br/> What we don't know is when.</strong>
</p>

<p>
  Most trenches across the Hayward fault were excavated to meet the requirements of the <a href="http://www.consrv.ca.gov/CGS/rghm/ap/index.htm">Alquist-Priolo Earthquake Fault Zoning Act</a>, to insure that structures are not built over active fault traces. Other trenches have been used by scientists to learn about the fault's capability as a source of earthquakes. Earthquake geologists probe in to the fault's past behavior using the tools of paleoseismology. Using radiocarbon analysis to date these past earthquakes,  scientists have shown that these large earthquakes occur roughly every 100 to 200 years on the Hayward fault.
</p>

<p>
  On the <a href="map.html"> Hayward fault</a>, perhaps the best and most accessible trench site is at <a href="topo_map.html" rel="ibox&amp;width=335&amp;height=610" width=330,height=494>Tule Pond</a>, also known as Tyson's Lagoon, just south of the Fremont BART (Bay Area Rapid Transit) station in Freemont, California. At this site, the 1868 M<sub>w</sub>6.8 earthquake is revealed in the trench by intensely-contorted, near-surface layers of silty sand and clay. A series of photos</font> take you through <a href="slideshow/index.php#dig"> digging of the trench</a> at Tule Pond.
</p>

