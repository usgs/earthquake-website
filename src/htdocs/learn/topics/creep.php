<p>
	Creep is steady fault movement, varying from continuous to episodic with creep events lasting minutes to days. Generally creep occurs without any associated earthquake activity (i.e., aseismic.) Creep has been monitored on the Hayward fault for fifty years (Lienkaemper et al., 2012) and is also observed along some sections of other faults in the San Francisco Bay region, including the San Andreas, Calaveras, Concord-Green Valley and Maacama (<a href="images/Creep_BSSA03.pdf">Galehouse and Lienkaemper, 2003</a>).
</p>

<h2>Deep Creep and Earthquakes</h2>
<p>
	How deep does creep go? How can a creeping Hayward fault still produce major earthquakes? The short answer is that the depth of creep varies, from as little 2 miles deep in northern Oakland to as deep as 7 miles near the northern and southern ends of the fault. Unfortunately, this means the lower, brittle fault zone remains largely locked (<em>i.e.</em>, it's not creeping), so it's building strain which can only be relieved by major earthquakes, which occur about every 100 to 200 years. The average recurrence time for earthquakes is determined by paleoseismologists, geologists who work in trenches across the faults. They document evidence of paleoearthquakes recorded in sedimentary layers, using radiocarbon analysis to date them.
</p>

