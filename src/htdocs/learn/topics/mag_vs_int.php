		<h2>Abbreviated Modified Mercalli Intensity Scale</h2>
		<p>
		<b>I</b>. Not felt except by a very few under especially favorable conditions.
		</p>
		<p>
		<b>II</b>. Felt only by a few persons at rest, especially on upper floors of buildings.
		</p>
		<p>
		<b>III</b>. Felt quite noticeably by persons indoors, especially on upper floors of buildings. Many people do not
		 recognize it as an
		 earthquake. Standing motor cars may rock slightly. Vibrations similar to the passing of a truck. Duration estimated.
		 </p>
		 <p>
		 <b>IV</b>. Felt indoors by many, outdoors by few during the day. At night, some awakened. Dishes, windows, doors
		 disturbed; walls
		 make cracking sound. Sensation like heavy truck striking building. Standing motor cars rocked noticeably.
		 </p>
		 <p>
		 <b>V</b>. Felt by nearly everyone; many awakened. Some dishes, windows broken. Unstable objects overturned. Pendulum clocks may
		 stop.
		 </p>
		 <p>
		 <b>VI</b>. Felt by all, many frightened. Some heavy furniture moved; a few instances of fallen plaster. Damage slight.
		 </p>
		 <p>
		 <b>VII</b>. Damage negligible in buildings of good design and construction; slight to moderate in well-built ordinary structures;
		 considerable damage in poorly built or badly designed structures; some chimneys broken.
		 </p>
