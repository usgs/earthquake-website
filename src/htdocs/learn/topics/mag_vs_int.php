<p>
Magnitude and Intensity measure different
characteristics of earthquakes. Magnitude
measures the energy released at the source of the earthquake.
Magnitude is determined from measurements on seismographs.
Intensity measures the strength of shaking produced
by the earthquake at a certain location. Intensity
is determined from effects on people,
human structures, and the natural environment.
</p>

