<div class="vault-condition">
  <h3>Vault Condition</h3>
  <p>The seismometer vault is built into a hillside and has an overburden of approximately 3 meters. Piers are isolated from floor and connected to bedrock. Temperature and humidity in the vault are stable. There is a four lane highway located 1100 feet from the vault. Traffic is heavy, and includes large trucks.</p>
</div>

<div class="site-geology">
  <h3>Site Geology</h3>
  <p>The vault is on interfingering lenticular units of volcanic breccia, conglomerate, sandstone and siltstone of cretaceous age. Seismometer piers are attached to decomposed rock of these materials.</p>
</div>

