<div class="vault-condition">
  <h3>Vault Condition</h3>
  <p>On 30 Oct 1992 the very short-period seismometers (Geotech Model GS-13) were relocated in a surface vault approximately 100 meters distant from the borehole. The seismometers are located on a concrete floor poured on top of exposed granite rock. Vault is covered with about 1 meter of earth. Temperature and humidity are stable, however, strong winds cause high frequency background noise. Prior to this time the very short-period system was located in the vault which was used for the ALQ seismograph system. Please note that the coordinates and elevation for this vault are slightly different. This information can be found in the listing for station</p>
</div>

<div class="site-geology">
  <h3>Site Geology</h3>
  <p>Pensylvanian and later sediments overlying a peneplained Pre-Cambrian complex of metasediments and granitic intrusives. Borehole is drilled through 6 meters of alluvial overburden and 94 meters of fractured granite.</p>
</div>



