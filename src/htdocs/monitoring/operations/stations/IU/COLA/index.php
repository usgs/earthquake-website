<div class="site-geology">
  <h3>Site Geology</h3>
  <p>The borehole is drilled into approximately 50 meters of various types of silt and loess. A colluvium layer exists between approximately 55 and 61 meters. Beneath the colluvium to 102 meters there are various types of granitic rocks, from felsic granitic finely grained rock to, at deeper sections, a highly altered granite. From 103 meters to 122 meters lithologies range from phyllite to calc phillite to quartzite at the bottom of the borehole. The surface vault sits atop the silt and loess deposits.</p>
</div>

