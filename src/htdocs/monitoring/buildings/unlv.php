<p>
	The UNLV Science and Engineering Building has a seismic array of 21 motion sensors installed. These sensors are strategically located in each floor where the maximum building response during a ground shaking is expected, mostly at the edges. Every story has at least two seismic motion sensors oriented horizontally in two orthogonal directions to detect building motion along the reference east-west and north-south directions. Floor rotation around the vertical axis can be computed from the recordings of the two sensors pointing same direction but located at different points. The building also has a triaxial sensor located on the first floor, which records the input motions in three orthogonal directions at this level. Specifically, the building&rsquo;s instrumentation is designed to record and compute:
</p>

<ul>
	<li>The swaying and twisting of the building</li>
	<li>The time it takes seismic waves to travel from the foundations to the roof</li>
	<li>How the frame of the buildings changed during the earthquake</li>
</ul>

<p>
	Other triaxial seismic motion sensor located away from the building records free-field ground shaking without any interference with the building to provide input for USGS <a href="/earthquakes/shakemap/">ShakeMaps</a>. These graphics depict ground motion and shaking intensity after significant earthquakes, giving a visual representation of a quake&rsquo;s behavior and impact for emergency response.
</p>

<h2>Structural Health Monitoring and Damage Detection</h2>

<p>
	It is imperative to monitor the condition of UNLV&rsquo;s one of the most important buildings in near-real time in order to be able to assess its structural integrity immediately after a major catastrophic event. Structural health monitoring will be achieved by implementation of an open-source on-site data analysis system. This innovative system provides information about how buildings move and distort during shaking, and where damage likely occurred. Several independent algorithms check for variations in movements of a building frame and its vibration characteristics, for travel time of seismic waves between foundation and roof, and for exceedance of the building&rsquo;s seismic design capacity.
</p>

<p>
	The obtained real-time data is also available to UNLV&rsquo;s faculty and engineering students through an on-site computer to study and understand the building&rsquo;s vibration characteristics and response during earthquakes.
</p>

