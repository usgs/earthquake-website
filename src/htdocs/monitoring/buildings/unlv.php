<p>
	The Science and Engineering Building at the University of Nevada Las Vegas (UNLV) campus has been recently instrumented for earthquake monitoring by the National Strong Motion Project (NSMP) of the U.S. Geological Survey (USGS) in collaboration with the UNLV&rsquo;s Department of Civil and Environmental Engineering and Construction. This is the first building in Las Vegas furnished with a sophisticated seismic monitoring system to monitor its structural integrity in near real-time. The monitoring systems, combining sensing technologies with an on-site computer, show how earthquakes affect the structure, providing invaluable information that can be used to judge the safety of the building after an event and to design safer structures in the future.
</p>

<div class="figure">
	<img src="img/unlv-exterior_2x.jpg" alt="Photo of the UNLV Building" width="784" height="326" />
	<p>The Science and Engineering Building, located on the north side of the UNLV campus, is a state-of-the-art facility designed for interdisciplinary research and education.</p>
</div>

<p>
	The UNLV Science and Engineering Building instrumentation is more unique than any building instrumented before. This particular structure has been awarded the Silver rating by the Leadership in Energy and Environmental Design (LEED) program. In order to obtain a LEED rating, the building must meet environmentally responsible and sustainable design, construction, and operation standards.
</p>

<p>
	This is the first environmentally friendly building instrumented by the NSMP, and the only building monitored for earthquake shaking in Las Vegas. The intention of instrumenting this particular steel building is to see how buildings that are designed with the environment in mind, hold up to the strong lateral forces created by earthquakes. It is intended to use the obtained data from the sensors and the building to improve on environmentally friendly designs.
</p>

<h2>USGS Uses Advanced Sensing Technologies</h2>

