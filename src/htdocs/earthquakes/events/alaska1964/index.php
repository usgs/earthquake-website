<p>On March 27, 1964 at 5:36pm local time (March 28 at 3:36 UTC) a great earthquake of magnitude 9.2 occurred in the Prince William Sound region of Alaska. The earthquake rupture started approximately 25 km beneath the surface, with its epicenter about 6 miles (10 km) east of the mouth of College Fiord, 56 miles (90 km) west of Valdez and 75 miles (120 km) east of Anchorage. The earthquake lasted approximately 4.5 minutes and is the most powerful recorded earthquake in U.S. history. It is also the second largest earthquake ever recorded, next to the M9.5 earthquake in Chile in 1960.</p>

<p>The map shows the epicenter of the 1964 Great Alaska Earthquake (red star), caused when the Pacific Plate lurched northward underneath the North American Plate.</p>

