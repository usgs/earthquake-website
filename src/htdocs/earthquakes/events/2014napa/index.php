<h2>What We Know After One Year</h2>

<figure class="right w450">
  <a href="images/tectonic-setting-lg.jpg">
  <img src="images/tectonic-setting-sm.jpg" alt="regional map"/></a>
  <figcaption>
  		Mapped faults and seismicity (magnitude 3+, 1947-2007) prior to the August 24, 2014 South Napa earthquake. The 2000 M4.9 earthquake near Yountville is shown as a white star, and the South Napa earthquake is shown as a yellow star. <strong>Click image for larger version.</strong>
  </figcaption>
</figure>

<p>
  In the wee hours of Sunday morning at 3:20am, the residents and wineries of Napa Valley were shaken by a magnitude 6.0 earthquake, the largest in the San Francisco Bay Area in over 25 years, and the first since the <a href="/earthquakes/events/1906calif/">M7.9 1906 earthquake</a> to rupture on the surface. Residents in this area feel small earthquakes often, and they generally pause for a moment and then go back to whatever they were doing. But this was no small earthquake, and as the small representative sample of <a href="https://jawbone.com/blog/napa-earthquake-effect-on-sleep/">Jawbone&copy; data</a> shows, many residents had trouble going back to sleep after this one.
</p>
<p>
  In the days, weeks and months that followed, scientists collected and analyzed data from instruments that recorded the event. Many others spread out across the Napa Valley and surrounding area to collect additional data such as fault rupture expressed on the ground surface, as well as shaking induced damage to buildings and infrastructure. Data collected from <a href="/research/wasatch/collect.php">airborne LIDAR</a> were used to look for effects on the ground surface. In addition, airborne and ground-based LIDAR data were collected in areas known to have surface cracks to measure and describe what happened. The following summary is what USGS (and USGS-funded) scientists have learned from the data one year after the earthquake.
</p>

<h2>Tectonic Setting</h2>
<figure class="left w300">
  <a href="images/Napa-seis-lg.jpg">
  <img src="images/Napa-seis-sm.gif" alt="Napa seismicity 1973-2015" width=300/></a>
  <figcaption>
    Seismicity in the Napa area 1973-2015, all magnitudes.<strong>Click image for larger version.</strong>
  </figcaption>
</figure>

<p>
  First, to put the South Napa earthquake in context, the West Napa fault, which was the causative fault, is part of the large San Andreas Fault Zone (SAFZ) system. The West Napa fault extends along the western margin of the basin that underlies much of Napa Valley. The relative motion on this fault is not well known but thought to be about 1 mm/yr (0.04 in/yr). The SAFZ is not a simple, single fault, but rather a complex collection of smaller fault segments that collectively accommodate the horizontal movement between the North American Plate and the Pacific Plate.
</p>
<p>
	Sometimes a single fault segment will slip and cause an earthquake, and other times many fault segments slip in unison, causing a larger earthquake. The South Napa earthquake included a few small segments that ruptured together to produce the M6 earthquake.. The only other noteworthy earthquake in the Napa area in the recent past (since 1973) was the M4.9 Yountville earthquake on September 3, 2000. The epicenter of that earthquake was 20 km (12 mi) NNW of the South Napa epicenter, and also on the West Napa fault system. In addition, a M3.0 earthquake occurred on August 5, 2014, 19 days before the M6.0, which, in retrospect, some scientists/seismologists might consider a <a href="/learn/glossary/?term=foreshocks">foreshock</a>.
</p>

<h2 style="clear:left;">Immediate Impact</h2>

<figure class="left w300">
  <a href="images/dyfi-geocode-lg.jpg">
  <img src="images/dyfi-geocode-sm.gif" alt="DYFI map" width=300/></a>
  <figcaption>
    The Did You Feel It? map with 41,356 responses as of this writing. <strong>Click image for larger version.</strong>
  </figcaption>
</figure>

<p>
  Many California residents have the <a href="/earthquakes/dyfi/">Did You Feel It? (DYFI) webpage</a> bookmarked, so as soon as the shaking stopped, they started reporting their experience of the shaking on DYFI.  Within 1 hour after the earthquake, over 25,000 citizen scientists entered their data on the website, creating a <a href="/earthquakes/eventpage/nc72282711#impact_dyfi">map of the distribution of shaking that they felt</a>.  The highest intensities were <a href="/education/mercalli.php">MMI VII-VIII</a> in the city of Napa and extending a littler farther north.  A shaking intensity of VII-VIII is described as strong to severe, and very likely woke up all but the most heavy of sleepers. Ultimately 41,000+ citizens shared their experiences, with the farthest felt report 300 km (186 mi) to the east in Reno, NV. The <a href="/earthquakes/eventpage/nc72282711#impact_shakemap">computed instrumental intensities</a> agreed well with those reported by residents.
</p>
<p>
  The shaking caused some damage to wood-frame houses and significant damage to some commercial buildings in downtown Napa, including the 1870 courthouse. Damage to wood-frame houses was mostly broken or cracked chimneys and failure of cripple walls (see Additional Resources below), short walls in the crawl space between the foundation and the floor. A few homes even shifted off of their foundations, which can be due to the failure of the cripple walls, which drop the floor to the foundation and sometimes allow houses to slide off the foundation.
</p>
<p>
  Some residents noticed a difference in the amount of water being discharged from natural springs. The change occurred within 1 hour after earthquake, but the discharge amount returned to normal within a few days. This is not an unusual phenomenon in areas with natural springs and geothermal features. The shaking causes the shifting of below-surface features that may temporarily (and sometimes permanently) change the flow from a spring or even stop the flow. When the pressure adjusts and equilibrates with the water table, the flow will often return. For further details, see <a href="/education/groundwater.php">Groundwater Effects from Earthquakes</a>.
</p>

<figure class="right w275">
  <img src="images/aftershocks.gif" alt="aftershocks"/>
  <figcaption>Aftershocks during the first week after the mainshock. Symbol size represents earthquake magnitude, and fill color represents depth. The crosses and letters indicated cross-sections not shown in this image.</figcaption>
</figure>

<p>
  In the month after the mainshock, there were 80 aftershocks of magnitude greater than or equal to 1.8. The three largest were a M3.9 on August 26, a M3.2 a week after the mainshock on August 31, and then a M4.1 10 months later on May 22, 2015. The number of aftershocks was less than usual for an earthquake of this size, 80 during the first month following the mainshock, compared to 320 following the M6.0 in Parkfield in 2006.
</p>

<h2>Fieldwork and Data Analysis</h2>
<p>
  Scientists found that some of the fault segments that ruptured on the morning of August 24 had been previously mapped as part of the West Napa fault system, but the rupture also extended northward where no faults had been mapped before. The length of the rupture was about 12.5 km (8 mi), and scientists measured up to 46 cm (18 in) of slip. No other surface features were found, such as evidence of <a href="/learn/glossary/?term=liquefaction">liquefaction</a> or landslides. This lack of ground failure was attributed to the lack of rainfall in the area; a shallower water table would have made the surface material less stable and more prone to ground failure.
</p>
<p>
  Shallow slipping across the fault, known as afterslip, continued after the mainshock and lasted for several months.  Afterslip is a slow movement on the fault, and it is yet unclear whether it is associated with, or independent of, the aftershocks. An additional 14+ cm (5.5+ in) of slip was measured during the first 24 hours, and up to 20 cm (8 in) after 48 hours, mainly on southern half of the main fault strand.  Afterslip causes additional damage to manmade structures, such as roads, but does not release seismic waves and cause shaking.
</p>

