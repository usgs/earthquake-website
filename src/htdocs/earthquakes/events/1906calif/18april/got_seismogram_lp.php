<p> One great value of old seismograms is that we can make quantitative comparisons of 1906 with the 1989 Loma Prieta earthquake. Fortunately, the same seismograph instrument at Gottingen, Germany was operating during both earthquakes. </p>

<p> If the Loma Prieta (M~7.0) trace looks smaller, it is because the 1906 (M~7.8) earthquake released approximately 16 times more energy:</p>

<img src="images/1906_loma_prieta.gif" alt="Comparison of 1906 and Loma Prieta records at Gottingen, Germany."  />

<p>Because we have much more extensive information about the ground motion that accompanied the 1989 earthquake, we can work backwards and infer what the ground motions must have been like in 1906.</p>

<p>We expect that the shaking in the next 1906-type earthquake will probably not be much more intense than the shaking felt in 1989 near the epicenter of the Loma Prieta earthquake, but it will last longer -- perhaps 45 to 60 seconds or more. Strong shaking in the Loma Prieta earthquake lasted only about 15 seconds. In a repeat of the 1906 earthquake, the region experiencing intense shaking will also be much more widespread than in the Loma Prieta event, affecting areas all along the rupture.</p>

