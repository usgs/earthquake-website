<p> The magnitude of 1906 has been redetermined in two recent studies that used rather different approaches:</p>

<h3>M = 7.7</h3>

<p> <a href="http://pasadena.wr.usgs.gov/wald/1906/1906.html">  David Wald, Hiroo Kanamori, Donald Helmberger, and Thomas Heaton </a> (Cal Tech and USGS) studied recordings of the 1906 seismic waves made at 96 observatories around the world. Data from 12 of these observatories were of good enough quality to be digitized and modeled, and suggest a surface wave magnitude (Ms) of 7.7. They also found that most of the shaking energy came from two separate areas on the fault -- one between Point Reyes and Fort Ross to the north and the second on the San Francisco Peninsula to the south.</p>

<h3>M = 7.9</h3>

<p> Wayne Thatcher, Grant Marshall, and Michael Lisowski (USGS)  have re-evaluated the geodetic data from before and after the 1906 earthquake. The offset on the fault plane produces ground deformation over a broad region around the fault. This deformation manifests itself as changes in the angles and distances between benchmarks in geodetic networks. Using the observed changes, these scientists constructed models of the slip distribution on the fault plane in 1906. Based on the amount of slip, these models suggest a moment-magnitude (Mw) of 7.9.</p>

<h3>M = 8.3?</h3>

<p>The traditional magnitude of 8 1/4 or 8.3 comes from Richter (1958). However, the Richter magnitude scale was developed for local earthquakes recorded on high-frequency seismometers. The preferred descriptor for large earthquakes rich in low frequencies is the moment magnitude as used by Thatcher et al (above). This magnitude is directly proportional to energy release and can be obtained from analysis of broad-spectrum seismograms or from the product of the rupture area and average fault slip. Each unit step in moment magnitude is equivalent to roughly a factor of 32 in energy release, thus it would require roughly 30 1989 Mw=6.9 Loma Prieta earthquakes occurring simultaneously to equal the energy release of 1906.</p>

<h3>Why can't scientists agree on magnitudes for earthquakes?</h3>

<p> For older earthquakes, the data are often of poor quality or the few stations existing at the time may not have been in the best locations to record the information needed. There are also a number of different ways to measure the "size"
of an earthquake, which because of details of the rupture process for an individual earthquake, may not always agree,
even for recent earthquake.</p>
