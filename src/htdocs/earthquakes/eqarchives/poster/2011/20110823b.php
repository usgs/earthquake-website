    <h2>Tectonic Summary</h2>
    <p>
    The Virginia earthquake of 2011 August 23 occurred as reverse faulting on a north or northeast-striking
    plane within a previously recognized seismic zone, the Central Virginia Seismic Zone.  The Central
    Virginia Seismic Zone has produced small and moderate earthquakes since at least the 18th century.  The
    previous largest historical shock from the Central Virginia Seismic Zone occurred in 1875.  The 1875
    shock occurred before the invention of effective seismographs, but the felt area of the shock suggests
    that it had a magnitude of about 4.8.  The 1875 earthquake shook bricks from chimneys, broke plaster
    and windows, and overturned furniture at several locations.  A magnitude 4.5 earthquake on 2003,
    December 9, also produced minor damage.
    </p>
    <p>
    Previous seismicity in the Central Virginia Seismic Zone has not been causally associated with mapped
    geologic faults.  Previous, smaller, instrumentally recorded earthquakes from the Central Virginia
    Seismic Zone have had shallow focal depths (average depth about 8 km).  They have had diverse focal
    mechanisms and have occurred over an area with length and width of about 120 km, rather than being
    aligned in a pattern that might suggest that they occurred on a single causative fault.  Individual
    earthquakes within the Central Virginia Seismic Zone occur as the result of slip on faults that are much
    smaller than the overall dimensions of the zone.  The dimensions of the individual fault that produced
    the 2011 August 23 earthquake will not be known until longer-term studies are done, but other
    earthquakes of similar magnitude typically involve slippage along fault segments that are 5  15 km long.
    </p>
    <p>
    Earthquakes in the central and eastern U.S., although less frequent than in the western U.S., are
    typically felt over a much broader region. East of the Rockies, an earthquake can be felt over an area as
    much as ten times larger than a similar magnitude earthquake on the west coast. A magnitude 4.0
    eastern U.S. earthquake typically can be felt at many places as far as 100 km (60 mi) from where it
    occurred, and it infrequently causes damage near its source. A magnitude 5.5 eastern U.S. earthquake
    usually can be felt as far as 500 km (300 mi) from where it occurred, and sometimes causes damage as
    far away as 40 km (25 mi).
    </p>
