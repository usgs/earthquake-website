<h2 id="description">Description</h2>

<p>
  This catalog contains a selection of historical earthquake scenarios produced
  by the Alaska Earthquake Center. These examples illustrate the ground motions
  anticipated for a handful of earthquakes that have occurred in the past or
  are considered plausible in the future. Each of these was developed to meet
  the needs of various stakeholders to support different types of evaluation
  and planning studies. All of the scenarios are considered realistic and
  scientifically accurate, given the state of knowledge at the time they were
  produced. However, this is not a comprehensive listing of scenarios relevant
  to Alaska, nor do these examples necessarily reflect the maximum credible
  magnitude for particular fault systems. Users seeking specific, current, or
  maximum credible scenarios are encouraged to contact the Alaska Earthquake
  Center directly for assistance. 
</p>



