<h2 id="description">Description</h2>

<h3 id="event-selection">Event Selection</h3>
<p>
  This catalog is an archive of scenarios created for Northern California around 2012.
  The rupture definitions are based on version 2 of the Uniform California Earthquake Rupture
  Forecast (Field and others, 2009). 
</p>

<h3 id="ground-motion-models">Ground Motion Models</h3>
<p>
  For magnitudes greater than 5.5 the ground motion parameters are computed using the
  Boore and Atkinson (2008) model.
  For magnitudes less than than 5.5 the ground motion parameters are computed using the
  Boatwright and others (2003) model.
    
</p>


<h3 id="site-response">Site Response</h3>
<p>
  Site response was computed using the Borcherdt (1994) factors along with Vs30
  estimated from the Wills and others (2000) map, as modified by the
  Northern California Seismic System.
</p>

<h3 id="extent">Extent</h3>

<p>This catalog includes ruptures in Northern California.</p>



