<h2 id="description">Description</h2>

<p>
  This catalog includes a single earthquake scenario for the southern arm of
  the New Madrid Seismic Zone. 
</p>


<h3 id="event-selection">Event Selection</h3>

<p>
  The earthquake source parameters were selected by Thomas Pratt in coordination
  with FEMA.
</p>


<h3 id="gmm">Ground Motion Models</h3>
<p>
  The ground motions are computed using the same methods as with the 2014
  BSSC Scenario Catalog, described
  <a href="../bssc2014/index.php">here</a>.
</p>


