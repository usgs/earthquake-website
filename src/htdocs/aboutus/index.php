<p>
  Earthquakes pose significant risk to <a href="https://www2.usgs.gov/blogs/features/usgs_top_story/nearly-half-of-americans-exposed-to-potentially-damaging-earthquakes/">more than 143 million Americans</a>. The USGS is the only Federal agency with responsibility for recording and reporting earthquake activity nationwide, and maintains the <a href="/hazards/">National Seismic Hazard Model</a>. Citizens, emergency responders, and engineers rely on the USGS for accurate and timely information on where an earthquake occurred, how much the ground shook in different locations, the expected economic and human impacts, and what the likelihood is of future significant ground shaking.
</p>
<p>
  The USGS estimates that several million earthquakes occur in the world each year, although many go undetected because they occur in remote areas or have very small magnitudes. The USGS now locates about 50 earthquakes each day; 20,000 a year.  The USGS is working to improve its earthquake monitoring and reporting capabilities through the <a href="/monitoring/anss/">Advanced National Seismic System (ANSS)</a>, which is now about 40% completed. Full implementation of ANSS will result in 7,100 new instruments installed in the ground and in structures in high-risk urban areas, as well as a national backbone of modern seismic instruments covering the entire Nation. When completed, the ANSS will provide emergency response personnel with real-time information on the intensity and distribution of ground shaking that can be used to guide earthquake disaster response efforts. Similarly, information on building shaking will equip engineers with the data they need to improve future building designs to better withstand earthquake shaking.
</p>
<p>
  A new initiative of the Program is to provide earthquake <a href="/research/earlywarning/">“early warnings”</a> in California, Oregon and Washington—and eventually in other high-risk areas of the country. The USGS “ShakeAlert” production prototype system is being built with state and university partners for robust, reliable operations.  Currently, selected early adopters are developing and deploying pilot applications that allow protective actions to be taken, triggered by ShakeAlert warnings in areas with sufficient coverage. The system does not yet support public warnings but those will be generated when the system is completed.
</p>

