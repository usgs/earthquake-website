<p>
  <strong>To provide and apply relevant earthquake science information and knowledge for reducing deaths, injuries, and property damage from earthquakes through understanding of their characteristics and effects and by providing the information and knowledge needed to mitigate these losses.</strong>
</p>

<p>
  The USGS Earthquake Hazards Program of the U.S. Geological Survey (USGS) is part of the National Earthquake Hazards Reduction Program (NEHRP) led by the <a href="http://www.nist.gov/">National Institute of Standards and Technology (NIST)</a>.
</p>
<p>
  The USGS role in NEHRP is to provide Earth sciences information and products for earthquake loss reduction. The goals of the USGS' Earthquake Hazards Program are:
</p>
<ol>
  <li>
    Improve earthquake hazard identification and risk assessment methods and their use;
  </li>
  <li>
    Maintain and improve comprehensive earthquake monitoring in the United States with focus on &quot;real-time&quot; systems in urban areas;
  </li>
  <li>
    Improve the understanding of earthquakes occurrence and their effects and consequences.
  </li>
</ol>

<p>
  The USGS Earthquake Hazards Program also provides <a href="/research/external/">Research Grants </a>, which fund external cooperators through a competitive, peer-reviewed proposal process. Participants include State and local governments, the academic community, and the private sector. Priorities for both the internal and external programs are guided by the Earthquake Hazards Program's annual science planning. Program Announcements are posted through the <a href="https://www.grants.gov/">Grant.gov</a> website.
</p>
