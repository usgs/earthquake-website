<h3>March 14-15, 2011, Meeting of expert independent panel, Memphis, Tennessee</h3>

<p>
  Noting the substantial uncertainties regarding the history and future of large earthquakes in the New Madrid Seismic Zone, in January, 2011 the NEPEC convened an Independent Expert Panel on New Madrid Seismic Zone Earthquake Hazard, and charged that eight-member panel with providing a report commenting on the level of hazard posed by future large earthquakes in the region, and on priorities for future research to better constrain that hazard. The panel met by phone several times in February, March and April, and also held a two-day meeting in Memphis, Tennessee in mid-March, in which they interviewed a number of key experts having different knowledge and perspectives on the issues. The panel plans to release a draft report for public comment on or about Monday, April 4, with comments to be received by email to <a href="/static/lfs/nepec/mailto:NMSZpanel@gmail.com">NMSZpanel@gmail.com</a> no later than Sunday, April 10. The NEPEC plans to receive and review the panel's report at its April 16 meeting (see above).
</p>
