<h2>2009</h2>

<h3>November 4-5, 2009, Pasadena, California</h3>

<p>
  On November 4, NEPEC met jointly with the California Earthquake Prediction Evaluation Council (CEPEC), to review methods for rapidly estimating the probability of a large earthquake following a possible foreshock or during a swarm of seismicity, review and discuss procedures by which Council findings are to be transmitted to the USGS and to the California Emergency Management Agency (CalEMA), and discuss the format and content of earthquake advisory statements that may be composed and delivered at times of heightened concern.  Findings of an International Commission convened by the Italian government to provide advice on &ldquo;operational earthquake forecasting&rdquo; following the damaging L&rsquo;Aquila earthquake of April 2009 was also presented.  The Councils also discussed review procedures for the project intended to deliver an updated Uniform California Earthquake Rupture Forecast (UCERF) in summer 2012. On November 5, NEPEC discussed outcomes of the previous day&rsquo;s meeting, planned topics to be explored by the Council in future meetings, and received briefings on previous discussion topics, including on scientific issues related to earthquake occurrence in New Madrid Seismic Zone, and an update on the Collaboratory for Study of Earthquake Predictability.
