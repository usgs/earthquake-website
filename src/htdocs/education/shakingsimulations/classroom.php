<p>
This page contains snapshots of the video clips from a
computer simulation of ground shaking from the 1906 earthquake
with annotations of some of the key scientific
features. Teachers can use the video clips in their classroom
to help teach about seismic waves and earthquake hazards.
</p>

<h2>Concepts</h2>

<ul>
  <li>
    Earthquakes begin at a point (known as the "epicenter," when
    viewed on a map).
  </li>
  <li>
    A long section of a fault can rupture during an earthquake,
    progressively expanding away from the epicenter. The rupture
    may grow for a few seconds to up to a minute depending on
    the size of the event.
  </li>
  <li>
    The earth shakes in all different directions and modes
    during an earthquake.
    <ul>
      <li>
        Scientists categorize these different modes of shaking
        into different groups called "P-waves," "S-waves," and
        "surface waves."
      </li>
      <li>
        Different groups of waves travel through the earth at
        different speeds, with P-waves moving fastest, then
        S-waves, and finally surface waves.
      </li>
      <li>A single earthquake generates all these types of waves.</li>
</ul>
  </li>
  <li>
    The intensity of shaking you experience depends on the
    <ul>
      <li>
        amount of energy released by the earthquake (measured by
        its magnitude, and related to the distance the fault
        slips during the earthquake)
      </li>
      <li>distance you are from the fault</li>
      <li>type of soil or rock underneath you.</li>
</ul>
  </li>
</ul>

