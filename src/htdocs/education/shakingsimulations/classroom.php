<h3 class="clear" >1.7 seconds</h3>

  <figure class="right">
  <img src="./1906/images/sf1906t0017.jpg" alt="Image depicting earthquake at 1.7 seconds" class=rollover />
  <figcaption>Move your mouse over image to see the annotations.</figcaption>
</figure>

<p>
  <b>About the Epicenter</b>: Earthquake starts at 0 seconds as
  a tiny patch of the fault begins to slip. We call the location
  where the rupture begins the "epicenter" when viewed on a
  map. The fault actually begins to slip deep below the earth
  (about 10 km or 6 miles deep for the 1906 earthquake), and
  seismologists call this point below the surface the
  "hypocenter" (sometimes referred to as the "focus" in
  textbooks).
</p>
<p>
  <b>About Seismic Waves</b>: Both P and S waves are generated
  immediately and start radiating outward from the earthquake
  starting point like ripples moving out from a stone dropped
  into a pond. Earthquakes typically release less energy in
  P-waves than in S-waves, so the P-waves cause less intense
  shaking than S-waves. In this movie, the initial <font
  color="#69b3b6">P-waves</font> show up as <font
  color="#69b3b6">light blue</font> colors and <font
  color="#fca63f">S-waves</font> cause the more intense <font
  color="#fca63f">yellow-orange</font> colors to appear. Because
  P-waves travel fastest, they are the first to reach the beach
  in San Francisco. The S-waves are not far behind but don't
  appear on the map at 1.7 seconds because they are still
  traveling from the hypocenter at 10 km depth towards the
  surface, while the speedy P-waves have already emerged.
</p>

<h3 class="clear">2.6 seconds</h3>

<figure class="right">
  <img src="./1906/images/sf1906t0026.jpg" alt="image depicting earthquake at 2.6 seconds" class=rollover />
  <figcaption>Move your mouse over image to see the annotations.</figcaption>
</figure>

<p>
  <b>About Seismic Waves</b>: S-waves are now visible, but they
  lag behind the P-waves because they travel slower. The
  <font color="#69b3b6">blue</font> and <font
  color="#fca63f">yellow-orange</font> circles in the annotated
  image outline the very first P- and S-waves emitted from the
  hypocenter.
</p>
<p>
  <b>About Fault Rupture</b>: Earthquakes are often shown as a
  single dot at their epicenter, but in fact there is movement
  along an entire, long section of the fault. For the 1906
  earthquake, the San Andreas slipped over a 480-km-long section
  (300 miles) from Mendocino down to San Juan Bautista! Not all
  of the fault moves at once, however. Instead, the fault starts
  to "unzip," with the rupture growing and spreading out away
  from the hypocenter.  The thick red line in the center of the
  annotated image shows the portion of the fault that has
  slipped thus far. In the movie, watch as the 1906 earthquake
  spreads out in both directions away from the epicenter. Some
  earthquakes only grow in one direction because a portion of
  the fault might be stronger and stay locked together,
  preventing the rupture from extending in both directions.
</p>

<h3 class="clear">3.4 seconds</h3>

<figure class="right">
  <img src="./1906/images/sf1906t0034.jpg" alt="image depicting earthquake at 3.4 seconds" class=rollover />
    <figcaption>Move your mouse over image to see the annotations.</figcaption>
</figure>

<p>
  <b>About Shaking Intensity</b>: Shaking intensity is strongest
  close to the fault and dies off with distance away from the
  fault. Because only a small section of fault has slipped in
  the first 3.4 seconds, the pattern looks a lot like a bullseye
  centered around the short section of fault that has slipped so
  far (indicated by the thick red line in the annotations). As
  the fault rupture grows over time, the bullseye will
  start to elongate parallel to the fault to a more football
  shaped pattern. For example, watch the shaking intensity near
  San Mateo over time. Even though it is far from the epicenter,
  it is still close a portion of the fault that eventually
  ruptures. The same is true for all of Marin county, to the
  left (northwest) of the epicenter.
</p>


<h3 class="clear">6.2 seconds</h3>

<figure class="right">
  <img src="./1906/images/sf1906t0062.jpg" alt="image depicting earthquake at 6.2 seconds" class=rollover />
<figcaption>Move your mouse over image to see the annotations.</figcaption>
</figure>

<p>
  <b>About Seismic Waves</b>: Seismic waves are generated at
  every point along the fault as that portion of the fault
  ruptures. The thin lines in the annotations show the P- and
  S-waves generated early in the earthquake (e.g., at the
  epicenter). They continue to spread out like ripples on a
  pond. If the earthquake only emitted P-waves and S-waves at
  the beginning of the earthquake, the ripples would be perfect
  circles about the epicenter. However, additional shaking
  energy is added at the tips of the rupture, indicated by the
  thicker circles. The final, football-shaped pattern you see is
  the combination of a whole bunch of ripples generated in
  succession along the fault (like throwing a bunch of stones
  into the pond one after the other in a line along the fault).
  This is why shaking is intense all along the fault and not
  just near the epicenter.
</p>
<p>
  Surface waves travel slower than P- or S-waves. They are
  difficult to see directly in this movie because they are mixed
  together with some of the S-waves.
</p>

<h3 class="clear">16 seconds</h3>

<figure class="right">
  <img src="./1906/images/sf1906t0160.jpg" alt="image depicting earthquake at 16 seconds" class=rollover />
    <figcaption>Move your mouse over image to see the annotations.</figcaption>
</figure>

<p>
  <b>About Seismic Waves</b>: Like a track and field race where
  one runner is faster than the other, the P-waves keep getting
  further and further ahead of the S-waves so that the distance
  between them gets greater as time goes on. Also, the shape of
  the wave fronts (thin lines in annotations) is not perfectly
  smooth. This is because seismic waves travel at different
  speeds through different types of rock. P-waves are always
  faster than S-waves, but all the waves get slowed down in some
  materials. Think of this as the difference between running on
  a smooth track (hard rock, like granite) versus thick mud
  (loose, poorly compacted sediments and soils).
</p>
<p>
  <b>About Shaking Intensity</b>: Near Tomales Bay, the dark
  colors indicate extremely strong shaking. In this case, the
  intensity is due to the fact that in the 1906 earthquake more
  energy was released in that section of the fault than along
  any other stretch. Different portions of the fault can move
  different amounts in the same earthquake. Here, the two sides
  of the fault slid more than 30 feet, while the slip at the
  epicenter was only about 12 feet (<a href="slipmodel.php">See
  a map of how much the fault slipped</a>). The epicenter only
  indicates where shaking starts -- not where shaking is most
  severe. The huge amount of movement near Tomales Bay released
  a huge amount of seismic energy, resulting in violent
  shaking.
</p>

<h3 class="clear">30 seconds</h3>

<figure class="right">
  <img src="./1906/images/sf1906t0300.jpg" alt="image depicting earthquake at 30 seconds" class=rollover />
    <figcaption>Move your mouse over image to see the annotations.</figcaption>
</figure>

<p>
  <b>About Shaking Intensity</b>: Seismic waves dissipate as
  they travel through the earth, so shaking is usually most
  severe closest to the fault. However, some types of rock and
  soil amplify shaking for the communities built on top of
  them. An excellent example is the city of Santa Rosa, which
  experienced very serious damage in 1906 (Download a
  classic photo of the <a
  href="./images/santarosacityhall.jpg">destroyed city hall</a>. When these soft
  soils extend deep down and are surrounded by harder rocks (a
  "sedimentary basin"), the waves get trapped and slosh around
  like waves in a bathtub, making shaking last longer and damage
  more severe. The dark red colors reflect the strong shaking in
  the soft Santa Rosa basin. A similar effect is visible in a
  valley above Richmond in the image.
</p>
<p>
  This computer simulation does not include the shallowest layer
  of soils such as Bay muds that surround the margin of the San
  Francisco Bay (because our computers are not yet big and fast
  enough to handle these small features). The actual observed
  shaking was much more severe around the Bay than these
  simulations show. Take a closer look at the <a
  href="/earthquakes/events/1906calif/shakemap/">actual
  shaking intensity on the Bay margins during 1906</a>.
</p>
<p>
  See a 3-D perspective movie with a closeup of Santa Rosa,
  below.
</p>

<h3 class="clear">After 30 seconds</h3>

<p>
  <b>About Fault Rupture</b>: The rupture for the 1906
  earthquake continues to expand along the fault for 54 seconds
  to the south and 90 seconds to the north. It stops when it
  hits a section of the fault that is strong enough to stop the
  rupture. That section will eventually need to slip in future
  earthquakes. Smaller magnitude earthquakes sooner, before the
  rupture has propagated very far. Scientists are still trying
  to learn more about what causes ruptures to stop, so they
  don't fully understand why some earthquakes are big and some
  are small.
</p>
<p>
  <b>About Shaking Intensity</b>: Shaking at any given location
  can continue for a long time for two reasons:
 <ul>
    <li>
      Waves continue to arrive from distant parts of the
      fault. Because these waves travel so far, these are less
      intense by the time they arrive.
    </li>
    <li>
      Seismic waves trapped in sedimentary basins continue to
      slosh around like waves in a bathtub.
    </li>
  </ul>
</p>
<p>
  Both of these effects diminish with time, but they can still
  cause damage to structures that were weakened by earlier
  intense shaking.
</p>

