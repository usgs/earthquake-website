<h3 style="clear: left;">Activity: Calculating Wave Velocities</h3>

<p>
  The box labeled "Watch here" is about 66.9 kilometers (45.6
  miles) from the earthquake epicenter. Calculate the speed of
  each type of wave by the following process (answers in table
  above):
</p>
<ul>
  <li>
    Record the precise time when each wave arrives. This is
    easiest for P- and S-waves, which are recognized by the
    change in colors.
  </li>
  <li>
    Convert the distances and times to the desired units. Your
    measured time is in seconds. If using meters per second,
    convert the distance into meters. If using miles per hour,
    convert the time into hours (should be a small fraction of
    an hour).
  </li>
  <li>
    Distance = Velocity * Time. So, calculate the velocity by
    dividing the distance by the time.
  </li>
  <li>
    Consider the meaning of these numbers. A person walks
    about 1 m/s and a bullet travels about 1000 m/s (~2200
    mph). These waves are literally "faster than a speeding
    bullet!."
  </li>
  <li>
    How much warning time would you have of approaching strong
    S-wave shaking if you lived 10 km from the epicenter of a
    large earthquake? 50 km? 100 km? [<a
    href="http://rallen.berkeley.edu/research/eew/elarms.php">Read more about earthquake early
    warning</a>]
  </li>
</ul>
<p>
  To calculate these velocities, you have to assume that the
  waves you see were generated at time 'zero', which means that
  they are coming directly from the earthquake epicenter and not
  from other parts of the earthquake rupture. Is this a good
  assumption? (Not really -- waves are generated along the
  entire length of the rupture, so their "starting time" will
  vary. Seismologists use computer simulations, such as the ones
  shown here, to understand the details of this complex
  shaking.)
</p>
