<p>
A new U.S. Geological Survey and Oklahoma Geological Survey analysis found that
145 earthquakes of magnitude 3.0 or greater occurred in Oklahoma from January
2014 (through May 2; see accompanying graphic).  The previous annual record, set
in 2013, was 109 earthquakes, while the long-term average earthquake rate, from
1978 to 2008, was just two magnitude 3.0 or larger earthquakes per year. Important
to people living in central and north-central Oklahoma is that the likelihood of
future, damaging earthquakes has increased as a result of the increased number of
small and moderate shocks.
</p>

<p>
Oklahoma’s heightened earthquake activity since 2009 includes 20 magnitude 4.0 to
4.8 quakes, plus the largest earthquake in Oklahoma’s history – a magnitude 5.6
earthquake that occurred near Prague on Nov. 5, 2011. The 2011 Prague earthquake
damaged a number of homes and the historic Benedictine Hall at St. Gregory's
University in Shawnee. Prior to the 2011 Prague earthquake, the largest earthquake
of Oklahoma’s history was a magnitude 5.5 earthquake that occurred in 1952 near
El Reno and damaged state buildings in Oklahoma City.
</p>

<p>
“While it’s been known for decades that Oklahoma is
<a href="http://earthquake.usgs.gov/earthquakes/states/?region=Oklahoma">
‘earthquake country’</a>, we hope
that this new advisory of increased hazard will become a crucial consideration in
earthquake preparedness for residents, schools and businesses in the area,” said
Dr. Bill Leith, Senior Science Advisor for Earthquakes and Geologic Hazards at
USGS. “Building owners and government officials should have a special concern for
older, unreinforced brick structures, which are vulnerable to serious damage
during sufficient shaking.”
</p>

<p><a href="http://bssa.geoscienceworld.org/content/103/5/2850.full">
USGS statistically analyzed</a> the recent earthquake rate changes and found that
they do not seem to be due to typical, random fluctuations in natural seismicity
rates.  Significant changes in both the background rate of events and earthquake
triggers needed to have occurred in order to explain the increases in seismicity,
which is not typically observed when modeling natural earthquakes.
</p>

<p>The analysis suggests that a likely contributing factor to  the increase in
earthquakes is triggering by wastewater injected into deep geologic formations.
This phenomenon is known as injection-induced seismicity, which has been
documented for nearly half a century, with new cases identified recently in
Arkansas, Ohio, Texas and Colorado.  A <a href="http://onlinelibrary.wiley.com/doi/10.1002/2013JB010612/abstract">
recent publication by the USGS</a> suggests
that a magnitude 5.0 foreshock to the 2011 Prague, Okla., earthquake was
human-induced by fluid injection; that earthquake may have then triggered the
mainshock and its aftershocks. OGS studies also indicate that some of the
earthquakes in Oklahoma are due to fluid injection. The OGS and USGS continue
to study the Prague earthquake sequence in relation to nearby injection activities.
</p>

<p>Collaborative USGS and OGS research to understand earthquake rate increase in
the central Oklahoma area includes quantifying the changes in earthquake rate,
assessing the implications of the increased small and moderate earthquake
activity for large earthquake hazards, and evaluating possible links between
these earthquakes and wastewater disposal from oil and gas production
activities. The OGS is also focused on seismicity in north-central Oklahoma.
</p>

<p>To more accurately determine the locations and magnitudes of earthquakes in
Oklahoma, the OGS has increased the number of monitoring stations and now
operates a seismograph network of 15 permanent stations and 17 temporary
stations, many of which are on loan from the USGS. There are also three
permanent seismic stations operated by the USGS and the Incorporated Research
Institutions for Seismology. Data from this network are shared in real-time with
the USGS National Earthquake Information Center, which provides 24x7 reporting
on earthquakes worldwide.
</p>

