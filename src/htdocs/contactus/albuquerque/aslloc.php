<p>
Visiting the ASL requires access through Kirtland Air Force Base.&nbsp; Please contact us for an appointment if you would like to visit the lab.
</p>
<p>
After passing through the Gibson gate, stay in the right-hand lane and turn right at the first traffic light (this is Pennsylvania Ave).&nbsp; After turning right, get into the left-hand lane and stay there.&nbsp; You will go through two 3-way stop signs, and then through another traffic light.&nbsp; Stay on Pennsylvania and eventually you will cross a bridge (no water, just a bridge).&nbsp; Drive approximately 3 miles&ndash;you will pass a golf course on your left and then a stable.&nbsp; A little farther down Pennsylvania will be the intersection of Pennsylvania and Lovelace Road.&nbsp; Turn right on Lovelace Road (you will see a sign just before the turnoff).
</p>
<p>
Drive another 3.4 miles.&nbsp; You will see two large white water towers up ahead on the left hand side.&nbsp; Just before the water towers turn left onto Target Road (if you pass the water towers you have gone too far).&nbsp; Drive 2.5 miles on Target Road until you come to our security gate.&nbsp; Call your host to have the gate opened.&nbsp; When the gate opens, follow the drive around to Building 10002.
</p>
<p>
Be sure to obey all traffic signs and speed zones on the base.&nbsp; Cell phone usage by the driver is forbidden on the base unless a hands-free device is used.
</p>
