<h2>Directions to the USGS Pasadena Office</h2>

<p><strong>From the 134/210 Freeway:</strong></p>
<p>
	Take the 134 East and follow the signs to Pasadena (210E).<br/>
  Exit at Lake Ave. and go right (South) for several city blocks.<br/>
  Turn left on Del Mar.<br/>
  Continue below from Del Mar.
</p>
<p><strong>From the 110 Pasadena Freeway:</strong></p>
<p>
	Take the 110N until it ends in Pasadena.<br/>
  Keep going straight (on Arroyo which is what the freeway turns into).<br/>
  Turn right (East) on Del Mar.<br/>
  Continue below from Del Mar.<br/>
</p>
<p>From Del Mar:</p>
<p>
	Turn right on Wilson Ave. Go through the stop sign at San Pasqual, and the
	office is a few houses down on the right. The address is 525 S. Wilson and the
	house is yellow with black shutters.<br/>
	The &quot;Geological Survey&quot;
	house is #39 on the <a href="http://www.caltech.edu/map">Caltech campus map</a>.
 </p>
