    <p class="description">
      These gridded text files contain S<sub>S</sub> and S<sub>1</sub>
      parameter values from the <cite>2012 International Building
      Code</cite>. Please note that these values will not exactly match
      those provided by the <cite>U.S. Seismic Design Maps</cite>.
      The maps interpolate the underlying
      probabilistic and deterministic ground motions, as well as the risk
      coefficients, before calculating S<sub>S</sub> and S<sub>1</sub>.
      Should you choose to interpolate the gridded values provided below,
      you will be doing so after the S<sub>S</sub> and S<sub>1</sub>
      calculations have already been performed at the grid points. Proceeding
      with this order of operations will produce results that slightly differ
      from those calculated in the Design Maps. The data below are provided as a
      reference. For the specific gridded data sets underlying the Design Maps,
      which can be used to recreate the Design Maps, please consult the
      <cite>2009 NEHRP Recommended Seismic Provisions</cite> data section.
    </p>
