    <p class="description">
      These gridded text files contain S<sub>S</sub>, S<sub>1</sub>, PGA,
      C<sub>RS</sub>, and C<sub>R1</sub> parameter values from the <cite>2010
      ASCE 7 Standard</cite>. Please note that these values will not exactly
      match those provided by the <cite>U.S. Seismic Design Maps</cite>.
      The Maps interpolate the underlying
      probabilistic and deterministic ground motions, as well as the risk
      coefficients, before calculating S<sub>S</sub> and S<sub>1</sub>.
      Should you choose to interpolate the gridded values provided below,
      you will be doing so after the S<sub>S</sub> and S<sub>1</sub>
      calculations have already been performed at the grid points. Proceeding
      with this order of operations will produce results that slightly
      differ from those calculated by the <cite>Design Maps</cite>. The data
      below are provided as a reference. For the specific gridded data sets
      underlying the <cite>Design Maps</cite>, which can be used to recreate the
      <cite>Design Maps</cite> results, please consult the <cite>2009 NEHRP
      Recommended Seismic Provisions</cite> data section on this page.
    </p>
