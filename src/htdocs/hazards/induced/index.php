    <p>
      The U.S. Geological Survey (USGS) has produced a 1-year seismic hazard
      forecast for 2016 for the Central and Eastern United States (CEUS) that
      includes contributions from both induced and natural earthquakes. The
      model assumes that earthquake rates calculated from several different time
      windows will remain relatively stationary and can be used to forecast
      earthquake hazard and damage intensity for the year 2016. This assessment
      is the first step in developing an operational earthquake forecast for the
      CEUS, and the analysis could be revised with updated seismicity and model
      parameters. Consensus input models consider alternative earthquake catalog
      durations, smoothing parameters, maximum magnitudes, and ground motion
      estimates, and represent uncertainties in earthquake occurrence and
      diversity of opinion in the science community.
    </p>
    <p>
      Ground shaking seismic hazard for 1-percent probability of exceedance in 1
      year reaches 0.6 g (as a fraction of standard gravity [g]) in northern
      Oklahoma and southern Kansas, and about 0.2 g in the Raton Basin of
      Colorado and New Mexico, in central Arkansas, and in north-central Texas
      near Dallas. Near some areas of active induced earthquakes, hazard is
      higher than in the 2014 USGS National Seismic Hazard Model (NHSM) by more
      than a factor of 3; the 2014 NHSM did not consider induced earthquakes. In
      some areas, previously observed induced earthquakes have stopped, so the
      seismic hazard reverts back to the 2014 NSHM. Increased seismic activity,
      whether defined as induced or natural, produces high hazard. Conversion of
      ground shaking to seismic intensity indicates that some places in
      Oklahoma, Kansas, Colorado, New Mexico, Texas, and Arkansas may experience
      damage if the induced seismicity continues unabated. The chance of having
      Modified Mercalli Intensity (MMI) VI or greater (damaging earthquake
      shaking) is 5â€“12 percent per year in north-central Oklahoma and southern
      Kansas, similar to the chance of damage caused by natural earthquakes at
      sites in parts of California.
