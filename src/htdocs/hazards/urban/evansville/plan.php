<p align="center">
	<strong><font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">Scientific Work</font></strong>
</p>
<p align="left">
	<em><font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">I. Introduction</font></em>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">The basic input parameters to high-resolution seismic hazard map calculations include information
  about 1) earthquake sources, 2) ground motion attenuation, and 3) near surface materials. For the
  first two of these it is critical to use the same input as used in the USGS national seismic hazard
  maps. The difference between urban hazard maps and the national hazard maps is in the third input,
  which this project will provide. The near surface geological materials, and the 3-dimensional
  variation in their thicknesses and physical properties, either amplifies or de-amplifies the level
  of earthquake ground shaking and lengthens or shortens its duration. They also affect the
  potential for ground failures to occur. A goal of seismic hazard mapping is to forecast these
  effects. The Memphis, Seattle, and other urban hazard mapping projects have developed the
  methodology for mapping, or forecasting ground motions and liquefaction susceptibility.
  However, as noted below, we anticipate new discovery and development.</font>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">In this context 'near surface' refers to different depths, depending on the type of map (ground
  motion, liquefaction or landslide susceptibility). The key controlling characteristics of the
  rocks and sediments also depend on the map type, but some are common to all. We discuss these
  common inputs below, and then those specific to each type of map in separate sections.</font>
</p>
<p align="left">
	<em><font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">II. Surficial and 3-D geologic maps</font></em>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">The general state-of-the-art is to assume that relevant characteristics of near surface
  materials (or soil in engineering terms) vary with their geologic classification or lithology.
  If such an assumption is true, it is useful because surficial geologic maps and 3-dimensional
  pictures of the lithology can be made at higher resolution than one can map most other material
  properties. Thus, 3-D geologic maps can be used as proxies, or interpolation tools, for
  mapping the physical properties. The appropriateness and accuracy of using such proxies
  determines the accuracy of the maps, and undoubtedly varies regionally and locally. In
  addition to the geologic mapping itself, a major effort must be dedicated to establishing the
  degree to which the lithology correlates with the relevant material properties.</font>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">Surficial geologic maps showing the distribution of geologic units, particularly the
  unconsolidated materials, will be generated using standard mapping techniques. Because
  the surficial geologic mapping work will involve several geologists, efforts must be made
  to insure uniformity in mapping style, format, etc. All maps, data etc. will be entered
  into the GIS of each Survey.</font>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">A geologic mapping sub-committee consists of Rich Harrison, Dave Moore, John Hill,
  Ron Counts, and Drew Andrew. All field mappers met on the field trip in late
  October/early November 2003, primarily for the purpose of deciding on how to do mapping
  uniformly.</font>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">Data constraints on the stratigraphy come primarily from surface geologic mapping and from
  logs of various types, from which the depths to lithologic boundaries are estimated. The
  Indiana Geological Survey has an ARC-GIS water-well database (ILITH) that will soon be
  expanded to include hydro-carbon wells. Engineering logs have been lost but they're
  committed to collecting them again and entering them into the database. If needed,
  additional data might be acquired using Indiana Geologic Survey personnel, auguring and
  gamma logging, sedimentological testing, and SPT measurement equipment. The Kentucky
  Geologic Survey (Ron Counts) has acquired engineering and other log data as part of
  the surficial mapping effort.</font>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">The basic analysis for deriving stratigraphic boundaries requires fitting discontinuous
  surfaces to point measurements of the boundary depths. A number of possible fitting
  procedures may be applied, as well as structural interpretations that are compatible with
  the surficial geology and basic geologic principles. Once we have determined what data
  exist, have collected and interpreted them (including measures of their accuracy), we will
  determine if and where additional information is needed. Once answered, plans for
  additional data collection and analyses can be made and executed.</font>
</p>
<p align="left">
	<em><font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">III. Ground motion maps</font></em>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">To estimate determinsitic and probabilistic ground motions that include the effects of
  shallow rocks and sediments we will follow the analysis procedure described in Cramer
  (2003). The key input information includes the shear-wave velocity (Vs),
  compressional-wave velocity (Vp), initial damping (Qs, Qp), and density of the
  sub-surface materials, how these vary in 3-dimensions, and measures of the accuracy of
  these characteristics. For more complex (non-linear) soil response calculations, additional
  soil properties will need to be known (geotechnical properties such as soil class (sand
  versus clay), plasticity, porosity, water saturation, and dynamic soil properties).
  'Shallow' in this context refers to depths extending to the base of the unconsolidated
  sediments. Minimally certain characteristics of the rock just beneath the sediments
  also should be known (Vs, Vp, density, and damping).</font>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">Currently the Indiana Geological Survey has ~35 Vs profiles (to bedrock), all in Indiana.
  The Kentucky Geological Survey has some shear velocity profiles. The CUSEC State Geologists
  have a new NEHRP project to assess what Vs data exist (for Evansville and elsewhere),
  and to put them in a database that will reside at the Illinois Geological Survey. Bob
  Bauer and Norm Hester are Project Chiefs. No information on density exists thus far.</font>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">Because measurements of the above characteristics are relatively costly to make, we will begin
  by asking whether they correlate with lithology. Once the above data gathering is complete,
  and using the surficial and 3-D lithologic maps also being generated, we will assess if and
  where additional shear-wave velocity, compressional-wave velocity, and density measurements
  should be made to be able to assess their correlation with lithology, and devise a strategy
  for collecting them. Should more profiles be needed, the Illinois and Kentucky Surveys have
  downhole Vs logging equipment that might be used. Whether assessing the utility of existing
  data or designing new data collection tasks, questions regarding the resolution and accuracy
  required must be considered along with those pertaining to resources, scheduling, data processing
  and archiving, etc.</font>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">State-of-the-art ground motion estimation considers the non-linear response of sediments to
  ground shaking. Non-linear effects may amplify or reduce ground motions relative to those
  motions estimated assuming that the output motion is simply proportional to the motion input
  to the base of the sediments. For sufficiently large input ground motions, the non-linear
  sediment response is thought to limit or cap the motion at the surface. While this implies
  a lower ground motion hazard, severe non-linearity ultimately may result in ground failure.
  Without question, predictive models of the non-linear response are highly uncertain, both in
  terms of the underlying theory and the input parameters that must be measured in the field.
  The crudest approach to include non-linear affects employ 'site-amplification factors' (e.g.
  those recommended in the 2000 NEHRP seismic provisions), which are standard multiplicative
  factors that depend on the gross sediment characteristics and input ground motion amplitude
  and frequency. More complete approaches account for the specific properties of the sediments
  and their underlying physical behaviors. In addition to requiring shear-wave and
  compressional-wave velocities and densities, such approaches also require specification of the
  'modulus reduction' and 'damping' properties of the sediments, which describe how the sediments
  lose strength and dissipate energy with shaking, respectively. These parameters vary with
  depth, sediment type, etc. and are extremely difficult to measure. However, some modern
  geotechnical tools exist for doing so. Use of these should be considered as well as
  compilation of whatever relevant information might exist to constrain these properties.</font>
</p>
<p align="left">
	<em><font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">IV. Liquefaction susceptibility maps</font></em>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">Susceptibility here refers to the inverse of the 'capacity' of the sediments to maintain their
  strength when shaken (i.e. to not liquefy). The liquefaction potential depends both on the
  capacity and on the 'demand', or the shaking levels the sediments are likely to experience.
  Thus, liquefaction potential maps combine ground motion and liquefaction susceptibility maps.
  Our strategy to mapping the susceptibility makes direct use of both geologic and geotechnical
  information, rather than only one as is often done in more traditional approaches. As for
  the ground motion parameters, this work focuses on establishing the relationship between the
  lithology and the properties that control liquefaction capacity.</font>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">Susceptibility is best estimated from cone penetrometer test (CPT) measurements, and also may
  be derived from standard penetration test (SPT) measurements that generally are more abundant
  but are of poorer or unknown quality. For a specified ground motion amplitude and duration,
  standard analyses are applied with the CPT or SPT measurements to derive a 'factor of safety'
  profile and then a 'liquefaction potential index' (LPI). LPI is a measure of potential to
  liquefy (from none to major liquefaction, 0 to 15). All the LPIs estimated for each surficial
  geologic unit are combined to determine probability density functions. These thus provide
  measures of the correlation of LPI and geologic unit, or equivalently the probability of
  liquefaction for each unit. These correlations or probability density functions then allow
  the geologic maps to be transformed into probabilistic liquefaction susceptibility maps.</font>
</p>
<p align="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">In addition to considering compilation of existing and collection of new CPT and SPT measurements,
  information about grain size and other geotechnical properties, and the ground water table are
  needed. In contrast to the ground motion mapping, 'shallow' in this context refers to the
  top few tens of meters and geologic units must be distinguished with greater resolution (e.g.,
  different types of fill may have very different susceptibilities while ground motion estimates
  are completely insensitive to these).</font>
</p>
