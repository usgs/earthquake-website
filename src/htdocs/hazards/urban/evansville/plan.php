<em><font size="-1" face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">(Last update, September 2008)</font></em></div>
<p align="left" text-alig="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">One type of seismic hazard map depicts the expected level of ground shaking caused by seismic waves and how that level varies spatially. Urban seismic hazard maps differ from the USGS national seismic hazard maps in that they are higher resolution and they account for the effects of the shallow rocks, sediments, and topography on earthquake ground shaking (i.e. site effects). These maps typically show the motion at a single frequency or parameter and for a particular probability of being exceeded during a particular time span. Such maps are probabilistic, and explicitly consider the likelihood and potential contribution of all possible earthquakes, a range of possible seismic wave propagation characteristics and site effects, and account for observational uncertainties. Deterministic or scenario maps, showing the ground motion for a single set of input parameters (e.g. a specific earthquake at a particular location, etc.), can be derived from a subset of the information used to make probabilistic maps. In addition to ground motion, another type of seismic hazard maps may show the spatially variable likelihood of ground failure (such as landslide or soil liquefaction), by combining the potential of materials to lose strength or fail when shaken and the level of shaking expected.</font>
</p>
<p align="left" text-alig="left">
	<font face="Trebuchet MS, Geneva, Arial, Helvetica, SunSans-Regular, sans-serif">Generating useful high resolution seismic hazard maps is both a technical and non-technical process. The maps and derivative products should be useful in meeting the needs of a broad constituency and thus will be widely advertised, disseminated and updated. These primarily non-technical aspects of mapping must be accomplished in close coordination with the technical work.</font>
</p>
