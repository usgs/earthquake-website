<p>
  This website contains information on faults and associated folds in the United States that are believed to be sources of M&gt;6 earthquakes during the Quaternary (the past 1,600,000 years). Maps of these geologic structures are linked to a database containing detailed descriptions and references. The database is intended to be the USGS&#8217;s archive for historic and ancient earthquake sources used in current and future probabilistic seismic-hazard analyses.
</p>

<p>
  Our website presents&#8212;for the first time&#8212;a single source that summarizes important information on paleoseismic (ancient earthquake) parameters. These data are compiled from thousands of journal articles, maps, theses, and other documents, as referenced herein. The database is designed to serve a variety of needs, both in terms of the user community and methods of delivering the data.
</p>

<hr/>

<h2 id='history'>History</h2>
<p>
  Starting in the early 1970s, mainly in response to national concerns about the siting of nuclear reactors, scientists needed to locate active and Quaternary faults and document their characteristics. This broad research initiative resulted in new maps and studies of Quaternary faults. State (<a href="#4876">Jennings, 1975</a>; <a href="#312">Witkind, 1975</a>; <a href="#312">1975</a>; <a href="#819">1975</a>; <a href="#2792">1976</a>; Menges and Pearthree, 1983 #2073</a>), regional (<a href="#147">Nakata and others, 1982)</a>, and National <a href="#312">Howard and others, 1978)</a> maps were compiled to show the location and relative timing of major faults. These map compilations, however, did not provide much supporting data. Subsequent state-scale compilations, such as those by <a href="#259">Johns and others, (1982)</a>, <a href="#242">Stickney and Bartholemew (1987)</a>, and <a href="#642">Hecker (1993)</a> provided some supporting database and were the first true fault compilations. The Quaternary fault and fold data for the United States has relied heavily on the past contributions, and on new efforts by State geological surveys and the U.S. Geological Survey.
</p>
<p>
  The most recent effort began in 1990 in support of the International Lithosphere Program (ILP), which formed Working Group II-2. Its main objective was to compile a World Map of Active Faults (Vladimir Trifonov, chairman). In 1992, the USGS agreed to help compile maps and fault descriptions for countries in the Western Hemisphere (North, Central, and South America, as well as Australia and New Zealand). This work continues to date, with many of the compilations for Central and South America countries having been published.
</p>
<p>
  In 1993, the U.S. Geological Survey began developing a database for Quaternary faults and folds for the United States in earnest, largely supported by NEHRP but with significant support from many State surveys. This product is more robust than the ILP products, mainly owing to the vast amount of data that has become available within the U.S. in the past 20-30 years and the importance that this data plays in regional and national seismic-hazard assessments (<a href="#4860">Petersen and others, 1996</a>; <a href="#5248">Frankel and others, 1996</a>).
</p>

<p>
  The descriptions contain information on geographic, geologic, and paleoseismic parameters that are deemed critical to making geologic-based assessments of seismic hazards. In addition, we provide narrative comments that clarify, justify, or expound upon these parameters. Many of the comments in the database provide justification for the paleoseismic parameters that were chosen to characterize the faults and folds.
</p>

<h3>Reference Materials</h3>
<p>
  For this compilation, we have limited our compilation to synthesis of published literature relevant to the United States. Our definition of published literature includes typical sources (journals and maps), as well as M.S. theses and Ph.D. dissertations, governmental contract reports (which includes many NEHRP-sponsored studies), abstracts, and open-file (preliminary) reports. We generally do not cite unpublished field mapping, field notes, and other gray-literature reports that are not generally available to the public. The data presented in the compilation are extensively referenced using the standard USGS reference style, with the exception of attaching a unique number to each cited reference for convenience. This numeric identifier allows us to clearly cite multiple-same year publications for authors.
</p>

<h2 id='class'>Fault Classes</h2>
<p>
  This table includes the definitions of classes used in the compilation of Quaternary faults, liquefaction features, and deformation (Crone and Wheeler, <a href="#260">2000</a>).
</p>

<table>
  <thead>
    <tr>
      <th>Class Category</th>
      <th>Definition</th>
    </tr>
  </thead>
  <tbody>
    <tr>
      <td>Class A</td>
      <td>
        Geologic evidence demonstrates the existence of a Quaternary fault of tectonic origin, whether the fault is exposed for mapping or inferred from liquefaction or other deformational features.
      </td>
    </tr>
    <tr>
      <td>Class B</td>
      <td>
        Geologic evidence demonstrates the existence of a fault or suggests Quaternary deformation, but either (1) the fault might not extend deeply enough to be a potential source of significant earthquakes, or (2) the currently available geologic evidence is too strong to confidently assign the feature to Class C but not strong enough to assign it to Class A.
      </td>
    </tr>
    <tr>
      <td>Class C</td>
      <td>
        Geologic evidence is insufficient to demonstrate (1) the existence of tectonic fault, or (2) Quaternary slip or deformation associated with the feature.
      </td>
    </tr>
    <tr>
      <td>Class D</td>
      <td>
        Geologic evidence demonstrates that the feature is not a tectonic fault or feature; this category includes features such as demonstrated joints or joint zones, landslides, erosional or fluvial scarps, or landforms resembling fault scarps, but of demonstrable non-tectonic origin.
      </td>
    </tr>
  </tbody>
</table>

<h2 id='uses'>Potential Uses</h2>
<p>
  Although seismicity maps and earthquake catalogs show the past 100 to 150 years of felt and instrumental earthquakes, many faults in the United States have return times of thousands to tens of thousands of years for surface faulting events. Clearly the short seismic record will not image all the active faults that exist. Thus, this collection of data on faults and folds that record ancient earthquakes will help augment the rather short felt and instrumental seismic record that is typical of the United States and other recently developed countries.
</p>
<p>
  The database is primarily a text-based collection of descriptive data that will serve a wide and varied audience. The search capabilities described below will allow the user to sort the data on a variety of fields (geographic, structural, time of movement, slip rate, etc.). The basic strategy in classifying the data has been to create a variety of bins (categories) to characterize these potential seismic sources in terms of their activity rates. You can sort the data by time of most recent movement (4 inclusive categories) or slip rate (4 exclusive categories). For example, if you want to see the youngest and most active prehistoric faults, one would search for faults that have moved in the past 15,000 years at rates&gt;5 mm/yr.
</p>

<h2 id='search'>Search Strategies</h2>
<p>
  The database has two search forms. The <strong>Quick Search</strong> form is very simple with only four search options available. Two options permit searches on <em>Name</em> and <em>Number</em> of a particular fault or fold. The other two options permit geographic searches by <em>State</em> and <em>County</em>.
</p>
<p>
  The <strong>Advanced Search</strong> form can be used to further limit the search results. The Advanced Search form allows queries on the above four parameters and on geographic options, paleoseismic characteristics, and structural characteristics. The additional geographic search options are <em>AMS sheet</em> and <em>physiographic province</em>. The searchable paleoseismic characteristics include <em>time</em> of most recent prehistoric deformation, <em>year</em> of historic deformation, and <em>slip-rate</em> category. Searchable structural characteristics include <em>length</em> of fault or fault section, <em>average</em> <em>strike</em> of fault or fault section, <em>sense of movement</em>, and <em>dip direction</em> of the fault.
</p>
<p>
  Complete as few or many of the fields as you wish. The narrower the search; the quicker the results will be available. If you expect that your search will result in a large number of results (more than 40), you can reduce the amount of time to obtain those results by limiting the number of results on each page at the bottom of the search form.
</p>
<p>
  There are three basic types of search fields: those with (1) pull-down menus, (2) text, and (3) numeric. The fields having pull-down menus provide all available options. Text fields like Name, County, and AMS sheet are not case sensitive and will search on partial words. The numeric fields such as Number, Year of historic deformation, Length, and Average strike should only contain numeric expressions. The Number field will not find specific sections (a, b, c, etc.); instead search for the fault number only. The Year of historic deformation requires a four-digit year in each field; use values that would encompass the historical record (such as 1600 and 2005) to search for all entries in this field. The length and average-strike searches will yield all records with inclusive values and show all sections of a fault if one of those sections has the desired value.
</p>
<p>
  Many modern web browsers have an &#8220;auto complete&#8221; function that will fill in data fields based on the first several characters or digits from your browser profile. You should turn off this feature prior to conducting searches that require entering characters or digits.
</p>

