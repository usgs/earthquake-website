<h2>Disclaimer</h2>

<p>
	Although it is anticipated that the new 2008 USGS
	hazard data will be the basis for the seismic design maps
	in future editions of the NEHRP Recommended Provisions, the ASCE/SEI 7
	Standard, and the International Building and Residential Codes, the values
	on those seismic design maps are expected to be materially
	different from the values on the 2008 hazard maps. More
	specifically, it is anticipated that &hellip;
</p>

<ol>
	<li>
		the USGS hazard values will be amplified by factors that convert
		(approximately) to spectral response accelerations in the maximum
		direction of ground motion,
	</li>
	<li>
		these factored hazard values will be multiplied by &ldquo;risk
		coefficients&rdquo; that adjust for a target <em>risk</em> (namely
		1&#37; probability of collapse in 50 years), and
	</li>
	<li>
		these factored and adjusted hazard values will be capped near known
		active faults by so&ndash;called deterministic ground motions (like the
		USGS hazard values were for the design maps in current editions of the
		model building codes).
	</li>
</ol>

<p>
  <strong>
	Thus, the 2008 USGS hazard maps should not be substituted for
	the model building code design maps nor should they be used with ASCE/SEI 41
	or 31 for seismic rehabilitation or evaluation.
  </strong>
</p>

