<h2 id='format'>Data File Formats</h2>

<h3>&ldquo;Hazard Curves&rdquo; Files</h3>
<img src="images/1HzCurve.snip.gif" alt="Snapshot of 1Hz Curve File" />
<p>
  These files contain hazard curves for the gridded geographic points as
  described above. All the hazard curves in the file share the same set of
  x-values (intensity measure levels). The first two lines tell you that
  these files contain data for both the western <em>and</em> central and
  eastern United States. The third line specifies the spectral period (in
  seconds) used when computing the hazard curves for the file. The following
  lines (with 1 value per line) contain the x-values (intensity measure
  levels) for all the curves in the file. Subsequent lines contain hazard
  curves with a single geographic point per line. The first two values
  specify the latitude and longitude (respectively) for the curve while
  subsequent values (on that same line) contain mean annual frequencies of
  exceedance ordered corresponding to the x-values at the top of the file.
</p>
</div>

<div class="formatDesc">
<h3>&ldquo;X&#37; in 50 Years&rdquo; Files</h3>
<img src="images/1Hz10pc50.snip.gif" alt="Snapshot of 10pc50 Ground Motions" />
<p>
  These files contain ground motion values for the gridded geographic points
  as described above. The ground motion units are in <em>g</em> where 1g =
  980.5 cm/s/s. Data contained in these files
  specify a single geographic point per line starting with longitude and
  latitude values (respectively), with the third and final value of each
  line specifying the ground motion for that geographic point.
</p>
