<p>
	Probabilistic seismic hazard maps have been prepared for Alaska portraying
	ground motion values (peak ground acceleration and spectral amplitude at
	periods of 0.2, 0.3 and 1.0 seconds) at probabilities of exceedance of 2%
	and 10% in 50 years. Preparation of these maps followed the same general
	strategy as that followed for the U.S.G.S. seismic hazard maps of the
	contiguous United States, combining hazard derived from spatially-smoothed
	historic seismicity with hazard from fault-specific sources.
</p>
<h2 id="2007">2007</h2>

<p>
	We present here time-independent probabilistic seismic hazard maps of Alaska and the Aleutians for peak ground acceleration (PGA) and 0.1, 0.2, 0.3, 0.5, 1.0 and 2.0 second spectral acceleration at probability levels of 2 percent in 50 years (annual probability of 0.000404), 5 percent in 50 years (annual probability of 0.001026) and 10 percent in 50 years (annual probability of 0.0021). These maps represent a revision of existing maps based on newly obtained data and assumptions reflecting best current judgments about methodology and approach.
</p>

