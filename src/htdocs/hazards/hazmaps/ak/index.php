<h2 id="1999">1999</h2>

<p>
	In this release of hazard maps we present eight (8) different maps with varying parameters. The computations for these maps and data used 50 year return periods on firm rock at 760 m/sec. All of the maps and data were prepared by combining hazard derived from spatially- smoothed historic seismicity with hazard from fault-specific sources. For more information please read our full documentation.
</p>

