		<h2>Abstract</h2>
		<p>
			We present here time-independent probabilistic seismic hazard maps of
			Alaska and the Aleutians for peak ground acceleration (PGA) and 0.1, 0.2,
			0.3, 0.5, 1.0 and 2.0 second spectral acceleration at probability levels
			of 2 percent in 50 years (annual probability of 0.000404), 5 percent in 50
			years (annual probability of 0.001026) and 10 percent in 50 years (annual
			probability of 0.0021). These maps represent a revision of existing maps
			based on newly obtained data and assumptions reflecting best current
			judgments about methodology and approach. These maps have been prepared
			following the procedures and assumptions made in the preparation of the
			2002 National Seismic Hazard Maps for the lower 48 States. A significant
			improvement relative to the 2002 methodology is the ability to include
			variable slip rate along a fault where appropriate. These maps incorporate
			new data, the responses to comments received at workshops held in
			Fairbanks and Anchorage, Alaska, in May, 2005, and comments received after
			draft maps were posted on the National Seismic Hazard Mapping Web Site.
			These maps will be proposed for adoption in future revisions to the
			International Building Code. In this documentation we describe the maps
			and in particular explain and justify changes that have been made relative
			to the 1999 maps.
		</p>
		<p>
			We are also preparing a series of experimental maps of time-dependent
			hazard that will be described in future documents.
