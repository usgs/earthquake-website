		<h2>Abstract</h2>
		<p>
			Probabilistic seismic hazard maps have been prepared for Alaska portraying
			ground motion values (peak ground acceleration and spectral amplitude at
			periods of 0.2, 0.3 and 1.0 seconds) at probabilities of exceedance of
			2&#37; and 10&#37; in 50 years. Preparation of these maps followed the
			same general strategy as that followed for the USGS seismic hazard maps
			of the contiguous United States, combining hazard derived from
			spatially-smoothed historic seismicity with hazard from fault-specific
			sources.
		</p>
		<p>
			Preparation of the Alaska maps presented particular challenges in
			characterizing the hazard from the Alaska-Aleutian megathrust. In the maps
			of the contiguous United States the rate of seismicity for recognized
			active faults was determined from slip rates estimated from geologic data.
			This approach is not appropriate for the megathrust because it has been
			demonstrated that a significant fraction of the subduction occurs
			aseismically. The characteristic earthquake hypothesis, based on
			recurrence rates determined from geologic data, is appealing for the
			portion of the megathrust that ruptured in the 1964 Alaskan earthquake,
			but is shown to be inappropriate for the western portion of the megathrust
			by the recent large earthquakes in the region which did not follow the
			characteristic model.
		</p>
		<p>
			Consequently the hazard from the western portion was estimated based on a
			truncated Gutenberg and Richter model derived from historic seismicity,
			and the hazard for the 1964 zone was estimated from a combination of a
			Gutenberg and Richter model derived from historic seismicity and the
			characteristic earthquake hypothesis with recurrence rates estimated from
			geologic data. Owing to geologic complexity and limited data, hazard
			models of the easternmost portion of the megathrust in the vicinity of
			Yakataga are the least satisfactorily constrained. Hazard is estimated for
			the recognized crustal faults of the Denali, Fairweather-Queen Charlotte
			and Castle Mountain fault systems based on available geologic slip rates.
			Hazard from other sources is estimated from spatially smoothed historic
			seismicity. Disaggregations of the hazard for Anchorage, Fairbanks and
			Juneau reveal the dominant sources of the hazard at each location.
		</p>

		<h2>Introduction</h2>
		<p>
			Alaska is the most seismically active state in the United States, and in
			1964 the site of one of the largest earthquakes since the beginning of
			instrumental recording.  Although the current population of the region is
			small by comparison with, say California, the consequences of a large
			earthquake in the region could be much greater now than at the time of the
			1964 Alaskan earthquake.  The probabilistic seismic maps we have prepared
			are intended to extend those prepared by Frankel et al. (1996) for the 48
			contiguous states, and with soon-to-be-published maps for Hawaii.  Our
			methodology follows the basic approach of Cornell (1968).  These maps are
			intended to summarize the available quantitative information about seismic
			hazard from geologic and geophysical sources.  Full color maps at a scale
			of 1:7,500,000 are available as U.S. Geological Survey Miscellaneous
			Investigations Series I-2679 (Wesson et al., 1999).
		</p>
		<p>
			The process of preparing these maps included a workshop held in Anchorage
			in the fall of 1996 attended by many scientists and engineers involved in
			aspects of earthquake research and structural engineering practice in
			Alaska.  Preliminary calculations of hazard were presented for discussion
			and a number of recommendations were made that affected the subsequent
			preparation of the map.  Draft maps were posted on the World Wide Web
			and circulated for comment in the fall of 1997.  The maps presented here
			have benefited greatly from both the original workshop in 1996 and from
			the review comments received.
		</p>
		<p>
			The strategy for preparing these maps is similar to that for recently
			prepared seismic hazard maps of the contiguous United States (Frankel et
			al., 1996).  The maps presented here include maps for peak ground
			acceleration and 1.0, 3.3 and 5.0 Hz spectral acceleration at
			probabilities of exceedance of 10% in 50 years (annual probability of
			0.0021) and 2% in 50 years (annual probability of 0.000404).
		</p>
		<p>
			The historical instrumental seismicity of Alaska and the Aleutians for
			earthquakes greater than or equal to magnitude 6 is shown in Figure 1.
			The preparation of the earthquake catalogs used for the analyses in
			this report (and shown in Figure 1) is discussed in a companion report,
			Mueller et al., 1998).  Clearly the majority of the seismicity in the
			region is associated with the Alaska-Aleutian megathrust fault
			extending eastward along the Aleutian arc into south central Alaska.
			The northwestward-moving Pacific plate is subducted along this
			megathrust beneath the North American Plate giving rise to the Aleutian
			trench and islands.  Additional significant seismicity occurs along the
			northwestward-striking system of right-lateral strike-slip faults
			extending southeastward through and offshore of the panhandle of
			southeast Alaska.  This system of faults forms the northeast boundary
			of the Pacific plate.  Additional seismicity occurs in central Alaska.
		</p>
		<p>
			The estimated rupture zones of the largest earthquakes in this century
			are shown in Figure 2 (Plafker et al., 1993).  During this century
			virtually the entire plate boundary from the westernmost Aleutian
			Islands to the Queen Charlotte Islands off British Columbia has
			ruptured in large to great earthquakes.  The only exceptions are areas
			near the Komandorsky Islands, near the Shumagin Islands,  and near
			Cape Yakataga (Sykes, 1971; Davies et al., 1981).  Near the
			Komandorsky Islands, historical records of large earthquakes in 1849
			and 1858 at the extreme western end of the arc have been judged as
			insufficient to conclude that plate-margin-rupturing earthquakes have
			occurred there (Sykes et al., 1981; Taber et al., 1991).  At this
			location subduction is occurring at a highly oblique angle, and it has
			been argued that the recurrence properties of large earthquakes here
			may differ significantly from those else along the arc.  Indeed,
			Cormier (1975) has argued that the region may be incapable of
			supporting a great earthquake.  In the vicinity of the Shumagin
			Islands, that is, in the region between the 1957 and 1938 earthquakes,
			it has been argued that no great earthquake has occurred in this
			century.  Similarly, the vicinity of Cape Yakataga has experienced no
			great earthquakes in this century.   These two regions have been
			identified as "seismic gaps," that is, the potential sites of future
			large earthquakes (Davies et al., 1981; Sykes, 1971, Lahr et al.,
			1980).
		</p>

