<h2 id='hi'>Hawaii</h2>
<p>
  In this release of hazard maps we present eight different maps with varying parameters. The computations for these maps and data used 50 year return periods on firm rock at 760 m/s. All of the maps were prepared by combining hazard derived from spatially-smoothed historic seismicity with hazard from fault-specific sources.
</p>

